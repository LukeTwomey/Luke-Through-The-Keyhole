                    <p>This was definitely one of the nicer places on my trip, and still only about £16 per night. If you can bear to sacrifice some privacy, then hostels are definitely the way forward when you're travelling somewhere in future. In fact, most hostels also have private rooms available anyway.</p>

                    <p>When you walked up the dirt track to the main road, that's all there was for miles in either direction - tarmac. Yet again, without my own transportation I was slightly scuppered. Fortunately (as you would expect with the location of the hostel), the main attractions for Punakaiki (the Pancake Rocks and blowholes) were within walking distance of the hostel. It just meant walking along the road all the way there... nice views though.</p>

                    <a href="images/posts/punakaiki/punakaiki-7_8584967066_o-large.jpg" class="fancybox">
                        <img src="images/posts/punakaiki/punakaiki-7_8584967066_o.jpg"/>
                    </a>

                    <p>After half an hour or so, I arrived at the main visitor centre for the area, and the local tearoom. I managed to resist going in for a snack, as I knew everything in there would have been incredibly expensive, and money was getting very tight by that stage! The walk down to the rocks from there is short, and you're soon by the edge of the cliff looking at fantastic views over the water.</p>

                    <a href="images/posts/punakaiki/punakaiki-12_8585036166_o-large.jpg" class="fancybox">
                        <img src="images/posts/punakaiki/punakaiki-12_8585036166_o.jpg"/>
                    </a>

                    <a href="images/posts/punakaiki/punakaiki-13_8583945829_o-large.jpg" class="fancybox">
                        <img src="images/posts/punakaiki/punakaiki-13_8583945829_o.jpg"/>
                    </a>

                    <p>The "pancakes" themselves (the layers in the rock) were formed by immense water pressure on dead sea creatures and plants on the seabed. This was then lifted out of the water by seismic activity over a long period of time.</p>

                    <p>I wasn't able to get there when the blowholes were active – around high tide, but from what I'd read online, you weren't even guaranteed to see them in action even if you did get there at that time. It also depended on the roughness of the sea.</p>

                    <p>Satisfied with my pancake fill, I headed back along the main road to get to the hostel. They had a main building there with a communal TV room and kitchen – like you'd get in every hostel, but much nicer in another log cabin style arrangement. I spent the rest of the day in there, and by the time I left to wander along to my own cabin to sleep, it was dark. I was walking along the winding path through the woods, when I heard a noise up ahead. As I got closer, my light picked up what I think was a possum stopped dead in its tracks in front of me, staring. We both looked at each other in this standoff for a few seconds, before it scarpered up the nearest tree! I'm glad it wasn't bigfoot. That would have been a whole lot more scary.</p>

