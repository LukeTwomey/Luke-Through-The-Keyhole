                    <p>From here we walked a short distance to our next stop, Tham Hoi (Snail cave). On the way there we passed a young girl and her mother harvesting crops of peanuts in their field. A few words from our guide, and a handful were passed over for us to try. I can confirm, they tasted like peanuts.</p>

                    <a href="images/posts/vang-vieng/vang-vieng-14_7823972862_o-large.jpg" class="fancybox">
