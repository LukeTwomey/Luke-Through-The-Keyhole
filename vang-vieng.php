                    <p>Snail cave went much further in, and we were walking for a good twenty minutes, lighting the way with head torches before heading back to the entrance. It was in this one that we saw a snake - apparently the first time the guide had even seen one in there - and a huge spider! Indiana Jones eat your heart out.</p>

                    <a href="images/posts/vang-vieng/vang-vieng-3_7822550320_o-large.jpg" class="fancybox">
                        <img src="images/posts/vang-vieng/vang-vieng-3_7822550320_o.jpg"/>
                    </a>

                    <p>Than Loup (Deep cave) was next, with stalagtite and stalagmite formations decorating the interior. This wasn't in anywhere near the same league as Kong Lo cave which I'd visited before, but still impressive nonetheless. It wasn't really possible to get photos inside these two caves, as there was no light whatsoever apart from the head torches. On a couple of occasions we would all turn these off and stand in silence, and it was a very surreal experience, not a chink of light or the slightest sound within.</p>

                    <p>Outside the cave our guide Mang pointed out a plant that the locals chop up and use in a soup. They drink the soup and if it tastes horrible and mildly burns their mouth and throat, they know they don't have Malaria! However, if the liquid tastes sweet to them, they need to get their arse to a hospital sharpish.</p>

                    <a href="images/posts/vang-vieng/vang-vieng-4_7822863278_o-large.jpg" class="fancybox">
                        <img src="images/posts/vang-vieng/vang-vieng-4_7822863278_o.jpg"/>
                    </a>

                    <p>We trekked to Tham Nam (Water) cave, and were able to experience the infamous Vang Vieng tubing, albeit on a tamer level. At this cave, it was time to strip to our swimwear, float out on a tube and pull ourselves through on a rope affixed to the walls of the cave. This one was easily the most fun! We got to a point in the cave where the water ended and the tubes washed up on a sandy beach. Leaving them behind, we moved further along and the roof of the cave got steadily lower, until we were forced onto our stomachs to wriggle through army style! This part only lasted for around five metres as we emerged into another more spacious section of the cave, and entered the water once again. It felt colder without the tube, as more of your body is submerged. It also slowly rose higher as we moved further in, until Mang said it was not safe to continue due to the fast flowing water we could now hear ahead of us. We returned to the tubes, no longer requiring the rope, as we were carried along at a steady speed back to the cave entrance by the flow of the water.</p>

                    <a href="images/posts/vang-vieng/vang-vieng-15_7824021188_o-large.jpg" class="fancybox">
                        <img src="images/posts/vang-vieng/vang-vieng-15_7824021188_o.jpg"/>
                    </a>

                    <p>After emerging from the cave, we sat down to eat lunch before trekking to our next destination, a traditional Hmong village. Naturally though, they had a stall set up to sell snacks to the tourists! I don't tend to like visiting villages like this, as it feels like a big invasion of privacy, so I didn't take any photos here. The villagers are clearly used to it though, and take full advantage where they can.</p>

                    <p>We carried on, through stunning scenery, until we met up with the songthaew again and were driven to a point on the river where we could kayak 9km back to town. Sadly I don't have any photos of this activity, for fairly obvious reasons. It's quite wet when you get out on the water. It gets even wetter when you capsize just after coming out of some "rapids" (very tame ones, like you see in the photo). And wetter still when the guide in the other kayak deliberately tips you over for a second time further downstream!</p>

