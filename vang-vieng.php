                    <h1>Vang Vieng</h1>
                    <p class="post-date">26th August 2012 10:53</p>

                    <p>No, I didn't go tubing. Shoot me.</p>

                    <p>Tubing involves floating down the Nam Song river in a tyre inner tube, grabbing hold of ropes thrown from riverside bars along the way, being pulled in to get suitably pissed, and high, before floating downstream to repeat the process at the next establishment.</p>

                    <p>Now what I've come to realise on this trip, is that actually I'm an old, old man who really wouldn't enjoy such shenanigans. Give me a night slowly getting drunk in a bar/pub, with decent music, chatting randomness with mates, over binge drinking till I can't control my actions any day. Oh yeah, no dancing either... dancing is the work of the devil.</p>

                    <p>A story I heard which perfectly exemplifies the type of person you get going tubing, is one of a nice young lad who got completely wasted at one of the bars. In his merry state, he decided it would be fun to jump into the river from the deck of the bar (plenty of the bars have this kind of setup, some also with rope swings and slides out into the water). One of the bar staff explained to the gentleman that it would be inadvisable to jump into the water, as it was rather shallow in that area and he was likely to do himself some harm. The guy turned round and punched the member of staff, before jumping into the water anyway, only to resurface completely motionless and float off down the river... plenty of people die in similar incidents each year, I heard a number around twenty five last year being mentioned, I don't know how accurate this is.</p>

                    <p>I set about finding other activities to keep me amused while I was in town. Heading to the local Green Discovery office (these guys run tours throughout Laos, and you'll usually find a branch in one of the places you stop at), I asked if any tours had already been booked by other people, which will always lower the price you pay. They had a one day tour for the next day, which two people had already booked onto, halving the price automatically. Perfect! Sign me up...</p>

                    <p>The songthaew arrived at 9am sharp (yeah right!) the next day, and we drove around picking up the rest of the group. More people had obviously booked after me, as we had five people in total, promising a fun day out. Driving around twenty minutes outside town we reached our first stop, Elephant Cave. This cave is a Buddhist temple and has a couple of Buddha statues, as well as a limestone formation resembling an elephant, hence the name.</p>

