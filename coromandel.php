                    <h1>Coromandel</h1>
                    <p class="post-date">22nd June 2013 15:53</p>

                    <p>I was reaching the end of my time in New Zealand. My penultimate stop took me to the North East of the north island, in Coromandel. As it turns out, I was actually in the wrong part – I should have gone to Coromandel Peninsular, which is closer to the attractions in the area. No big deal though – it just meant going on a shuttle bus tour for the day with a couple of other people. In actual fact, I think it still would have been difficult to get around all the places if I'd been in the right area anyway, they were still fairly spread out.</p>

                    <p>The hostel I was staying at was not the usual type I'd become accustomed to. I was still sleeping in a dorm, that was still the cheapest bed there - but it was located on a campsite. An overwhelming number of people in New Zealand hire campervans to tour the country in. For me, travelling alone it wasn't really a viable option as it would have been way too expensive. But for many, it's a fantastic way to see the country. Taking full advantage of this, there are grounds all over New Zealand that provide an open patch of grass with electricity points and whatnot for them all to park up and plug in overnight. Individual chalets dotted about also made it feel very much like a holiday park! There were beautiful views of the surrounding area from the grounds – but that's something you're pretty much guaranteed with most places you go to!</p>

