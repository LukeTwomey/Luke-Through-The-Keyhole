                    <p>After enjoying the water and the boats, and treating myself to an ice cream... I crossed on the ferry to meet the shuttle to drive to our next stop, Hot Water Beach. This is quite a funny one, because as you walk along the sand there are very few people on there, but in the distance you can see a huge swarm of bodies all jostling for position in one small section of the shoreline.</p>

                    <a href="images/posts/coromandel/img_9768_8674116687_o-large.jpg" class="fancybox">
