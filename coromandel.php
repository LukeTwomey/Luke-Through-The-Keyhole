                    <p>As we continued our journey in the bus, we were informed of a pizza place that we would be driving past, called Luke's Kitchen. Now as some of you may know, I love pizza just a tiny, little bit... so I just had to buy one from a place called Luke's Kitchen, didn't I?! We stopped off to pick up a menu. The plan was to phone them up to order when we started driving back, and pick it up on the way. Something to look forward to for the evening then!</p>

                    <p>Back to the task at hand... and we had arrived at Whitianga, where there is a very small passenger ferry which crosses the bay. Our guide dropped us off here to explore for a while in our own time, while she drove round the long way and arranged to meet us on the other side. I settled down by the water to enjoy the view and tuck into my packed lunch (yes, that's how organised I am!).</p>

