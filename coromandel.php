                    <p>The reason is because at this one point on the beach the water under the sand gets heated up by the thermal activity in the ground, and by digging down into the sand you can make your own little hotpool to lie in and relax! It sounds easy in principle – but when you actually get there you realise it's a logistical challenge!</p>

                    <p>First you need to find a spot to start digging – no small feat in itself, due to the huge number of people there! When you do eventually find a spot, you need to dig for quite a while before you have anything even remotely resembling a pool to lie in. The chances then are quite high that your pool will either be too hot, or too cold – so you then need to try and construct a channel from someone else's pool to steal all their hot water! Even after you've got the temperature bang on, you can't yet lie back and enjoy your work – as you need to build up a big wall at the front in an attempt to stop any rogue waves that decide to ruin the spa atmosphere!</p>

                    <p>All in all, it's great fun, if a little hard work! And to be honest, it's not exactly a pool you end up with – more of a puddle... but really cool nonetheless!</p>

