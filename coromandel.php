                    <p>I made a point to go to a recommended restaurant to try raw oysters and mussels – as the area is well known for the quality of this food they produce. I'd never tried them before, so I was willing to at least give it a go – although I must say, when I got in there the BBQ ribs for the same price on the menu were extremely tempting! The mussels came in a tomato like sauce, and the oysters just came with balsamic. I didn't dislike them – but I probably wouldn't be ordering them again, put it that way. After finishing both dishes (and still feeling hungry!), I almost regretted not ordering the ribs! But I was glad I tried them at least. I guess I'm just not a seafood person!</p>

                    <p>Back at the campsite, what I hadn't banked on was the wailing of a loud air raid siren later that evening! Whilst running around screaming with my arms flailing around in search of an air raid shelter, I was told it's used by the fire service and is completely normal. It's only now, having just looked it up while writing this, that I've discovered it's specifically used to summon the town's volunteer fire fighters, as there is no permanent department on standby for any emergencies. It was completely surreal. And it was LOUD. Why they can't just give them beepers I don't know...</p>

                    <p>The following day I was up early for the tour shuttle bus pickup. We first stopped off at a viewpoint which gave us a great 360 degree panorama of the area. As views, go it wasn't too shabby!</p>

