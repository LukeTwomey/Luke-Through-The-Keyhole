                    <h1>Singapore</h1>
                    <p class="post-date">14th November 2012 07:51</p>

                    <p>Possibly one of the nicest places I've been to so far. Everything is clean and modern, and the transport system makes it very easy to get around. It is more expensive than other southeast Asian countries, but it's easy to see why. It's a very pleasant place to be.</p>

                    <p>Once again I was staying in Chinatown, at Matchbox The Concept Hostel. This place was really funky. Slightly more expensive than I would usually have paid but worth it just for the experience. The dorm beds aren't your typical bunk beds, you sleep in a self contained pod, with your own light and power points. The common area was well designed, with a huge television, DVD collection, bean bag chairs, foot massager and swings (not shown in this photo I stole!). A very unique experience and definitely recommended if you ever visit the country.</p>

