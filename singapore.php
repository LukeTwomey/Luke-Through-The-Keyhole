                    <p>Singapore zoo came very highly rated, so it was a must see for me while I was there. Unfortunately, the weather took a turn for the worse in the middle of my visit, with very heavy rain and thunder and lightning. But after an hour or two, this soon passed by and I was able to see the rest of the animals. I've really been very lucky with rain, it has in fact not been the "ideal" time of year to visit a lot of the places I've been to, but even in rainy season it's not stopped me from being able to do what I wanted to do in each place. If the rain does come, it usually only hangs around for an hour or two.</p>

                    <p>The zoo was home to a pair of white tigers, which I have to say were probably my favourite animals! I also discovered there is a pair of pandas resident in the zoo, but having only just arrived they are being allowed to settle into their new environment, before going on display to the public in December. Very disappointing, but you can't win them all!</p>

