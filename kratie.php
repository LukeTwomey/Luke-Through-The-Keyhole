                    <h1>Kratie</h1>
                    <p class="post-date">11th August 2012 10:21</p>

                    <p>The main draw of Kratie (krat-chay) is the opportunity to see the endangered Irrawaddy dolphin up close. They live in very specific areas of the Mekong river, and Kratie is one of the places where you can pay for a boat to take you out on the river to catch a glimpse of these rare mammals.</p>

                    <p>According to everything I'd read, the best time to see them was early in the day, or late in the afternoon (I assume they head out to the shops to get a few groceries around lunchtime). However, I had it on good authority from the owner of the guesthouse I stayed at that I could safely go around 11 to noon and they'd still make an appearance.</p>

                    <p>As the boat steadily powered it's way out to the middle of the river where the dolphins are known to congregate, I honestly had no idea what to expect. If I'm honest after their first appearance, my initial reaction was one of disappointment. I heard a 'whoosh' of air behind me, quickly turning around to briefly see a shape disappear beneath the water around fifty metres away. We repeated this process a few times, with me starting to wonder if this would be worth the money.</p>

                    <p>Why were they not jumping around like lunatics, splashing me with their flippers and balancing balls on their noses like "normal" dolphins? I knew I should have packed a beach ball and a hoop...</p>

                    <p>As time went on though, thanks to the efforts of the boatman slowly manoeuvring us with the oars, the dolphins gradually grew closer and closer, to the point where they were only a few metres away from the boat. Seeing them up close like this was pretty special, and I soon forgave them for being flat out lazy.</p>

                    <p>Taking photos of these guys was nigh on impossible. There was absolutely no way of knowing where they would pop up next. The best chance you had was focusing the camera where they last surfaced and hoping they came up there again. I'm just glad I have a digital camera, not film, as I ended up with countless shots of plain, boring water as I was too slow to catch the buggers.</p>

                    <p>The closest encounters came just as we were about to head back to dry land. A few metres away from the boat, the dolphins would do their usual trick of blowing some air, then resubmerging, but thanks to some Dr. Doolittle style copycat "whoosh" noises by the boatman, they seemed to hang around at the surface for a little longer. This continued for ten more minutes or so, with me half expecting this guy to don a rubber dolphin suit and jump in with them. He didn't, much to my disappointment, but it was still really enjoyable all the same. </p>

