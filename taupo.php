                    <p>The second group that had booked the time after ours was actually unable to jump. They were given the option of getting taken back into town or waiting around to see if it would clear. Throughout the day it rained on and off though, so I was very lucky to have booked the first jump and actually been able to go ahead with it.</p>

                    <p>I can easily recommend skydiving to anybody. You'll enjoy it, I guarantee you. Even if you're terrified at first, after doing it you'll not regret it. It's amazing. Just don't forget the parachute.</p>

