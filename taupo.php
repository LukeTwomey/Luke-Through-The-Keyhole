                    <h1>Taupo</h1>
                    <p class="post-date">3rd June 2013 22:15</p>

                    <p>In Taupo, no one can hear you scream. Apart from your tandem master, who I'd imagine would have some fairly major hearing problems by the end of their career.</p> 

                    <p>It's well known throughout the backpacker circuit that Taupo is the place to do your skydive. Not only is it a great view while you're jumping, but it's cheaper than other places in New Zealand - presumably as so many people go there. Also only one person goes SPLAT per week, so your chances of survival are pretty good.</p>

                    <p>Having already booked my jump online before I arrived, I was dismayed to arrive on the coach the day before to cloudy skies and rain. In these conditions, it's impossible for the jump to go ahead as you need visibility of the ground while you are falling (for obvious reasons). </p>

                    <p>Naturally I was hoping for the best the following morning as my alarm went off. When I arose it was cloudy, but not as bad as it had been the previous day. As a matter of course, you are instructed to phone the skydive company some time before you are due to be picked up by their shuttle. When I phoned the first time they asked me to call back in ten minutes, as they were waiting to see if the cloud cover that was over the airfield currently would clear with the wind. Ten anxious minutes later, I redialled their number. The cloud had moved... the jump was on. There was no getting out of it now!</p>

                    <p>Arriving at the airfield, I finally got to see what I would be *gulp* jumping out of. It didn't even look like it could take off, let alone climb to 15,000 ft!</p>

