                    <p>After around one minute of freefall, another tap on the shoulder told me to grab hold of my shoulder straps again, and our descent was immediately arrested as the parachute was deployed. You can get a feel for just how quickly this happens by watching the cameramen in the video continue falling further down before opening their own chutes.</p>

                    <p>After what felt like complete chaos from the moment we exited the plane, a deafening silence took over and for the first time I could properly take in what was going on. I was worried that with some cloud still in the sky, it would spoil the view somewhat, but even with cloud in places the view was spectacular. It was completely unique too, obviously at no time would I ever be at that vantage point again, and it really was something else - as you can see from the stupid grin on my face!</p>

