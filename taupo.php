                    <p>Elad passed me the handles for the parachute, and let me steer us around for a while, going all over the place, which was so much fun. As we grew closer to the ground, he took control again and made us go into a steep spiral downwards which was awesome! We levelled out again, and directly below us was the landing spot where we could already see the first guys had arrived. We descended steadily, and as we approached the ground I lifted my legs as instructed and we came back to earth with a very smooth landing!</p>

