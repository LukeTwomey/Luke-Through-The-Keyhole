                    <p>The adrenaline rush is amazing. Compared to the bungy, it was actually less scary believe it or not. I think it's because you're further away from the ground so you don't really have a perception of the height you're at. Overall it was definitely more fun than the bungy though, as it lasts longer so you have more time to appreciate it.</p>

                    <p>Once again I got extremely lucky with the weather, as it was like this shortly after we landed:</p>

