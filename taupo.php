                    <p>At first things are, quite literally, a blur. Despite wearing goggles, the air still rushed around the edges and made my eyes water. The noise is huge, as air blasted past my ears. I felt a tap on my shoulder, an indication that I should extend my arms to make things easier for Elad. Up until this point, you are instructed to hold onto your shoulder straps.</p>

                    <p>I soon spotted the cameraman in front of us, who grew closer and began waving his arms around in front of him, as if trying to signal me to do something. This continued for most of the time we were free falling, hence the confused expression on my face for the majority of the video! Luckily I'd opted for the photos package too, so I could at least pick out some normal photos from those (or as normal as you could ever look falling through the air at approximately 120mph!).</p>

