                    <p>After getting kitted up, and meeting my tandem master Elad (aka crazy person), I began my final walk to the plane. In total there were around twenty people on there, and it was a really tight fit. That was really the last thing on my mind though!</p>

                    <a href="images/posts/taupo/07_GOPR9113-large.jpg" class="fancybox">
                        <img src="images/posts/taupo/07_GOPR9113.jpg"/>
                    </a>

                    <p>It takes about 15-20 minutes to get to the right height. You have a choice of jumping from either 12,000 or 15,000 ft. Everyone in our group had opted to jump at the higher altitude, so we had no delays in getting up there. Packed in like sardines, the noise from the engines drowns out any last minute prayers you may be making. As we got closer to the jump height, we were handed an oxygen mask and pumped full of gas. One would logically assume this is because the cabin isn't pressurised, and the oxygen levels are lower as you climb higher. You could also quite easily believe it's just to "take the edge off" and make you a little more compliant when your turn to jump comes around!</p>

                    <a href="images/posts/taupo/09_GOPR9142-large.jpg" class="fancybox">
                        <img src="images/posts/taupo/09_GOPR9142.jpg"/>
                    </a>

                    <p>The 15 minutes were soon up. I was second in line to jump. The roller hatch was pulled up, and the guys in front scooted forward on their arse. Turning 90 degrees, they were then dangling precariously on the edge. The cameraman clambered out of the opening and was literally hanging off the side of the plane. He is the one who jumps at the same time as you, but separately, to film you with a helmet mounted camera. Heads turned to the left, for one last photo before the inevitable. Then, on the count of three, they were gone.</p>

                    <p>There was now a big empty space between me and the hatch. Any thoughts you might have at this point of scrambling backwards toward the front of the plane, and clinging onto the pilot for dear life are scuppered, as you are strapped on tight to your tandem master. Edging slowly forward inch by inch, we reached the side of the plane.</p>

                    <p>The cameraman did his Spiderman bit, climbing out to hang onto the side. It felt as if I was just dangling, as the tandem master is actually sitting on the edge, but you're just in front of him, held in the straps. Moving my head around, there was just time for one last photo (I went for abject terror, I think I nailed it), before I was hurled forward and begin falling. Very, very quickly.</p>

