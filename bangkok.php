                    <p>I took a walk to the infamous Khao San road while I was in the area. This is backpacker central, and the number of guesthouses, food stalls, souvenir shops, bars and conmen that accompany that is mind blowing. I walked along, ignoring the many people trying to tailor me a suit (been to Hoi An already, sorry!), the Indian "psychics" informing me I was a "lucky man" and the restaurant promotors, before stopping at a Pad Thai stall to sample the street food. It was ok, but I've definitely had better elsewhere. It gave me time to soak up the atmosphere though. I was amused at the signs advertising fake id's, diplomas etc for sale, similarly the bar actually named "We do not check ID card", but I didn't really like it very much. Far too crowded and chaotic for my liking.</p>

