                    <p>Also in the vicinity of the palace are Wat Pho and Wat Arun. By the time I'd returned to that area from Khao San road, storm clouds were gathering and thunder could be heard in the distance. Before the rain arrived I grabbed some quick snaps before working out how to get back to the Skytrain. Unfortunately the river taxi I'd taken to get to the palace initially was no longer running due to some unknown event on the river. I had no idea which bus to get, so was left with little option but to jump in a taxi. This was a complete pain, as seemingly none of the taxi drivers in Bangkok actually want to use the meter. They insist on agreeing a set (vastly inflated) price at the start of the journey, in order to extort more money from unsuspecting tourists. I went through about six different taxis before finally hailing one who was happy to turn on the meter for me. The fare at the end was only about a third of the price of one of the highest amounts I'd been offered previously... demand the meter!</p>

