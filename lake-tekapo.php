                    <p>On the morning I was due to leave, I was waiting for my coach as per usual, when one pulled into the carpark bound for a different destination. Nothing unusual there, I was being patient and waiting for mine which was still due. But someone else waiting for the same bus as me asked the driver that had just arrived where the other coach was. He said he'd seen it pass him at a stop he'd made back down the road. This caused mass panic and many phonecalls from the driver to dispatch in an attempt to locate the other coach. It turns out all that mayhem was for nothing as the other coach calmly pulled up five minutes later. The other passengers and I boarded, and relaxed into our seats as we headed off in the direction of Mount Cook.</p>

