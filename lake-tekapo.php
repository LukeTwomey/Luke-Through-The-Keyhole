                    <h1>Lake Tekapo</h1>
                    <p class="post-date">27th April 2013 23:33</p>

                    <p>I arrived on the coach and walked a short distance to my hostel, leaving my bag there for a few hours as it was still too early to check-in. After exploring for a while, I went back and tried to check in, but the girl on the reception desk was struggling to find me. "Are you sure you booked this hostel?", she politely asked. I checked my reservation email. Turns out I was in the wrong one! Just another day in the life of a capable, experienced backpacker...</p>

                    <p>When I did arrive in the hostel I'd actually booked, I walked into the dorm, dumped my bag and there was a girl asleep in one of the bunks. She woke as I entered and the very first words she uttered to me - no "hi" or anything - was "have you got a cigarette?". No. No I don't have a cigarette you impolite moocher.</p>

                    <p>Lake Tekapo is a huge draw for tourists due to the stunning turquoise colour of the water. This is caused by 'glacial flour' suspended in the water, which is produced by rocks grinding together and producing a very fine powder as they are dragged down by glaciers in the alps. The resulting effect is quite remarkable.</p>

