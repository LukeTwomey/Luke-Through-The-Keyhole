                    <p>As you walk around you can't fail to notice the huge amount of asian tourists everywhere! Evidently New Zealand in particular is high on the list of places to visit, and quite frankly I can see why! Everywhere you look, even on the coach in between places you're blown away by the scenery, it really is fantastic.</p>

                    <p>While the lake was undeniably beautiful, once I'd seen it and walked around for a bit I felt like I'd done all I needed to do there. There's not an awful lot else to see in the immediate area. I believe there are other trails for hiking, but I was pacing myself for treks I had planned for later in the trip! I did get to see a statue of a collie... I bet none of you can claim that.</p>

