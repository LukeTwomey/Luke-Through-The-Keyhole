                    <h1>Luang Prabang</h1>
                    <p class="post-date">31st August 2012 08:21</p>

                    <p>Coincidentally I met the same two backpackers on this journey as I did heading to luang-prabang. I was in the big, local bus and bumped into them when we stopped halfway for food. They'd opted for the minivan, but due to some sick, twisted turn of fate had ended up with the same driver (micro-sleep man) we'd had going to luang-prabang. Apparently he drives that route too... None of us fancied their chances of making it to Luang Prabang in one piece.</p>

