                    <p>On arriving in Luang Prabang, I was tired. I was hungry. But I wasn't worried. I'd done my research. I'd found the best pizza joint in town and boy was I looking forward to that pizza! Thin, crispy base, tasty tomato sauce, delicious melted mozzarella, covered in meat.... I walked along the road once and I couldn't find the place. Odd... But possible that my pizza excitement had caused some temporary blindness. I walked along the road again... still no sign of it. By now my mouth was watering and my stomach was rumbling uncontrollably. I asked staff at one of the restaurants near where it should have been... "Oh yes, that place shut down about two months ago". Nooooooooo! Crushing disappointment. I frantically searched on my phone for an alternative. A restaurant called "The Pizza"?! Promising, I thought. Surely a restaurant actually named after a certain type of food would excel above all others when it comes to cooking it? There would be some kind of moral obligation to their customers to make sure that one thing on their menu was outstandingly amazing wouldn't there?! I couldn't have been more wrong. It was rubbish. I contemplated changing the sign above the door to The Shit Pizza, but I restrained myself.</p>

                    <p>Luang Prabang is a really nice place to walk around very slowly, and chill at a nearby cafe/smoothie stall when you become exhausted from over-exerting yourself in the process. There are lots of nice tree lined streets, countless wats, and tiny alleyways to explore.</p>

