                    <h1>Arrival in Nanning</h1>
                    <p class="post-date">12th May 2012 11:24</p>
                    <p>The first train journey on my trip so far was mercifully uneventful. It departed Guilin around 8am and only lasted four or five hours. I did manage to oversleep by around forty-five minutes, but luckily had left myself enough time anyway!</p>

                    <p>What appear to be no smoking signs as I know them from the UK, actually have completely the opposite meaning in China. These are designated smoking areas where people (including train staff) can gather and have a much needed cigarette during the long, tiring journey. Actually they were right by the toilets, maybe they're being considerate to their fellow passengers by masking the smell?</p>

                    <p>The toilet was pretty funny. My advice is if you really, really must use the toilet on a train here, try and do so early. I managed to avoid it for the duration (preferring dehydration), others weren't so lucky. Someone very early on must have had really bad aim while they were laying some pipe, as every single person I saw for the remainder of the journey opened the door, gave a look of absolute disgust at the horror before them and turned right back round again. For locals to even do that, I'd imagine it was pretty bad. So here's some photos of the toilet from hell in all its shit splattered glory:</p>

                    <p>OK not really. Those of you who want that kind of thing, there's other websites. Get off my blog.</p>

                    <p>We get to Nanning, and it's 35 degrees celsius. I'm from the UK, I didn't even know that temperature existed. Carrying my big pack (but still tiny compared to other backpackers, I don't know how they do it!) I predictably arrived at my hostel a sweaty mess. I've since come to learn that's a recurring theme and I just have to live with it.</p>

                    <p>Nanning is a really nice place, it has a very pleasant atmosphere to it. It's pretty funny walking around there getting stared at constantly by people, because there's so few Westerners there. Other than the people I met in my hostel I think I saw two. This actually turns you into something of a Z-list celebrity. I had a couple of people come up to me and ask to have a photo taken with me, presumably to stick on their refrigerator. Such a hard life being a celebrity and keeping your fans happy.</p>

