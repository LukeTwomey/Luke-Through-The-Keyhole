                    <h1>Vientiane</h1>
                    <p class="post-date">21st August 2012 10:08</p>

                    <p>In the last post I'd just been dropped off at a road junction with assurances from the songthaew driver there'd be buses stopping en route to Vientiane which I could get on. I wasn't quite sure whether this was true or he was just telling me this to get a fare, but I was at least reassured when I saw locals sitting around in the same spot with bags.</p>

                    <p>I sat down, ordered a pot noodle (it's important to sustain yourself with healthy meals while travelling - there was also no real alternative) and sod's law ensured as soon as my food arrived and I'd eaten one mouthful, the bus showed up. My rucksack was loaded, and me and my pot noodle found an empty seat to finish what we'd started.</p>

                    <p>By now it was 7pm, and I had only been waiting for the bus for five minutes, which I was pretty happy about. All I needed now was for the final leg of the journey to magically take five minutes and I'd be laughing. Unfortunately, the bus didn't arrive at Vientiane bus station until 1:30am, and I was pretty exhausted.</p> 

                    <p>When I arrive somewhere I usually check on google maps on my phone to see how far away I am from the hostel or guesthouse i intend to stay at. If it's only a couple of kilometres I'll walk there, any further and I'll get a tuk tuk or a motorbike. The bus station we got off at was 10km out of town, so I knew I had no chance of walking it. I went through the usual bargaining process with the first driver I saw, getting him down to 20,000 kip (Â£1.50). I loaded my bag, sat down and waited. And waited. And waited... Another chat with the driver and it became clear he was waiting for other fares from other buses not yet arrived. He wanted around four other people to make it worth his while.</p> 

                    <p>After one hour, three other buses and witnessing a local lady carrying a couple of slimy looking, wriggling carrier bags (God only knows what was in there!), I was still the only one sitting in the back of this tuk tuk. I was completely knackered and just wanted a shower and a bed. I moaned at the driver and he said sure we can go now - if you pay 100,000 kip... This wasn't going to happen, so I decided to cut my losses and begin walking the long road into town. I would stop at the first guesthouse I saw for one night and deal with everything the next morning.</p>

                    <p>One kilometre or so further on, a motorbike pulled up offering a ride. I'd been saved! We agreed 30,000 kip to the guesthouse I wanted to stay at, and sped off down the road. I was relieved and happy I was finally going to get some sleep that night! That is, until the guy pulled over by the side of the road exactly 2km (I checked!) away from the guesthouse, demanding an extra 20,000 to take me the rest of the way. "You little blighter", I thought (that's the edited version). No amount or arguing with him about what we agreed initially would move him. I (very) reluctantly paid the thieving bastard and finished the journey on my own two feet.</p>

                    <p>Having finally made it to the guesthouse I was completely ready for a shower, then bed.....  "We're full, sorry". Bugger. Book in advance, folks. I've been lucky so far in that everywhere I've been has always had room due to it being the off season. Things just had to change tonight, didn't they? He sent me next door, to another guesthouse that luckily I'd also read good things about, and I was blessed to find a free dorm bed available. Shower, bed, sleep.  Heaven.</p>

                    <p>So I made it to Vientiane, and what a nice place it is. The capital city of Laos, but not overly busy or hectic. In fact it suits Laos down to the ground as this is the most laid back place I've been to yet. It would be out of place to have a sprawling metropolis here.</p>

