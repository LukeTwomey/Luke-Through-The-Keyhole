                    <p>That video came pretty quickly right? Well that's exactly how it was for me! A short ride over to the platform, one guy who put things over my ankles, another guy who rigged everything up in the chair, then before I knew it I was standing at the edge ready to jump! "3, 2, 1" and then I just had to go didn't I? How embarrassing would that look if I didn't go first time?! I honestly wasn't terrified, nervous of course! - but I probably would have been a whole lot more nervous had the whole thing been dragged out! They obviously do that on purpose so you don't have time to think about it too much and potentially change your mind!</p>

                    <p>When I booked it I picked an early time, one of the first for the day. As a result there were only three of us in my group, two girls and I. So of course, predictably, it ended up being me that jumped first as one of the bungy guys said something like "Luke will show you girls how it's done, won't you mate?". Oh. Thanks. Now I HAD to go first or I'd look like a pussy. Well played bungy guys. Once you are all hooked up you have to awkwardly shuffle over to the edge as your legs are bound together. There is a brief moment for you to think "What the actual feck am I doing?!" before looking around – and down – at the view before you. There's also a scary moment when the guy drops that big red thing, it's quite heavy and pulls you forward! It feels like you're about to get pulled off the edge, but he's holding you from behind.</p>

