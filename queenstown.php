                    <p>I had an interesting experience in the hostel that night, I was down in the TV lounge watching a movie, and when it ended I headed back to my dorm. Turning the key in the door, and swinging it open, the room was dark apart from some dim light through the windows – everyone tucked up in bed. I got my phone out to use the screen to see where I was going, walked over to my bunk, and found someone lying in my bed! Now if it had been a hot girl I would have been pretty happy, but it was some tall Japanese guy instead. Not impressed. He was still awake... "That's my bed!" I said... "Ohh, sowwy!"he replied, jumping out. I got in, to observe him in the faint light look around blankly and then walk out the door. The problem for him, was that the room was full and there were no more beds. Obviously why he'd decided mine was suitable... I don't know what happened to him after that, as thoughts of him and the double booking slipped from my mind as I drifted off to sleep, ready to wake up the following morning to leave for Wanaka.</p>

