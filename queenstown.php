                    <p>After doing that in the morning, the rest of the day seemed pretty mundane in comparison! Queenstown is a nice little town situated right by the water, which I always love. It's busier than the other places I'd been to but still relaxing enough to make it peaceful. Actually, come to think about it, it's the perfect place to chill after doing something as extreme as bungy jumping!</p>

