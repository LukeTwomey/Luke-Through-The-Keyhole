                    <p>I arrived in Chiang Mai with not the faintest idea about how long I'd be staying there, or even where else in Thailand I'd be heading (besides the obligatory Bangkok). Four days later I still hadn't made any plans. Chiang Mai made me lazy! OK... lazier than usual.</p>

                    <p>The stupidest thing actually amazed me on my first day here, so much so I just have to mention it briefly. I've become so accustomed to pedestrian crossings meaning precisely bugger all in Southeast Asia, that I nearly collapsed in shock when a car actually stopped to let me cross a road, and I wasn't even at a crossing! I guess that's proof the drivers are nicer in Thailand... or Chiang Mai at least. You really do have to see some of the antics on the roads here in Asia to believe them. The scary thing is, I've been here so long now they actually just seem normal.</p>

                    <p>Chiang Mai is also apparently home to the world's smallest dog. You may have to enlarge the photo (click it) to actually see it...</p>

