                    <h1>Chiang Mai</h1>
                    <p class="post-date">28th September 2012 05:02</p>

                    <p>Thailand. Specifically Chiang Mai, "Cultural capital".</p>

                    <p>Almost immediately on arriving in Thailand, it felt more developed and 'westernised'. The roads were smoother, and three lanes wide at some points, allowing for motorway speeds. When we stopped for a break it was at what you'd consider a "proper" petrol garage/service station. There were more glass fronted shops, and big superstores... it was vastly different to the previous countries I'd been to. That's not always a good thing, of course...</p>

