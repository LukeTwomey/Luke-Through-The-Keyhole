                    <p>Chiang Mai is just nice. It's a pleasant, calming place to be. The quaint little soi's (lanes) host charming daily Thai life, from women hanging out their washing to kids fighting with sticks. I stayed in the old city, which is a 4 square km piece of land surrounded by a moat. The size of it makes it easy to walk around, safe in the knowledge you'll never actually get lost, merely take a slight diversion.</p>

