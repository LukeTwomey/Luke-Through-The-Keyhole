                    <p>Seriously, what's the point of having a dog that tiny?! I'd be paranoid I was going to crush it every time I crashed out on the sofa.</p>

                    <p>I used Chiang Mai as a place to just relax after Laos. I think maybe I planned too many stops there, travelling to a different part of the country every one or two days can be surprisingly tiring, the buses and minivans really aren't much fun. I do actually miss my motorbike! Even though it tried to kill me... I still really want to get one when I get back to the UK though, I can't help it. I don't think you can really put across how much fun it is, and what a great feeling until you've ridden one and experienced it for yourself. An '06 Yamaha R6 will be on the cards eventually though.... watch this (empty parking) space.</p>

