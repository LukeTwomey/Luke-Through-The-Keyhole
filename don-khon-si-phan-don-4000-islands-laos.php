                    <p>The deck of the guesthouse gave a great view over the river to Don Det, and downstream to the connecting bridge. There were hammocks outside each of the rooms so people were often relaxing there enjoying the peace and quiet. I met Ella, Emilia and Kevin also staying in the same guesthouse, and we went out for an eventful dinner the one night we were all there together. It started normally enough, but as we began receiving our meals the wind picked up substantially. Before long the inevitable happened and the heavens opened, prompting the restaurant owners to batten down their (largely ineffective) canvas hatches. The wind was blowing the rain in across the restaurant, and we still got fairly wet and cold despite moving the table further away from the edge of the deck! Definitely a meal I'll remember for a while. </p>

                    <p>Oh, Ella reckoned I look like Hollywood actor Lee Pace, who I'd not actually heard of before - what do you reckon?</p>

