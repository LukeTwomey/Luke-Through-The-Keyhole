                    <h1>Don Khon, Si Phan Don (4000 Islands), Laos</h1>
                    <p class="post-date">12th August 2012 08:06</p>

                    <p>Oh dear, I ended up in a place basically built for lying in a hammock all day again! I hate when that happens...</p>

                    <p>Welcome to Laos. And what a welcome. Sleepy Si Phan Don, otherwise known as 4000 islands is in the south of Laos, pretty much straight after the border from Cambodia.</p>

                    <p>I have no idea if there are actually 4000 islands or not, I'd be willing to bet that even if someone actually went to the trouble of counting them, they ended up at 3987 or something and rounded up, which is cheating really, but I'll let them off. Regardless, there are far fewer than that in rainy season when I arrived, as the majority of them are underwater.</p>

                    <p>Two exceptions to this are the islands of Don Khon and Don Det, commonly visited by tourists passing through the area. I opted to stay in Don Khon, which is supposed to be the quieter of the two. The islands are linked by a bridge anyway, so you can easily visit the neighbouring island if you so wish. I had intended to do just that, but my laziness and the hammock on the deck outside my room colluded to ensure that didn't actually happen.</p>

                    <p>Don Khon is serious competition for Otres Beach in Cambodia for the most relaxing place I've been to so far. There are a couple of waterfalls you can visit anytime you want to interrupt your busy 'lying down' schedule - I roused myself long enough to take a walk to one.</p>

