                    <p>It was a shame to see (and hear) very large trees being cut down by chainsaw all around us as we were walking. If you've never heard it before, it's a very loud noise when they come crashing to the ground! We could only assume it was to make way for other huge, ugly hotel complexes like those we saw being built in a couple of places along the road. Another example of a town that will no doubt one day become ruined from excessive development and tourism.</p>

                    <p>So, in a bid to get away from civilisation entirely, my next stop was Taman Negara, the biggest national park in Malaysia, which promised lots more trekking, and the chance to sleep overnight in the middle of the rainforest. Blair Witch Project anyone?</p>

