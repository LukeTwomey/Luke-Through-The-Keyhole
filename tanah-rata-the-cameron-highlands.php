                    <p>On arriving I went to a few different places to find a cheap and cheerful hostel that had space for me in their dorm. Kang's lodge fit the bill, with a nice attic style, wooden beam affair in the roof of their building. At a price of RM 10 (Â£2), it was not the cheapest I'd been in so far in Asia (but obviously still a relative steal compared to the UK!), but Malaysia in general is more expensive. A taste of things to come, now I'd already visited the cheapest countries.</p>

                    <p>As I only planned to be in Tanah Rata for one day, I opted to take a half day tour the next day in order to ensure I saw all the local highlights. In actual fact, the tour was only ok, not brilliant. It didn't get off to the best of starts when we took a trip up to Gunung Brinchang to get a view from the highest point in Malaysia, at 6,666 feet above sea level. Unfortunately it was cloudy, so despite climbing a purpose built viewing platform, we had no view to enjoy. Disappointing, but these things happen. The views slightly further down, however, were still amazing.</p>

