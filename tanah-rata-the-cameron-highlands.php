                    <p>Our last stop was a Buddhist temple. This was ok. It looked very similar to other temples I've seen along the way... the best was yet to come though, and strangely it wasn't a part of the tour! Behind the temple was the start of a jungle trek, which we could follow for a couple of hours in order to get back to the town. At least, it would have taken us that length of time if we hadn't got lost.</p> 

                    <p>The tour guide dropped off three of us there (all actually staying in the same hostel) and we were given instructions to follow paths two, three, then five. Easy. The problem was, at the end of path two there were two directions to choose from. Left was marked as path three, and right was also marked as path three. When you join a path in the middle, which way do you go?! The wrong way, of course... like us! We realised our error when we reached a road and there was a sign from there pointing back in the direction we'd came, to path five. Our little detour probably cost us another hour, but it was a fun excursion nonetheless.</p>

