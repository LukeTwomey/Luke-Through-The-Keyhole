                    <p>We were gazing across a tea plantation, owned by a Scottish family and employing more than two hundred workers. The tea is cut every three weeks, as only the light green, new leaves can be used to make tea. A clever device is used which allows the workers to harvest ten times more than they would by cutting manually. They drag it over the top of the trees and the leaves get blown into the bag at the back. Unfortunately the tea factory was closed on Mondays, when I'd booked the tour (I knew this at the time), so we couldn't go in to have a look at how it's processed once it's cut. I'm sure we could have had free tasters had we been able to visit, but as many of you will know, I don't drink tea anyway (I am English, believe it or not) so I wasn't too disappointed there.</p>

