                    <p>A strawberry farm came next. For Lord only knows what reason, there are absolutely loads of strawberry farms in the Cameron Highlands. The area is so famous for strawberries in fact, that the shops have also produced all kinds of strawberry themed souvenirs for the tourists to enjoy. The farm we were visiting had a cafe attached, and with it the promise of cream tea, I naturally had to try some -just the scone mind - but it wasn't a patch on what you would get in England! There were also many other delights you could try. I went for the sundae, and the strawberry chocolate milkshake. They were gooood. I was very full! I also think I've had my strawberry allocation for the year.</p>

