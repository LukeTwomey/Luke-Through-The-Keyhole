                    <h1>Tanah Rata, The Cameron Highlands</h1>
                    <p class="post-date">8th October 2012 09:15</p>

                    <p>Getting to Tanah Rata didn't go very smoothly. The bus arrived on time, no problem there. In fact I was overjoyed with what I saw. there were huge, comfortable seats, with masses of legroom! It was by far the nicest coach I've been on yet. The problem came when we stopped off at a bus station for ten (scheduled) minutes to set down and pick up more passengers. I returned from the toilet to find an official looking pickup truck with police style lights mounted on the top, and some stern faced guys in uniform (I guess the Malaysian transport police) chatting to our driver. There appeared to be some concern with the door of our coach, which didn't quite shut fully, and required securing in order to stop it from opening further as we were driving along. I guess we were waiting around for 30-45 minutes while everyone had a big conversation about this door, opening and closing it, pulling and pushing it countless times. I was fully expecting them to say we couldn't go any further, when suddenly the police wandered off, the driver jumped in his seat and off we set! Lucky escape, I thought, except that wasn't quite the end. As we drew nearer to our final destination, the bus pulled over to the side of the road and the driver told us the police also weren't too happy with the state of one of his tyres. They'd also apparently told him a couple of times before about this, and insisted it be changed immediately... next thing we knew we were being jacked up and you could hear the sound of wheel guns as the wheel was removed and replaced with another. Coming from other countries in Asia, this was a refreshing change, where there was absolutely no policing of buses or minibuses at all!</p>

                    <p>Tanah Rata itself is very scenic, with nice views of the surrounding hills everywhere you look. It actually reminded me of one of those American towns you see so often on screen, like in Twin Peaks or Twilight, with the trees everywhere it had that kind of feel to it. Although admittedly I've never been personally, just seen them on screen. It also had a very nice park (yes I love my parks!).</p>

