                    <p>My favourite bit was arriving at a point where it was safe to get down on your stomach, and wriggle to the edge of the rock to poke your head over the side, peering down into the canyon below. Quite a feeling, and certainly not one to try if you're not fond of heights, or falling from them!</p>

