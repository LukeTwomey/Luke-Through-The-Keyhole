                    <p>There was also a point where you could straddle a huge crack in the rock, where literally the whole of the side of the canyon is gradually coming away. If you throw a rock down the gap you can hear it fall for a long way down deep into the rock. At some point the whole lot will eventually give way and slide off the side.</p>

                    <p>The fun continued, as we made our way in the coach to a station where they keep camels, dingos and so on. For a small fee, you were able to ride one of the camels for a few minutes around a small compound on the farm. The one I ended up on was extremely grumpy, and did nothing but moan from the first person to ride him. We were walked down to one end of the pen, before being bumped around mercilessly on the way back as the camels ran to keep up with their handler. Good fun, but probably not a pet I'd consider getting anytime soon.</p>

