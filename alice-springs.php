                    <h1>Alice Springs</h1>
                    <p class="post-date">15th January 2013 04:58</p>

                    <p>One thing on the agenda here - Ayers Rock, otherwise known as Uluru. It would have been almost criminal for me to visit Australia and not go there.</p>

                    <p>My options again were limited - I ended up booking a three day tour including Uluru (Ayers Rock), Kings Canyon and Kata Tjuta (The Olgas). Far more convenient than trying to arrange things yourself, and the added benefit of a group to go with to make it a bit more fun!</p>

                    <p>"Here's something for ya, the first of our two right hand turns for the day. The other one is 200km's down the road. If you want to go to Kings Canyon it's a little more complicated. That's three right turns...". Our tour guide fancied himself as a comedian. In fairness, he was actually quite amusing.</p>

                    <p>On the drive out we were treated to some information about snakes. Apparently, around 90% of the venomous snakebites in Australia are "dry bites". This is where the snake doesn't actually inject any venom. The reason for this is because it is defending itself, and warning you away - if it were to inject venom it would be left defenceless and have no other means of protecting itself. If we were to encounter any snakes during our tour, it would likely be the inland taipan or the king brown - in number one and two spots on the twenty deadliest snakes in the world. One drop of venom can kill twelve people. Reassuring. When they're looking straight at you, they're aiming for a strike and you really should be in another place. Swiftly.</p>

                    <p>I got lucky with the temperature, the week before I'd arrived it was in the 40's - I literally would not have been able to move. While I was there it was in the mid 20's, which was nice and comfortable. We stopped at a lookout on the way to Uluru, across to Mount Connor (shaped like a toothbrush!). At first glance when you see a big rock on the horizon from the window of the bus, you think it's Uluru. It's not.</p>

                    <p>Our first stop was straight to the rock (the real one, not the imposter!). We got off the coach and were told of the importance of only taking photographs in the designated areas. Certain parts of Uluru are seen as extremely sacred areas, and the aboriginal people do not allow photos to be taken at those points. The rock has an 11km walk around its base - which we eagerly began. The guide didn't - he obviously had more important things to do, like sitting in the coach. It was quite a spectacle to look across and see such a huge object in front of you, at first... if I'm honest it gets a bit "samey" after a while. For the whole 11km you pretty much have the same view, and while it's an impressive sight, you don't get a sense of the enormity of it all - as you do when viewing it from a distance. Learning from my experience in Darwin with the most annoying flies in the world, I'd bought a baseball cap and fly net to keep the buggers at bay. It worked brilliantly, and the best bit was it was highly fashionable and made me look cool.</p>

