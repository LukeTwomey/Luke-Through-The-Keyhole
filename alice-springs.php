                    <p>Leaving the high winds of the valley behind, we made our way to our next campsite, stopping along the way to gather firewood. It's while you're doing this you become acutely aware of the possibility of being bitten by a camouflaged snake. Good fun.</p>

                    <p>On the menu that night was kangaroo steak and camel burger. Both were delicious! Kangaroo is very gamey, and camel basically tastes like beef. Eat them. I still need to try crocodile and emu...</p>

                    <p>Our final day was spent at Kings Canyon. You had a choice of two different walks here, one in the canyon itself where you were more likely to see wildlife, or a longer one climbing to the canyon ridge for the views. The majority went with the latter option, and we soon found ourself gazing spellbound across the outback.</p>

