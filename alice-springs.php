                    <p>After the sun had set, and the crowds of people had abated, we made our way to our campsite for the night. Pulling up to an open stretch of grass with a shelter next to it to eat, we unpacked our swags and settled down for the evening. For those who have no idea what a swag is (like me when I first heard about them!), it's basically a really posh sleeping bag. The outside of it is waterproof - and has a flap that covers your head at night if it does decide to rain - ensuring you stay dry. Inside there's a thin foam mattress with a pillow, all inside the traditional sleeping bag padding. It was so comfortable you wouldn't believe it. All swagged up, we lay beneath the stars on a beautifully clear night. This was seriously one of my favourite bits of the tour - it's only looking up at the stars and actually taking the time to appreciate them that you realise just how tiny everything really is. I saw some shooting stars as well which was amazing, and I lay there for a long time in awe before finally drifting off to sleep.</p>

                    <p>The next morning we rose before the sun, to make our way to a different viewpoint in order to catch the sunrise over Uluru. Our campsite location and early start meant we got to the site before everybody else, and were in front row seats for the view. Unfortunately, I'd failed slightly with my camera - I'd forgotten to charge my spare battery, and the one in the camera was virtually dead. This meant I wasn't my usual shutter-happy self, but luckily I still managed to get a few pics I was happy with. By being very selective with the photos I took with my camera, I managed to make it last for most of the rest of the tour, using my iPhone as a substitute in the meantime. The viewpoint we had gone to also gave great views over Kata Tjuta - double bonus!</p>

