                    <p>You have the option of climbing the rock at one point - at least you normally do, it was closed on the day we went due to high winds. The aboriginal people politely request you do not climb - due to the sacred nature of the site. But if you are so inclined, you are allowed to do so. Regardless, we didn't need to make that decision as it was already made for us that particular day!</p>

                    <p>After completing the base walk, and returning to the coach, we headed off to join the masses (and I do mean masses!) of people gathering at the best vantage point for viewing Uluru at sunset. The number of people here was just staggering - and apparently it's like that 365 days a year, without fail. Crazy, you might think initially - but as the sun begins to set you really do understand why. The view is quite incredible, with Uluru displaying beautiful red colouring from the warm light of the sun. And what better way to enjoy it than with champagne, crackers and dips?!</p>

                    <a href="images/posts/alice-springs/alice-springs-29_8234440983_o-large.jpg" class="fancybox">
