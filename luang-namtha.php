                    <h1>Luang Namtha</h1>
                    <p class="post-date">2nd September 2012 11:35</p>

                    <p>Luang Namtha was my final stop in Laos, and the furthest north I got, at 60km from the Chinese border.</p>

                    <p>This bus ride also had a story to tell (unfortunate for me, probably amusing for you). After the last minivan trip, I was pretty sure I didn't want to experience that particular driving style again, they're also devoid of any legroom. I bought a ticket for the big bus which left at 6pm and arrived in Luang Namtha around 3-4am. Not perfect, but I was at least assured it was a big, VIP sleeper bus...</p>

                    <p>It bloody wasn't!</p>

                    <figure>
                        <a href="images/posts/luang-namtha/camera-roll-208_7905591428_o-large.jpg" class="fancybox">
