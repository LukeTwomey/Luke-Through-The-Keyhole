                    <p>The second day we were trekking through the forest for five hours. There had been rain during the night, which made some bits very muddy and tricky to navigate, but it was still a great experience. What amazed me was that all three of us "normal" people booked onto the tour were dripping in sweat throughout, but Oun was bone dry. He was wearing a backpack and his back wasn't even sweaty under that. It just goes to show how fit he is and how used to the climate he is. He said he does a tour every weekday (varies between kayaking, trekking, mountain biking or rock climbing), and works in the office at weekends. Very impressive.</p>

