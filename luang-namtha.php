                    <p>We also got stuck in a huge traffic jam for an hour at about 1am, I can only assume due to a landslide. The mountain roads were nothing but mud in some parts. This meant we actually arrived in Luang Namtha at about 6am. I hadn't really been able to sleep on the "bus". I'd drifted in and out, resting my head on my trusty inflatable pillow, but sitting bolt upright really doesn't make it easy for me to drift off. I checked in at my guesthouse and immediately went to bed to catch up on some sleep!</p>

                    <p>When I woke around midday, I headed to Green Discovery again to make some enquiries. There was good and bad news. They had a two day tour, leaving the next day with one person already booked on. Perfect! I put my name and deposit down. The bad news was, the woman that had booked before me informed the sales guy she could only afford it if four people went. So I did the only thing I could do... hunted down and harassed as many random people in the street as I could find! It worked as well... a French couple put their name down too! Weirdly enough, the original woman didn't actually show up again, so it was just me and the French couple, Batiste and Caroline. Fine with me, it still worked out cheaper. As long as they didn't start getting off in front of me... that's just bad taste isn't it?</p>

                    <p>The following morning at 8:30 we met up at the office, bags packed, and were introduced to our guide, Oun. He had actually previously worked out of the Vang Vieng branch, and knew Mang, the guide who took me out when I did the tour there. He'd been in Luang Namtha for three years, working at Green Discovery all that time, so he knew his stuff. Unfortunately one of the many things he knew was the forest we were going to was home to wild cats, bears, spiders, green snakes and king cobras (yes, the ones that can spit venom and will actually chase you if you piss them off) and other things that could potentially kill us. He took great delight in telling us this. Thanks Oun.</p>

                    <p>We rode in the van for about one hour outside of town, before turning down a dirt track to reach a stony beach by the side of the Nam Tha river. The kayaks were offloaded from the roof of the van, and we set about packing all of our belongings into dry bags in preparation for the soaking they were likely to receive on the river.</p>

                    <p>I was in a kayak with Mr Ling, our temporary second guide. Oun was in a kayak on his own with the majority of the baggage we had, and Batiste and Caroline were in the third. We got off to a great start, which lasted for all of five minutes. Somewhat expectedly (in my opinion), having no prior experience in a kayak, Batiste and Caroline encountered some difficulties at the first set of mild rapids we encountered. Oun was shouting at them to keep left, but they were pulled right by the current, - having not yet gained enough experience with steering - under a low hanging tree branch. This stopped their kayak in its tracks, turning it sideways while still stuck on the branch. The flow of the water under the boat inevitably flipped it over, and they ended up in the water with the contents of their kayak.</p>

                    <p>Oun and Mr Ling sprung into action, and the kayak was promptly righted, and Batiste and Caroline hauled inside. Other than looking fairly shocked (and wet, unsurprisingly), they were fine. We carried on, with the Frenchies quickly improving over the next three hours or so. We soon stopped for lunch. Sticky rice, spicy bamboo, green beans and beef, and chicken and pumpkin (I think) laid out on a tablecloth of banana leaves.</p>

