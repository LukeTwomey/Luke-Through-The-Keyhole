                    <p>Having had our fill, we continued our journey down the river for another couple of hours. At one point i saw sonething move extremely quickly out of the corner of my eye, turning round to see something disappear into the bushes. Oun said it was likely to be one of the wild cats they have there, and i was a little disappointed not to have had a proper look. There was also unfortunately another section right near the end where Batiste and Caroline capsized again. I didn't fall in once... that must make me an expert right? Or maybe it means I was in the kayak with one of the guides... anyway, not important.</p>

                    <p>We arrived at the village we were to stay in overnight, and offloaded all our things. At the first sign of our arrival, all the local kids ran down with small platters displaying the goods they had for sale, the majority of which were bracelets. We figured it would be rude not to buy one each, it wasn't expensive for us but would help them out somewhat. So unexpectedly, I now wear a bracelet. Not going to put a pic up of this, if you want to see that badly contact me!</p>

                    <p>We were actually in a lodge just outside the village, which is where we ate and slept that night. The following morning we went up to visit it before beginning our trek.</p>

