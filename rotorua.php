                    <p>A short way outside the city (but, predictably far enough that I'd have to pay for a shuttle/day tour thing!) is the Wai-O-Tapu Thermal Wonderland! I know, it sounds a little overhyped... but it is pretty good, and certainly on another level to the small, bubbling mud pools immediately surrounding the city. One thing that was a little underwhelming, and didn't exactly live up to the "Wonderland" title was the Lady Knox geyser. It promised an "eruption" every day at 10am, which we arrived for in plenty of time on the minibus. Now I don't know about you, but when I hear the word eruption I expect something pretty violent, maybe powerful enough to blow the front row of tourists 10 feet back in the air with a loud, explosive bang! It was incredibly disappointing, it quietly spurted some water and steam (admittedly) fairly high into the air, but it was all rather tame for my liking.</p>

