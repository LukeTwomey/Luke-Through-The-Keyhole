                    <h1>Rotorua</h1>
                    <p class="post-date">7th June 2013 22:32</p>

                    <p>If you're planning on visiting Rotorua, do so with a clothes peg firmly clamped over your nostrils. It whiffs a little...</p>

                    <p>It's not due to the locals having eaten too many baked beans, but the thermal activity in the area producing hydrogen sulphide emissions, which smell like rotten eggs. While I guess this may put some people off, the novelty of walking around town and encountering bubbling pools of mud and steaming chasms does more than make up for it.</p>

