                    <p>After arriving back in town at the end of the thermal wonderland trip, I was keen to visit the Wingspan National Bird of Prey Centre. This is a little known "attraction" in the area, as they don't market themselves as a tourist destination as such. Indeed, when I'd previously asked about them in the iSite visitor's centre, they hadn't heard of them before. They're actually a conservation, education and research organisation for birds of prey. Every day at a set time, they provide a flying display of their birds which I really wanted to catch. I'd enjoyed the one I'd seen at the koala sanctuary in Brisbane, but that wasn't really the main attraction and hadn't lasted very long.</p>

                    <p>Unfortunately, once again I was at a disadvantage not having my own transportation. I had to resort to catching a taxi as walking all the way would have made me late for the display. I realise this makes me sound like a complete cheapskate, but when you're backpacking (especially as it was near the end of my trip!) I had to watch the pennies pretty closely! Anyway, I made it in good time, and after having a look around their large aviary, I settled down with the other people there (only around seven or eight people, it was great!) to watch the display.</p>

