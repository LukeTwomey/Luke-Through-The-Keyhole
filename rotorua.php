                    <p>The city itself is not very busy, and has some nice architecture and buildings to admire while exploring. However, the main event really is the thermal "goings-on". There was one point where an area in Sulphur Bay was cordoned off and signs warned people not to pass beyond that point as the deceptively hard surface was actually very thin, and there was a danger of it cracking underfoot and you getting scalded by ridiculously hot steam rising from below. I watched as some people blatantly ignored this and began wandering all over the area past the barrier. I secretly hoped for the ground to crack beneath them as a consequence of their idiocy, but alas â€“ no joy. I continued my wandering in disappointment.</p>

