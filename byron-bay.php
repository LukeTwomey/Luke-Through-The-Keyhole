                    <p>As always happens when you're having fun, the time flew by way too quickly and the lessons were soon over. Overall I reckon I did pretty well. It's tricky at first but definitely not as hard as I expected it to be. I doubt I'll be out on 100 foot waves anytime soon though.</p>

                    <a href="images/posts/byron-bay/p1310520_8285868787_o-large.jpg" class="fancybox">
                        <img src="images/posts/byron-bay/p1310520_8285868787_o.jpg"/>
