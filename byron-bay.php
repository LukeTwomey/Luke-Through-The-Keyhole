                    <p>If I lived by the beach and the surf was good enough, I'd definitely buy a board. I often saw locals come down later on in the day around the same sort of time to surf, presumably after knocking off from work. Not a bad way to kill a little time at the start of your evening!</p>

                    <p>Being in the area also afforded me the opportunity to visit the most easterly point of Australia, Cape Byron and the lighthouse there. It's a short walk from town and a nice tour around the coast to get good views of the rocks and out to sea.</p>

