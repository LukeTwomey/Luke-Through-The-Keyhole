                    <h1>Byron Bay</h1>
                    <p class="post-date">19th January 2013 02:14</p>

                    <p>If you're looking for somewhere so laid back it's almost horizontal, this is the place to go.</p>

                    <p>My days in Byron consisted of waking up, eating, heading down to the beach in my swimshorts, chilling at the beach and in the water all day, wandering back to my hostel when it got cooler, showering and getting changed, dinner, drinks. Repeat the following day. I guess this was only really topped by my stay at Otres Beach in Cambodia, where I probably could have rolled out of my bed and onto the beach.</p>

                    <p>One activity that did break this cycle, but still saw me headed to the beach was my time spent surfing! I had been keen to try this along the coast somewhere, and apparently Byron was the place, so I'd waited until I arrived there! Good reviews online saw me head along to Black Dog surf school to book a couple of lessons.</p>

                    <figure>
                        <a href="images/posts/byron-bay/_dsc1882x_8286921212_o-large.jpg" class="fancybox">
                            <img src="images/posts/byron-bay/_dsc1882x_8286921212_o.jpg"/>
                        </a>
                        <figcaption>Yes that's a surfing dog...</figcaption>
                    </figure>

                    <p>I was equipped with a top (for some reason all the different schools need to wear a certain colour), and a huge, foam board to learn on. Apparently the larger it is, the easier it is to balance, and the foam meant it wouldn't hurt quite as much IF the board hit me WHEN I wiped out!</p>

                    <p>They began by showing the group exactly how to lie on the board, how to stand up when you're on the wave, how to turn, and the one thing they don't show you which you easily manage to figure out by yourself, is how to fall off.</p>

                    <figure>
                        <a href="images/posts/byron-bay/p1310511_8285882865_o-large.jpg" class="fancybox">
                            <img src="images/posts/byron-bay/p1310511_8285882865_o.jpg"/>
                        </a>
                        <figcaption>Post *splash*...</figcaption>
                    </figure>

                    <p>To begin with, for the first lesson the instructors stood out in the water at the point it's best to catch the wave, and then pushed me along on the board as a good one arrived. All my concentration was then focused on actually standing up! The second lesson I was then allowed to try catching my own waves, by paddling when I saw a good one coming in. I soon learnt, to my surprise, this is actually a skill in itself. Often I found myself paddling to catch a wave then feeling it die down to nothing. The instructors are obviously very experienced in picking the best ones.</p>

                    <p>After the first few goes at standing up to actually catch the wave, it started to feel a little more natural. I started concentrating less on which foot goes where and what position my body should be in and more on actually balancing on the board! I did still have to keep my feet in check though - if your weight is too far back the board tilts back and the wave goes on without you. Too far forward and you are unceremoniously dumped forward to faceplant the water as the front of the board digs in!</p>

                    <p>I was just thinking to myself as I rode along the waves "I wish I had someone who could take some photos with my camera right about now" (just for the obligatory proof!), when the next person I saw was from the surf shop, up to her waist in the water, camera in hand, snapping away! God bless these companies and their enterprising ways to squeeze a little more money out of me.</p>

