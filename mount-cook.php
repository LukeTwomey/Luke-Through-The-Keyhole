                    <h1>Mount Cook</h1>
                    <p class="post-date">28th April 2013 13:00</p>

                    <p>Mount Cook (as you would probably expect), is pretty darn big. Like, mountain big. Arriving on the coach, you can see it looming in the distance and it's really quite astounding.</p>

                    <p>I arrived in the early afternoon, and I hadn't originally intended to go out - rather stay in the hostel and write some of my blog (I think you can guess how well that went while I was in New Zealand, as you're still getting updates now I'm back home!). There was no way I could stay inside with a view like this calling to me though...</p>

                    <a href="images/posts/mount-cook/mount-cook-3_8488965588_o-large.jpg" class="fancybox">
                        <img src="images/posts/mount-cook/mount-cook-3_8488965588_o.jpg"/>
                    </a>

                    <p>I stopped off at the visitor centre, and discovered the Hooker Valley track was still easily do-able that afternoon. Apparently it's frequented by classy ladies in 6" clear heels every few hundred metres. I packed up my bag and eagerly headed off in that direction...</p>

                    <p>The track goes right to the edge of the glacier, where a lake has formed and chunks of ice which have broken off the edge can be seen floating in the water. While I was walking, I saw two crazy things. Crazy thing number one was a guy in flip flops... now I virtually lived in flip flops while I was away, but trust me, this wasn't a track you wanted to be doing  that in. Crazy thing number two was people running along it. Mentalists. Anyway, the view at the end was just brilliant, with the reflection of the mountain in the (nearly) still lake. I stayed for as long as I could, before having to return to make sure I got back before dark.</p>

