                    <p>I'd worked up quite an appetite, and I decided to treat myself to a quality meal at a grill I'd walked past earlier on in the day. I was on my way there, when a car pulled up and a couple asked for directions, coincidentally to the same restaurant I was walking to. They kindly offered me a lift, and I accepted, having always been advised to both take sweets and get into cars with strangers.</p>

                    <p>The two abductors - Denise and Dave - and I sat down to eat our meals with refreshing beverages to wash them down. Turns out they were both police officers and were in New Zealand on their honeymoon, travelling around the south island in a car. They had not intended to go to Mount Cook originally but changed their plans last minute, and had just arrived when I saw them. I told them they made a great decision! The decision to go to the grill wasn't bad either.</p>

