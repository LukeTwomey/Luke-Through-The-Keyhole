                    <p>Kampot has much the same kind of vibe, very chilled and relaxed (apart from when you cross the dangerous looking old bridge, when your arse clenches somewhat). The attraction of the area is Bokor mountain, where there is a brand new hotel/casino complex. There are also plans afoot to develop the whole of the area to include new housing and more hotels. Frankly in my opinion it looks like an eyesore, but decide for yourself. There is also an old abandoned hotel and church which I'd heard about, which I was really interested in. I love exploring old buildings, back in England I'd previously wandered round an old mental asylum with some friends, generally getting spooked out and taking cool photos. Good times. Unfortunately I'd arrived a little too late, and the old hotel was already being restored by workers back to its former glory. It still looked pretty eerie when the mist rolled in though. I was more fortunate with the church, and got a few nice photos inside.</p>

