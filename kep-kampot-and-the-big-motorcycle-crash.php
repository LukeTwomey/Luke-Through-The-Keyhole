                    <p>I enjoyed the atmosphere of Kampot for nearly one day before things became very stressful and painful, extremely quickly.</p> 

                    <p>Remember the fantastic, joyous time I'd been having riding my beloved Honda Win around the place? It suddenly decided to turn on me in a vicious, unprovoked attack. Well technically, my Win didn't turn on me, the motorbike that crashed into me turned on me and decided I'd had enough fun for a while. The other guy hit the side of me as I was turning into a side road across his path, but it was clear from the damage to the front end of his bike that he was travelling at quite some speed, at night. Both of us ended up sprawled on the road, thankfully only with scrapes and bruises.</p>

                    <a href="images/posts/kep-kampot-and-the-big-motorcycle-crash/7603051718_6d4a6701b2_o-large.jpg" class="fancybox">
                        <img src="images/posts/kep-kampot-and-the-big-motorcycle-crash/7603051718_6d4a6701b2_o.jpg"/>
