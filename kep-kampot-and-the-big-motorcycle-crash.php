                    <h1>Kep, Kampot &amp; The Big Motorcycle Crash</h1>
                    <p class="post-date">19th July 2012 13:32</p>

                    <p>Kep is famous for its crab. Which would have been great, had I actually enjoyed the crab I tried there. I've never had to work so hard for a meal in all my life. It would be perfect diet food, as I'm certain you expend more calories actually going through the ordeal of eating your meal than you consume. I didn't even think it tasted all that great. Sure, it was nice, but not fantastic. The little bits of shell in my mouth didn't help matters. I can safely say I'm over crab. The view in Kimly restaurant was amazing though, and it's built over the water so you hear the waves crashing underneath you. Very cool.</p>

