                    <p>He immediately demanded $200 for the damage to his bike, and I naively agreed, having read online it's best to just pay out and avoid getting the police involved. His friends turned up, took us to the nearest ATM, only for me to discover my wallet had somehow got bent up in the crash, including my credit card held within. I tried straightening it up somewhat, fearful of snapping it, then fed it to the machine which predictably swallowed it and never spat it out again. Shit. I explained the situation to my new pals, they seemed pleased.</p>

                    <p>Expecting that I would do a runner without paying up (hard when my motorbike was unrideable), they wanted to know where I was staying. We all happily rode off to my hostel (ironically named Blissful), where I was quite literally saved by Ez, the owner. He told them in no uncertain terms, they were not getting a penny out of me that night and we would bring both bikes down to the hostel and inspect them there with the tourist police. I wasn't too sure about that bit, but frankly, the guy's been there eight years so he knows his stuff, and I was in no fit state to argue even if I'd been inclined to. I actually later found out, if you're the ones who call the tourist police, they're almost on your side anyway.</p>

                    <p>My buddies wandered out of the bar, and Ez sat me down and poured me a large scotch. I don't usually drink this but I definitely wasn't going to decline on this occasion. After virtually downing it I went back to the scene of the accident and started walking my bike back to the hostel, as it was unrideable due to a broken clutch lever.</p>

                    <p>When I returned, I found the police (of the tourist variety, to be honest I still don't understand the distinction) had been called in by Ez, and the group of Khmer guys were back. Much arguing/negotiation ensued, translated by staff at the hostel and mediated by the police. It was eventually agreed the damage to the bike(s) would be repaired, and I would pay the bill. The alternative would have been the police getting further involved, taking the bikes into the police compound, determining liability etc, which was a whole heap of hassle I didn't want.</p>

                    <p>The following morning we got the bikes to the mechanic and had them checked over. The other guys bike would cost $175, mine would cost $75. This would have been fine, had I actually been able to withdraw any money. If you've been reading regularly you may remember I lost my debit card in Hanoi. My second card was now trashed. I had a third emergency card, which also would have been great, if I could remember the pin number for it. I really don't make things easy for myself sometimes. A Skype call to super mum back home, giving her a near heart attack before a day at work, saw £400 rescue money winging it's way to me via Western Union. With the money situation temporarily sorted, the bikes were repaired, the garage was paid, and everybody lived happily ever after.</p>

                    <p>Except I didn't particularly want to get on the bike again. I know that's technically the first thing you should do, but I'd known it was dangerous enough in the first place when I bought the bike, and I figured my luck had run out. I planned to ride the bike a short 100km to my next location, Sihanoukville, where I would attempt to sell it.</p>

