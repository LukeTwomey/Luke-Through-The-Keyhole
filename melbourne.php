                    <p>That's a Nissan Micra with a 6 litre, 500bhp engine inside. It can reach 0-60 in 1 second and pushes 200mph (I wished). I picked the car up in the afternoon and drove straight down to the Twelve Apostles to get there for sunset and the best photo opportunity. The astute amongst you will notice there are considerably less than twelve apostles left now – that's due to the others being claimed by the sea as the limestone rock is steadily eroded. In fact, that's how the apostles were formed in the first place – the sea slowly erodes the rock either side of them, then behind as it pushes further up the beach – the harder rock in the column takes longer which leaves it stood in the middle of the sea while the rest of the coastline recedes.</p>

