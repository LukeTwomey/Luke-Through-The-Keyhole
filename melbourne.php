                    <p>And wandered down to the gate. GATE 38a. No check-in desks down at the gate funnily enough. I was directed back whence I'd came, out into the hall I started in, walked along the desks again looking more closely at the screens above them, only to find two lonely Tiger Airways desks nestled in amongst Qantas and Jetstar. Checked in, went through security AGAIN, and walked back down to the gate AGAIN. I do worry about myself sometimes.</p>

                    <p>The "hostel" I stayed at in melbourne came highly recommended on the review sites I use. The Bendigo Hotel is conveniently located near to a train station to easily get into the town center, cheap, lots of friendly people running it, and above a pub... ahhhhh ok, now I see why it's so popular. It really was good though, it's not your stereotypical hostel as it's just a couple of dorms above the pub with some shared facilities. But a lot of the people staying there are working in the area or even in the bar downstairs at nights. It was a very cool place with live music at night, and a  really friendly, family atmosphere with the owners. Highly recommended.</p>

                    <p>While I was in melbourne I was keen to visit the Great Ocean Road, particularly The Twelve Apostles. This meant hiring a car as it was some way out of melbourne, but still the closest place to it. Damn you Australia for being so big! It wasn't all bad news though... I got to drive this BEAST of a car for a couple of days.</p>

                    <a href="images/posts/melbourne/img_6740_8671388007_o-large.jpg" class="fancybox">
