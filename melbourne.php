                    <h1>Melbourne</h1>
                    <p class="post-date">23rd April 2013 21:11</p>

                    <p>I did something pretty idiotic at the airport flying from Sydney to Melbourne. I wasn't going to include this in the blog, as it makes me look like an utter retard, but I reckon you would all enjoy laughing at my stupidity. See how nice I am to you guys?</p>

                    <p>In my defence – the flight was very early in the morning (read: cheap), and I was still half asleep. Ok, now I've made my excuses... I arrived at the airport and walked into the hall with all the check-in desks. I looked at the nearest monitor, to see where I needed to go to to check in for my flight. 38a..... perfect. I walked all the way along the desks looking at the numbers, but they only went up to 36 or something like that. That's odd, I thought. I asked the nearest attendant where 38a was. "Through security and straight ahead". Odd, why are the other desks in a different place? I wondered.</p>

                    <p>I proceeded through security, having my scissors thrown away in the process, ate my customary (healthy) McDonalds breakfast:</p>

                    <a href="images/posts/melbourne/sydney-17_8489278622_o-large.jpg" class="fancybox">
