                    <h1>Brisbane</h1>
                    <p class="post-date">16th March 2013 23:14</p>

                    <p>In Brisbane I couchsurfed again, this time with Ruthanne and her cool housemates. The place felt very much like student digs, when I arrived there were many bottles, caps and party poppers strewn across the floor from a recent birthday party! There were all super friendly and made me feel really welcome though. They were just putting up their Christmas decorations and had an insane amount of lights and other goodies in the garden for Santa's grotto. It was an epileptic's nightmare, but I loved it!</p>

                    <p>I was on the sofa in the living room, which was really comfortable, at least I thought so. And giving true meaning to the Couchsurfing title! Is it just me that actually really likes sleeping on the sofa? Anyway...</p>

                    <p>It was in Brisbane I had my first encounter with a koala bear! I'd not seen one in the wild yet, so I visited Lone Pine Koala Sanctuary, a place housing over one hundred and thirty resident bears! I was pretty sure my odds of seeing one there were good. Sure enough, I spotted this fella:</p>

