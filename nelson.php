                    <p>While I was in Nelson I met a family I'd briefly spoken to over dinner at the cabin in Punakaiki. The parents were there visiting their daughter who had moved to New Zealand for work. It turns out the guy used to live in Selsdon, which is just up the road from where I live now! Back in Vietnam I'd also met a girl who used to work at one of the restaurants I went to for a Christmas party at my old job in Wimbledon. It's funny that you can go so far around the world and still run into people from back home. They do say it's a small world.</p>

                    <p>The night before I was due to leave, I managed to pull off a spectacular Luke special. As it got close to dinnertime, I went up to the dorm room and for some odd reason I left the key in the lock as I unlocked the door and entered the room. I knew I'd done this, and I told myself to take it out as I was leaving, but I obviously didn't, as late that night after watching movies in the TV room, I went to go to bed and couldn't open the door as I had no key!</p>

                    <p>I knocked quietly, in case anyone was still awake (it was late, probably around 2 or 3am) but no one opened the door. I ended up having to sleep on the floor in the TV room! Luckily there were lots of big cushions to lie on, so it was comfortable enough and warm with the door closed. I got up early the next morning and luckily had enough time between reception opening and my coach leaving to quickly get in the dorm, grab my stuff and head off to the coach stop, bound for the interislander ferry, and Wellington, my first stop on the North Island.</p>

