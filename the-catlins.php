                    <p>I found myself rounding a corner and actually saying "wow" on more than one occasion. Actually I was pretty glad not to have a nice car of my own when driving on some of those roads... quite a few of them were just dirt and rocks! At the very best, if your car wasn't covered in millions of scratches, dents and stonechips, it would be completely filthy and dusty after every drive. I couldn't handle that, my cars need to be constantly clean!</p>

                    <p>There were a few waterfalls available for my viewing pleasure along the main coastal road in The Catlins. Purakaunui Falls (I have no idea how that's pronounced either) was the first one I arrived at and looked pretty impressive from the photos I'd seen, so I decided that would be the one I visited. I wasn't entirely sure how long my journey would be that day and I wanted to make sure I had enough time to get to the things near the end of the route and then get back to my hostel in time to cook some dinner, so I decided just to stop at that one.</p>

