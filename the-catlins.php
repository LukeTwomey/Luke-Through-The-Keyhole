                    <p>I continued driving through gorgeous countryside and winding coastal road, congratulating myself on making such a brilliant decision to hire a car and go down there! It was very quiet too, not too many tourists around – they were still there but not in huge numbers, which was nice. I guess because it was slightly more inaccessible than other places (unless you had your own transportation).</p>

                    <p>Reaching Kaka Point, I parked the car up and walked ten minutes along a path winding around the cliffs to a lighthouse, which overlooked the "Nuggets". These are rocks just off the coast, which, when you see the pics you'll probably agree are aptly named.</p>

