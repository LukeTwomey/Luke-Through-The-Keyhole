                    <h1>Exploring Hong Kong</h1>
                    <p class="post-date">25th April 2012 17:28</p>
                    <p>CouchSurfing. Hands up if you've heard of it! No prizes if I already told you before, that's cheating.</p>

                    <p>The idea is, you stay at the home of a local while on your travels. They offer you a couch/bed/sofa/floor out of the kindness of their heart, allowing you to learn about their culture. This presents a unique opportunity to bypass the typical hotel/hostel frequented by backpackers and really get to know the place you're staying in, as well as gaining a new friend to boot.</p>

                    <p>OK, you're sceptical. Why would anyone do such a thing? What if you get an axe murderer? They like murdering things with axes right? Yes. They do. Fortunately though, you can read reviews from other travellers before staying with someone. This helps you avoid the weirdos and undesirables.</p>

                    <p>I had arranged to stay with Lilian before I left the UK. She kindly offered to host me for two nights on the nineteenth and twentieth of April. She's probably been the best thing about Hong Kong so far, so I'm glad she did!</p>

                    <p>She's put up with me, showed me around places I otherwise wouldn't have seen and generally been pretty amazing and lovely. If you had any doubts about CouchSurfing, I guarantee they'd be dispelled by a stay with Lilian!</p>

