                    <p>Just enough time to get your breath here, before continuing up the steepest section to the very top. The reward was well worth the effort.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-15_8584238259_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-15_8584238259_o.jpg"/>
