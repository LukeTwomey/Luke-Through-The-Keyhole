                    </a>

                    <p>There were many people gathered at this point, as it was a perfect spot to sit and eat your packed lunch and take in the view for a while before turning round and heading back the way you came. You can usually continue onwards, but volcanic activity in the area had forced the authorities to close the track from that point (or a little further on), deeming it unsafe for the time being to go any further.</p>

                    <p>I was quite happy staying there anyway... the path dropped away steeply down the other side and was also nothing but shingle. Watching plenty of people first slide down the slope initially (a lot of them ending up on their arse), then some time later struggle back up, slipping down in the loose rock – I was content to just sit at the top!</p>

                    <p>I stayed there for at least an hour, more I think – taking in the view, before the time came when I had to head back to the car park for the coach (or risk being left behind!). It's funny how the path seemed a lot longer when my feet were beginning to ache and my blisters were rubbing! I got there eventually though, and soon enough arrived back at the hostel to have a well earned break for the rest of the day.</p>

