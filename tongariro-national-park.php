                    <h1>Tongariro National Park</h1>
                    <p class="post-date">28th May 2013 19:33</p>

                    <p>When you travel to the Tongariro National Park, it's with one goal in mind... completing the Tongariro Alpine Crossing. Home to Mount Ngauruhoe (which formed the basis for Mount Doom in the Lord of the Rings trilogy - after being digitally altered, mind) and luckily with less orcs and ringwraiths and whatnot...</p>

                    <p>The footwear I'd bought with me had served me well up until now, a pair of Keen Newport H2 sandals. These are not only at the peak of backpacker fashion, but have a sturdy sole, toecap to save your toes from any nasty impacts and are completely submersible in water.</p>

                    <a href="images/posts/tongariro-national-park/8871839754_088ac08311_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/8871839754_088ac08311_o.jpg"/>
                    </a>

                    <p>I only wore these on hikes, living in flip flops (thongs if you're Aussie) the vast majority of the time. Unfortunately on this occasion, they just weren't going to cut it. In fact, if I had worn them, they would have been the ones getting cut, literally. Here's what happened to some poor guy's shoes, the terrain is that rough...</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-14_8584235075_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-14_8584235075_o.jpg"/>
                    </a>

                    <p>So that meant hiring boots from somewhere. I had the option of going with some relatively new, shiny, modern ones from a local hire shop in the very small town. However, the hostel (never missing the opportunity to make some more money!) also had boots for hire at a much cheaper cost. These appeared to be from the 1800's, but I'd heard boots get more comfortable with age, so I figured these ones must almost float over the ground.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-6_8585285312_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-6_8585285312_o.jpg"/>
                    </a>

                    <p>They didn't, sadly. And they gave me blisters. But at least they didn't fall apart!</p>

                    <p>Up early the next morning, and I loaded myself and my pack onto the shuttle bus from the hostel to the start of the crossing. The bus was full, and there were at least five or six others there when we arrived, that's how popular this walk is. I had loaded my pack with enough food and water to sustain a small army, based on an experience I had in the rainforest in Malaysia, woefully underestimating the amount of water I would need.</p>

                    <p>The sun was still low in the sky as I began walking, making for a nice warm light over the rocky, dry landscape.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-2_8584172735_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-2_8584172735_o.jpg"/>
                    </a>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-3_8584175905_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-3_8584175905_o.jpg"/>
                    </a>

                    <p>It was cold at first, but the sun rapidly increased the temperature, and I soon had to stop to remove my fleece and take on some water. The going was easy at first, nice and flat and very little effort. It would have been easy to get lulled into a false sense of security, but I knew it would soon get a lot tougher!</p>

                    <p>Sure enough, on approaching the last of the available toilet stops along the route, you could see the path rise steadily up and up the rock. This was a little harder... it always is going uphill! Nice views when you get up there though.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-5_8584182439_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-5_8584182439_o.jpg"/>
                    </a>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-4_8584179133_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-4_8584179133_o.jpg"/>
                    </a>

                    <p>On reaching the top of that climb, you're presented with two choices. You can either climb Mount Doom, or continue further along the track and get to a different lookout point, above the Emerald Lakes. Some brave (crazy) people I'd spoken to had actually done both in the same day before. But, I wouldn't have had the pleasure of doing this even if I'd wanted to. The bus driver had already told us that we would only have time to do one of the two, before they came back to pick us up at the end of the day.</p>

                    <p>As cool as it would have been to say I climbed Mount Doom, it looked like way too much hard work for me! There was loose scree all the way up, and for every couple of steps up you took, you would slide down almost the same amount. After standing at the base watching people struggle upwards, I decided I'd be perfectly happy seeing the Emerald Lakes! I left the hard work to Frodo and Sam.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-16_8584239363_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-16_8584239363_o.jpg"/>
                    </a>

                    <p>Yes, those people are actually dressed as hobbits. No, I have no idea why either.</p>

                    <p>After choosing the easy option, there's a long flat, straight again before you reach another steep uphill climb. This one was trickier, as there were no steps so you had to mind your footing. There was still solid ground for the most part though, so it was a hell of a lot easier than the alternative I'd already dismissed.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-17_8585341846_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-17_8585341846_o.jpg"/>
                    </a>

                    <p>Unsurprisingly, around halfway up the path levelled out to provide more fantastic views.</p>

                    <a href="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-8_8585293528_o-large.jpg" class="fancybox">
                        <img src="images/posts/tongariro-national-park/national-park--the-tongariro-crossing-8_8585293528_o.jpg"/>
                    </a>

