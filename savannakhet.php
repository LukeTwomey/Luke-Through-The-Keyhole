                    <h1>Savannakhet</h1>
                    <p class="post-date">15th August 2012 18:19</p>

                    <p>Savannakhet turned out to be a bit of a failure all round, but despite that I still enjoyed it.</p>

                    <p>I had intended to go on a guided trek the day after I arrived, to one of the protected areas (national parks) in the vicinity, called Dong Natad. You would learn about the park, the animals, try eating berries and plants etc while making your way to the centre and Nong Lom lake. I'd wanted to do this with a guide as apparently there are lots of paths and I didn't fancy getting myself lost in a forest.</p>

                    <p>So when I arrived I headed straight to the tourist information centre, enquiring about how much it would cost. It worked out to around $60, which is more than I was expecting, and in the end after debating it for a while I decided to save my money. There are apparently far better trekking opportunities in Luang Nam Tha, where I'm heading later. The main problem I'm having with the tours in these places, is they are all very expensive if you want to do them alone. The price decreases if you go in a group, but it can be tricky finding other people who would want to go as well. There were two others who wanted to do the same tour, but they had left the day I arrived.</p>

                    <p>With the trek off the cards, I moved onto my next item on the list. The Dinosaur Museum. This had very good reviews online, and apparently was a good way to spend an hour or so. I headed over there on my rented bicycle, locked it up and walked in, only to be told by a workman that it was closed for renovation. Things weren't working out so well...</p>

                    <p>In the end I contented myself with cycling around the town enjoying the laid back atmosphere. It felt good to be back on a pushbike again, even if it had seen better days. I went for a haircut, picked up some laundry, bought some toothpaste, and generally just did "stuff"! All very boring and mellow, but it was enough for me. The Beer Lao sipped by the Mekong River went down pretty well, too.</p>

