                    <h1>Wanaka</h1>
                    <p class="post-date">8th May 2013 17:06</p>

                    <p>Wanaka is a glorious little town situated (surprisingly) next to Lake Wanaka, in the Otago region on the southern island. Despite arriving on the coach on a cloudy, slightly drizzly day, the appeal of the place is all too apparent. From the lakeside you are treated to spectacular views across the water to the base of mountains as far as the eye can see (certainly not something New Zealand is short of!).</p> 

