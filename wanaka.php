                    <p>My initial upbeat mood was somewhat dampened when I arrived at my hostel though - I checked into my room no problem, set my bag down to open it as I'd done countless times before, when suddenly one of the straps holding the lid on the bag snapped off! I guess that's what comes of accumulating too much extra stuff along the way! I tied a knot in it, and it lasted me as a makeshift repair until I got home. As part of their "ALL MIGHTY" warranty, Osprey - the manufacturer of the bag - have let me know where to send it for repair or replacement. This lasts for the lifetime of the pack, and is great peace of mind and great customer service. So despite evidence to the contrary, the bag has been fantastic. If you're looking for a new rucksack for any reason I can definitely recommend the Osprey brand. Trust me, it's taken a lot of beating on the trip.</p>

                    <p>Wanaka is not the biggest of places, but that's really its appeal. It's a good place to go to enjoy astounding scenery, in a pleasant, quiet town environment.</p> 

