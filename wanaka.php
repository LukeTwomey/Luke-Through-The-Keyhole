                    <p>The following morning things were ever so slightly rushed. I somehow managed to turn the alarm off on my phone and go back to sleep. When I eventually woke up, and looked at the time it was five minutes after when the coach was meant to be arriving to go to my next destination. This wouldn't have been quite so bad had it actually stopped at the hostel â€“ but as Wanaka is so small, they just stop at the visitor's centre by the lakefront, five minutes' walk away. I quickly jumped out of bed, chucked all my stuff in my backpack and raced out the door, praying I hadn't missed it.</p>

                    <p>It didn't look good. I arrived in the car park and it was empty. No coaches. I got my phone out and called Intercity, the coach company. "Was the coach from Wanaka to Fox Glacier running late at all?". "According to our information the coach was running on schedule Sir". Shit. Missed it. I was just contemplating stealing a car to race after it and intercept it further along the road, when I swung around and saw it turn the corner into the car park! Hoorah! I celebrated wildly by skipping around the car park gaily and throwing fresh flowers into the air, delighted that I had made it. Birds sang, tiny woodland animals clapped their paws in joy.</p>

                    <p>I boarded the bus, and contentedly settled down into my seat for the journey. A couple of hours in, approaching a rest stop, I had literally just thought to myself "wow, how lucky was that?!", when I remembered I'd left my camera in the locker at the hostel. Bollocks.</p>

