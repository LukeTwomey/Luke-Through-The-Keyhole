                    <p>You can hire a large boat to take you out on the lake somewhere. I'm not sure of the details exactly, but the reason I mention it is I was sitting by the lake one evening as it was returning and heard drunken shouts of "Three! Two! One! Wheeeeyy!" as a guy was chucked into the water by his mates. The funniest part (now looking back - not so much at the time) was observing this guy trying to swim to the shore - clearly struggling - before the captain of the boat deemed the situation serious enough to turn the boat around AND launch the fast dinghy off the back to go and pick him up. Let that be a lesson to you folks... go swimming when you're drunk. It's hilarious entertainment for the whole family!</p>

