                    <p>A short walk out of town lies Puzzling World, a place dedicated to illusions and puzzles. There are lots of fascinating exhibits here, including a hall of faces, which when you cover one eye and move around the room actually appear to turn their heads to follow you - creepy stuff!</p>

                    <p>You walk slightly uphill to enter another room (very difficult to walk in!) which has a few interesting things to try that work based on your sense of 'level' being skewed. You can put a ball on part of an upwards slanted pool table, which will then magically roll uphill and into the pocket! The table is actually one or two degrees sloped downhill, but your mind tells you it is sloped upwards due to the angle of the room itself.</p>

                    <p>In the same room there is also a series of streams of water running into each other, which appear to be impossibly flowing uphill!</p>

                    <p>The Ames room somehow makes you appear tiny in one corner, and huge in the other. I have absolutely no idea how it works, but it's really clever nonetheless! This technique was apparently used in The Lord of The Rings films, in order to make the actors playing the hobbits appear smaller than they actually are.</p>

