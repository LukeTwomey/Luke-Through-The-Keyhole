                    <h1>Arrival in Hanoi</h1>
                    <p class="post-date">14th May 2012 16:31</p>
                    <p>They played surreal,  remixed, poor quality Vietnamese music videos on the bus having crossed the border. It didn't take me long to feel like sticking screwdrivers through my eardrums rather than listen any longer. Looking back on it, I have no idea why I didn't put my headphones in and listen to some Aqua or Britney Spears. I can only assume I was somehow brainwashed by the shockingly bad singing and psychedelic videos.</p>

                    <p>Arriving in Hanoi makes it all worthwhile though. Aside from being physically accosted by 'moto' and taxi drivers as soon as I stepped off the bus, I was happy to get here. As it turns out, I didn't actually have a clue where 'here' was, so I did end up taking a moto (a ride on the back of a motorbike for a small fare) to my hostel. That alone was an experience in itself, the place is massive and packed full of backpackers from all over using the bar, restaurant and computers there. I could barely hear the girl checking me in down in the reception area, quite a change from others I've been in where it's just me and a few other people!</p>

                    <p>I'm staying in the Old Quarter, and when you step out on the narrow streets you had better have your wits about you, or you'll easily get hit by a car or a motorbike. The pavements aren't actually pavements, they're simply used as motorbike parking. Pedestrians effectively walk in the gutter, bicycles and motorbikes are slightly further over and cars/vans/buses occupy the middle.</p>

                    <p>There will regularly be occasions where you have to stop walking as there's no more room left in the road when a car comes through, and the motorbikes divert around it directly into your path. For this reason I tend to prefer walking on the left hand side of the road so I can see the traffic coming at me and if necessary jump out of the way accordingly!</p>

                    <p>Hoan Kiem Lake is in the historical centre of Hanoi, and is generally a good place to start sightseeing and getting your bearings from. Turtle Tower stands in the middle of the lake, and further along you have the Temple of the Jade Mountain also on its own island:</p>

