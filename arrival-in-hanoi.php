                    <p>The next day we visited Hoa Lo Prison, sarcastically referred to as the 'Hanoi Hilton' by US prisoners of war who were imprisoned there. Most of the prison was demolished, but what remains has been made into a museum, there are also some of the original cells. The museum made a point of emphasising how well treated the American POW's were during their stay at the facility, but apparently torture was commonplace. It was a very sobering place, with an eerie atmosphere to it, especially looking into the cells through the old spy holes in the door.</p>

                    <p>That night we went to a Korean restaurant where you cooked your own food at the table. I had never been to a place like this before, and it was a really fun experience (if a little hot!). The food tasted lovely, all thanks to pro chef Vicki:</p>

                    <a href="images/posts/arrival-in-hanoi/01_Camera Roll-403-large.jpg" class="fancybox">
