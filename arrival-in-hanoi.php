                    <p>It may sound a little odd visiting a Korean restaurant in Vietnam, but I'd never been and just fancied a change. I'd had no shortage of Vietnamese food by this stage, and it tastes amazing. My favourites are Pho, which is basically a noodle soup which you can have with chicken or beef, and Bun Cha which is skewered pork served with noodles and a tasty soup thing again which you put some of the noodles into, along with some of the pork, then some salad (don't ask me what, just green leaves!) and eat all together. Repeat the process. It tastes delicious! I don't have any pictures though. Mental note, start taking photos of my food.</p>

                    <p>We also visited the army museum, which I unfortunately didn't get any photos of. But they did have some nice old planes and tanks and things to wander around. They also had the wreckage of a bomber which was shot down during the war.</p>

                    <p>Vicki had to go back to Beijing the next day, where she's studying. She had already extended her stay longer than she'd originally intended anyway, so had no choice but to go back. I booked the next tour for myself for the following day, to Halong Bay.</p>

                    <p>This was a two day, one night tour, sleeping on the boat. You get picked up from Hanoi and driven to the harbour where you sail out to Bai Tu Long Bay - a quiet part of Halog Bay where only one or two companies are licensed to go. I deliberately chose this, as while it was slightly more expensive I thought it'd be worth the money.</p>

                    <p>You get to visit Thien Canh Son Cave, go kayaking for a few hours, swim from a white sandy beach for around an hour and the next day visit Vung Vieng floating fishing village before returning to Hanoi.</p>

                    <p>This has probably been my favourite thing so far. The boat was POSH, the kayaking was so much fun, the scenery was fantastic and lying out on the deck at night under the stars with nothing but silence and a nice cold beer was so relaxing.</p>

                    <p>The people were also very cool, there was only sixteen in total so it was easy to get chatting with everyone. I was sat on a table with three people from Melbourne (Des, Rhonda and their son, Phil), as I was on my own and so it made up a four. Des actually managed to pick up Irish in my voice, I couldn't quite believe it and I'm still not actually sure how this is possible. My Dad's side of the family is Irish, but I've lived in England all my life, how does that work?!</p>

