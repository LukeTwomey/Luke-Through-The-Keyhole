                    <p>I leave you with my tip of the trip so far. Don't leave your debit card in an ATM. They're useful to help you get cash, which in turn helps you to live. Luckily I bought a spare card which I'm getting money with, but I'm paying a charge, the one I lost was free.</p>

                    <p>In fairness, it's not my fault! The machines here are the opposite to the UK, cash first, THEN card. Stupid ATM. Phoned the bank to cancel the card and a replacement was ordered. My parents have received it back at home so now it's just working out the best way to get it sent out here! What a pain in the arse.</p>

