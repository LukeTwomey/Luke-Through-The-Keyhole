                    <p>It was while I was down at the lake having a well earned rest on a bench that I got chatting to a Chinese girl named Vicki. She was staying in Hanoi with a friend for a short while and had already been wandering around the city getting lost for a couple of days (her sense of direction is woeful) but still enjoying herself all the same! We ended up hanging out together for the next few days, going on tours and checking out the city.</p>

                    <p>We first went on a day trip to Hoa Lu, which used to be the capital of Vietnam before Hanoi. We visited temples which were built for emperors Dinh Tien Hoang and Le Dai Hanh, their sons, and Queen Duong Van Nga. This was all very nice, it's just a shame you couldn't walk ten metres without a local trying to sell you hats, drinks or fans.</p>

                    <p>After the temples we had lunch, which was a buffet style affair. I tried some fried, roasted and grilled goat. I wimped out the first time around, but got some on my next plateful. It actually wasn't all that bad, tasted like chicken (not really).</p>

                    <p>After lunch we took a short drive to the village of Tam Coc, where we boarded small rowing boats and took a trip along the river, passing under three limestone tunnels. Look closely and you'll see these villagers rowing with their FEET. I have no words to describe how awesome they are. They were proper cheeky buggers though, we had already been advised by the guide that a tip is appropriate, so we had ready 20,000 dong each (there were two ladies rowing our boat). Instead of waiting for us to turn round after the end of the ride, they forcefully prodded us in our backs and demanded "tip, tip!". We handed the two notes over, advising there was one for each lady, and one of them tried keeping both then asking for more! We politely refused.</p>

