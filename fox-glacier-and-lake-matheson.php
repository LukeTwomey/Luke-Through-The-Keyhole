                    <p>I could always have got up the following day (I'd deliberately scheduled a rest day after the hike!) and walked back for sunrise, but..... it would have meant getting up, and it also would have meant walking the round trip to the lake again. I decided I was quite happy with the pics I had!</p>

                    <p>I'd stayed past sunset where I was, as I thought the sky would have been more spectacular. This meant walking back around the lake, surrounded by forest in the growing dark, with just a dim headtorch to light the way. It was quite unnerving, to say the least. I could hear noises around me that I'm sure I wouldn't have heard in the daylight! My pace quickened, and I was soon back at the (also dark) road. At least there were fields either side of that though, so some light from the sky could be seen. But I had another odd moment, when I was sure I heard a faint voice behind me. I looked around and thought I could see a dark sillouhette approaching. The shadow grew closer, until eventually I could make out a guy jogging along in complete darkness, occasionally singing random song lyrics to himself to pass the time. He passed me, nodded, and vanished into the distance. Nothing weird about that at all...</p>

