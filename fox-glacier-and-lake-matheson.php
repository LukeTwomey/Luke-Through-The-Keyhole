                    <p>After arriving back in town from the glacier, I decided – being the glutton for punishment that I am – to walk to Lake Matheson and check that out. It's famed for having a brilliant reflected view of Aoraki/Mount Cook and Mount Tasman, which I was obviously keen to photograph. So, after a 7km walk on tired legs, and a further 1km around to the far end of the lake, I was sadly disappointed! The view was still nice, but unfortunately just a little cloudy, which obscured the peaks! Ah well – you can't win them all.</p>

