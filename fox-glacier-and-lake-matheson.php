                    <p>Tall walls of ice surround you as you climb further up, only to reach huge crevices sinking deep down as far as you can see. The glacier is apparently receding at the moment due to there having been unusually dry weather in the country for that time of year. It was actually the driest February on record while I was there... lucky me! As an aside... I also got very lucky with the weather in Australia too. Having passed through Bundaberg, I kept in touch with the girls I met in the dorm there, and they'd all been evacuated due to flooding, they sent me these eye opening photos.</p>

