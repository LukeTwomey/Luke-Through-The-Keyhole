                    <p>Continuing further up the glacier, as you reach a less steep section where there are no steps in sight, you are shown how to walk correctly with the crampons on your feet. When going uphill, walk "like a duck" and point your feet out to the side, when coming down a hill, bend your knees SAS style, legs wider and dig in hard with each step. And try not to get too distracted by the views and lose your footing!</p>

