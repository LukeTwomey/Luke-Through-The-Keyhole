                    <h1>Fox Glacier & Lake Matheson</h1>
                    <p class="post-date">14th May 2013 18:42</p>

                    <p>Leaving my camera behind was a pretty stupid thing to do. Not entirely unexpected though, given my previous track record. At the next stop off point, I wandered down the coach to speak to the driver. This set in motion something I wasn't expecting at all, and something I'm sure would never have happened back home! The driver told me to give him the key. He said he would take it back down with him the following day, hand it over to another driver halfway along the route, and get him to drop it at the hostel. I should phone the hostel and ask them to open the locker when they receive the key, then drop the camera off to the information centre where the coach leaves from the following morning (checking first with the information center that they would't have a problem with this). The driver would then pick it up from there, bring it up halfway again, then hand it over to my driver who would give it to me when he reached Fox Glacier... Believe it or not, all this went off without a hitch! Two days after I arrived, I met the coach in Fox Glacier, and there was my camera, safe and sound. Thanks Kiwis!</p>

                    <p>Fox Glacier is 13km long, which is slightly longer than the 10km Franz Josef glacier, some 23km further North. I had heard that recently it had become impossible to access the Franz Josef glacier by anything other than helicopter, which obviously would have made the whole thing a fair bit more expensive. Fox was still accesssible by foot, which basically decided for me which one I should visit.</p>

                    <p>The Fox Glacier Guiding company offer a multitude of trips to the glacier. When walking (and not riding in the helicopter), you have the option of a half day walk (requiring a "moderate" level of fitness"), or a full day walk (requiring "good" fitness). Now, "good" is probably the last word I would have used to describe my fitness as at the time, probably not even "moderate". More like "shockingly bad" to "woeful". But, I'd come to Fox to go on the glacier, and I wanted to make sure I spent the longest time possible up there! If I'd have collapsed from exhaustion, they could have airlifted me out of there anyway...</p>

                    <p>The morning of the hike, we were fitted with crampons (spikes for your boots), walking poles, coats, boots, socks, bags and any other equipment we thought we might have needed. We were then herded into the old school, renovated bus (nice touch), and headed off on the short trip to the bottom of the glacier. I have to say, it wasn't exactly a bad view.</p>

