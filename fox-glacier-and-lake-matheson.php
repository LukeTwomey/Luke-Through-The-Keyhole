                    <p>It was warm at first, with the sun out... but when we stopped to eat our lunch it went in. Despite wearing a long sleeved top, a t-shirt over that, and then a fleece, I was still cold. Who would have thought it, standing on a huge chunk of ice?! The guide had a spare coat stashed away in their rucksack that they lent me, along with some super stylish gardening looking gloves.</p>

