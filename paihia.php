                    <p>On the way back we stopped in the pleasant seaside town of Russell, home to no more than 1000 people. A little bonus of going on the tour is that they gave you a free ferry ticket back to Paihia in the price, so I stayed there for an hour or two and the tour boat returned.</p>

                    <p>There was a lookout there I could have walked up to if I was feeling energetic – on this particular day I wasn't, so I contented myself with just wandering around and then sitting down with an ice cream by the water!</p>

