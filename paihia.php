                    <h1>Paihia</h1>
                    <p class="post-date">18th June 2013 13:27</p>

                    <p>The coach pass I'd bought when I first arrived in New Zealand allowed me to use my assigned number of "trips" to book various tours throughout the country. I'd used some of these already for my Milford Sound and Glowworm cave adventures, and the next one I'd arranged was a boat trip in Paihia.</p>

                    <p>This wasn't just any boat trip though... oh no... this was a dolphin boat trip! I was hoping this one would be a little livelier than the last one I'd been on way back in Cambodia. Technically I'd seen dolphins there, but only for a second or two at a time – just as they were ducking back under the water. Much like this:</p>

