                    <p>I nearly didn't get to enjoy the dolphin shenanigans though, as the boat ran into engine troubles not long after it left the dock. We were floating in one spot for maybe half an hour while the crew worked on it below deck. Luckily they got it up and running again and back up to full speed, and we were soon flying along with the dolphins swimming alongside.</p>

                    <p>The boat was packed, and I had to push a few people overboard in order to get room to take my photos from the side of the boat. I think it was a worthwhile sacrifice.</p>

                    <p>The tour was named the "Hole in The Rock" tour, as, funnily enough, one of the attractions was a big hole in the rock in one of the bays, big enough to sail the boat through. This was clearly a popular thing to do, as we weren't the only big boat there to make the trip. We queued up behind them and watched them breeze through first, before bringing up the rear. It was quite amusing listening to the Captain on the tannoy before we went through... she sounded quite nervous as the water was a little rougher than usual. Potentially a Titanic style disaster, but thankfully nothing that serious!</p>

