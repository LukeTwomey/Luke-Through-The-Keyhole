                    <p>We safely returned to Nahin, once again navigating the flooded road, and I began the second leg of the journey back to the junction I'd visited earlier that day. I was assured I could get on many buses here from various different places when they stopped for a refreshment break, and continue my journey north to the Laos capital, Vientiane.</p>

