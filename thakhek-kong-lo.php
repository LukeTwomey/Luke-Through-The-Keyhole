                    <p>We reached the end of the path, returning back to the water where our boat was waiting. We continued on for what seemed like forever (in reality it took us just over an hour to get from one end to the other) until eventually we literally saw the light at the end of the tunnel. As we emerged out the other side a spectacular view of lush green mountains, shrouded in mist greeted us. We continued some way ahead, before turning the boat around to return the way we came. We were running against the clock to get back to the songthaew, where the driver was concerned the water level could rise further making it impossible for us to drive back.</p>

