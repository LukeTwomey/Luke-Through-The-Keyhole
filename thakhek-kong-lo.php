                    <h1>Thakhek, Kong Lo</h1>
                    <p class="post-date">17th August 2012 06:53</p>

                    <p>Getting to Thakhek from thakhek-kong-lo was easy. One bus. Getting to Kong Lo from Thakhek was a total pain in the arse, but completely and utterly worth it.</p>

                    <p>I'm sure, like me, you often think to yourself "I just wish there was a cave which I could travel through by boat, in complete darkness save for a couple of head torches, for 7 kilometres". Fear not my friends! I've found just the place.</p>

                    <p>From Thakhek, catch a 7am bus to a random road junction three hours away. Catch a songthaew (a kind of flatbed truck with benches and a roof bolted to the back for carrying passengers) to Ban Nahin, arriving there one hour later. From Nahin, catch another songthaew to Kong Lo, roughy another one hour trip (or at least it should have been). Apparently three days of almost nonstop rain do bad things to the Nahin - Kong Lo road, bad things indeed.</p>

                    <a href="images/posts/thakhek-kong-lo/kong-lo-16_7772817860_o-large.jpg" class="fancybox">
                        <img src="images/posts/thakhek-kong-lo/kong-lo-16_7772817860_o.jpg"/>
                    </a>

                    <a href="images/posts/thakhek-kong-lo/kong-lo-17_7772828538_o-large.jpg" class="fancybox">
