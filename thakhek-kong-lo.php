                    <p>I thought it was game over for sure, but our driver wasn't going to let a piddly little flood stop him, no Sir. He got out and busily started doing something by the side of the songthaew while we all wandered down to the front of the queue to get a better view. One of the trucks didn't fancy his chances, and tried to perform a three point turn to go back the other way. This didn't work out so well for him when the front of the truck went down the ditch he couldn't see underwater, lifting one of the back wheels off the ground and rendering it impossible for him to reverse back into the road. Everybody piled in to help, adding weight to the back, and pulling against the sides to successfully get him back on the tarmac.</p>

                    <a href="images/posts/thakhek-kong-lo/kong-lo-18_7772846650_o-large.jpg" class="fancybox">
                        <img src="images/posts/thakhek-kong-lo/kong-lo-18_7772846650_o.jpg"/>
                    </a>

                    <p>We assumed we were next in line to turn around (hopefully less dramatically than the last attempt) when our driver called us all back into the songthaew. Much to our surprise, we were soon driving round the other stopped tractor things and heading straight into the water. Our driver was clearly a mechanical genius (expert bodger) and had done something to somehow let the engine breathe as we drove (sailed) through the water. We made it across the flood and to the other side where there were more vehicles stopped coming the other way, and were soon flying along again towards the cave.</p>

                    <!-- <iframe allowfullscreen="" frameborder="0" height="270" src="http://www.youtube.com/embed/WGTeKbuIU7E?fs=1" width="480"></iframe> -->

                    <p>We arrived at the river, and having bought our tickets, boarded a long tail boat to be transported to a small beach by the side of the cave opening. The rough water just outside the cave entrance made it impossible to enter that way. We clambered over roughly carved steps and into the relative darkness of the cave opening. Many boats were waiting in the water, along with outboard motors connected to long propellor shafts. Our boat was prepared, and we boarded once again, ready to make our journey into the unknown.</p>

