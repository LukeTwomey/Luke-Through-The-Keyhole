                    <p>As the engine noise echoed off the cave walls, the steady "put put" drowned out the sound of streams of water flowing from the cave roof to the water below. We quickly left daylight behind, and soon the only light to be seen was from the dim head torches we were wearing. At the front of the boat was a spotter, illuminating the way for the 'driver' at the rear. The torches were only just bright enough to see by, and it felt like it would be all to easy to miss one of the cave walls with the light and crash into it, but the boatmen are vastly experienced and know exactly what they're doing.</p>

                    <p>The feeling as you glide through the water in the darkness is unlike anything I've experienced before. It really is astonishing, it seems like its never going to end, and you can sense the cave walls all around you despite not being able to see them. The boat was expertly guided on a very specific path, not only to avoid the cave walls, but the large volumes of water falling from the cave roof which would quite easily have soaked us through in a split second.</p>

                    <p>Partway through the cave, we stopped once again and carefully stepped onto dry land. The spotter jumped off and led us up some stone steps before flicking a switch to instantly illuminate the area we were now in. A stunning view of stalactites and stalagmites greeted us, with amazing rock formations all around cleverly displayed at their best. This went on, and on, as we walked further along inside the cave, each turn revealing a more impressive formation.</p>

