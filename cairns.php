                    <p>The boat did provide the option of diving if you wanted to, once at the reef, but it really wasn't worth it at the places we went to. The first dive site was at Paradise Reef - full of beautifully patterned, vibrantly coloured fish. It was best to swim just at the edge of the reef, as it dropped off steeply into the depths below, as there lay the greatest variety of marine life. But as I was swimming along here, I could see the scuba divers not far below. They couldn't have been more than ten metres down, and I could see everything they could from the surface anyway. The great thing about snorkelling is it's also far less cumbersome than the scuba equipment you need to use when diving. Great for quickly jumping off the back of the boat and getting maximum time in the water with Nemo.</p>

                    <p>I had debated whether or not to hire an underwater camera for this trip, but as I'd already got plenty of underwater shots from my scuba diving in Thailand, I decided against it.</p>

                    <p>Paradise Reef was a fantastic location, I believe mainly due to the fact our tour company "Passions of Paradise" was the only operator allowed to visit that particular reef. This helps ensure marine life remains healthy and as undisturbed as possible. It certainly appeared to be thriving, the coral was very colourful and seemed in good condition. The fish were everywhere, and very diverse.</p>

                    <p>After our first snorkel, we sailed on to our next site at Michaelmas Cay.</p>

