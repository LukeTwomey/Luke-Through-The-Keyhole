                    <h1>Cairns</h1>
                    <p class="post-date">17th January 2012 02:03</p>

                    <p>Hi, my name's Luke, and I'm a nudeaholic. I stayed with another male nudist. I'll let you start wondering about my sexual orientation while I continue with the the blog.</p>

                    <p>Hands up if you've heard of The Great Barrier Reef! *raises hand eagerly* ... Cairns is one of the places you can visit the reef, and having dived in Thailand previously to get a taster of marine life, I was really looking forward to it. The fact I'd dived already on my trip also gave me a conundrum though - I wasn't sure whether to pay a bit more money at the reef and go scuba diving, or save and just snorkel instead. In the end I went with snorkelling.</p>

                    <p>The vessel was a modern, stylish catamaran, with full access at the back to get down to the water.</p>

                    <a href="images/posts/cairns/cairns-9_8235920327_o-large.jpg" class="fancybox">
