                    <p>The beach was a birdlife reserve, but obviously the most interesting stuff was underneath the water. Clownfish, angelfish, stingrays,  maori wrase, butterflyfish, sharks. There was a great variety. Also immediately surrounding the boat were huge numbers of giant trevally, an extremely quick fish that are an apex predator in those waters. The crew of the boat would throw food overboard to the fish, and they're actually clever enough (and quick enough) to see the food in mid-air, and swim to where it will land before it's even hit the water, an amazing sight.</p>

