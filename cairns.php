                    <p>Back on dry land I made my way back to Frank's. Frank was a Couchsurfing host who had very kindly answered an emergency couch request I placed on the website. The hostel I had been staying in was one of the worst I've encountered. There were no useful staff members in the whole place, instead being run by backpackers themselves in return for free board. I was lying in bed one day, when a lost looking Japanese girl wandered into the dorm and asked if there were any beds free. I had no idea, but pointed out the ones immediately surrounding me were already in use. The girl strolled off, presumably to find a useless staff member, and I went out for a while, thinking no more of it. When I returned a short time later, I found all my things had been dumped off my bunk onto the one above me, including my bedding, and the girl had taken up refuge there. Brilliant. Thanks for that. I'm sure you're not likely to anyway, but don't ever stay at Reef Backpackers in Cairns.</p>

                    <p>Having received Frank's invitation, I jumped at the chance. There was a slight catch, however... Frank's home is a nudist one. And it's even more nudey than Stephen and Kevin's place in cairns, as being nude is more of a rule/lifestyle rather than an occasional thing. To the extent that clothes would need to be removed on the secluded patio before even entering the house. Frank was also hosting other surfers at the time as he had a few spare beds. Four naked men sitting down round a table to dinner is something I doubt I'll ever experience again. The funny thing is, it actually felt less weird eating dinner in that situation than it had on the nights I'd spent in the hostel. They gave out daily food vouchers for a huge backpackers down the road, letting you pick up a free "meal" each night. This resulted in a huge queue of people lining up to get their free dinner. It almost felt like a homeless shelter.</p>

                    <p>Frank is a really cool guy, he's hosted hundreds of backpackers already so is well experienced and fantastic for helping out with seeing things in the area. To the extent that he hired a car for us all - splitting the cost between us - and drove us up to Port Douglas and Mossman Gorge on a day out. Above and beyond the call of duty! And a great example of the kindness of hosts you'll experience on the Couchsurfing website.</p>

