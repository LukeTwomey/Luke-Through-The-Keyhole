                    <p>Due to this only being thirty odd years ago, people are still hugely affected today. With over three million people killed, that was every one in four of the population. Every local you meet here in Cambodia has members of their family who suffered. It also strikes you walking around, just how few old people there are in the country.</p>

                    <p>On a (much) lighter note, which i definitely needed after visiting those two places in one day, the following day I booked a tour to Phnom Tamao Wildlife Sanctuary. This place is commonly mistaken as a zoo, but does in fact take in unwanted and mistreated animals before attempting to release them into the wild if at all possible. I had a really great time here, made all the more enjoyable by the lovely tour guide Vathana. She's been doing the tour for years, and the people at the sanctuary are just like family, so she's allowed into all the backstage areas. We got to go into the tiger house for example, which was an unexpected treat. We also got to go round the back of the leopard enclosure, and have some very thin looking chainlink fence seperating us from them. I think this was my favourite animal overall. Vathana's favourite there is Lucky, the elephant. She's very intimidating at first, due to the sheer size of her! But you soon get used to that. Feeding her is a very slimy job though...</p>

