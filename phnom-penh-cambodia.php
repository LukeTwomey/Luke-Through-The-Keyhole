                    <p>After the tour, Vathana invited us back to her home to have dinner (and amazing homemade banana milkshakes!) with her and her husband. On arriving, she entered the kitchen, refusing any assistance from us (apparently we would have just slowed her down!), and emerged later with an amazing homemade feast! Vathana's kindness is indicative of the experience I've had with all Khmer people since I've been here. But then I also thought that of Vietnamese people. I've since spoken to other travellers who have said Vietnamese people weren't that friendly, that certainly wasn't my experience.</p>

                    <p>The last place I visited before I left was the Royal Palace and the silver pagoda. The latter so named due to having a floor covered in silver. However, they cover most of it up with carpet and the bits that aren't covered cant be photographed anyway! Ah well, the buildings look nice outside...</p>

