                    <p>While countless Khmer people died at S21, it would not have been 'efficient' for the Khmer Rouge to execute masses of people here. After torturing them until they could bare the pain no longer, the victims were forced to confess to ridiculous crimes they did not commit, sentenced to death, and transported in groups to Choeung Ek - an old Chinese graveyard a short distance outside the city. Here the Khmer Rouge would unload the prisoners, and execute them next to mass graves. The bodies have since been exhumed, and the bones now lie in a monument at the site.</p> 

                    <p>The bodies that were found have been removed, but as you walk around the edge of the mass graves, on the 'paths' you can still see garments, fragments of bone and even whole teeth as they are gradually uncovered by rainfall. If that weren't horrific enough, possibly the worst part for me was standing by 'The Killing Tree'. Bullets were expensive for the Khmer Rouge, so they would use alternative methods to execute people. Infants would be held by their legs and swung against the tree, then added to the mass grave with their mothers. You can't quite comprehend the evil that would be required to carry out such an act, and I was very overwhelmed not just at that particular location, but in the whole place.</p>

