                    <h1>Phnom Penh, Cambodia</h1>
                    <p class="post-date">11th July 2012, 17:28</p>
                    
                    <p>The ride from Ho Chi Min City to Phnom Penh was an interesting one. From the outset I wasn't even sure when I made it to the Cambodian border if I'd be allowed to cross with my motorbike. I'd read conflicting reports online, some people had no problems, others were refused. I wanted to try it anyway, and if I was turned away I could just return to Ho Chi Minh, sell the bike and jump on a bus to take me there instead.</p>

                    <p>It turned out to be extremely easy. I parked the bike up, walked inside and asked a border official (who, by the way, appear to be the most miserable people in the world) who told me to line up like everybody else, then wheel my bike through when I'm done. Erm, ok!</p>

                    <p>After literally walking through the border, I had only gone a few kilometres when it started raining. Heavily. This continued until I arrived in Phnom Penh, for around four hours. I was pretty depressed by the time I arrived, so checked into the first reasonably priced hotel I found, and headed straight out to the best rated pizza place I found on Trip Advisor! Pizza has become my comfort food on this trip. It's the one western dish I keep coming back to in the various places I've been visiting. Sometimes you just need a taste of home! (England, not Italy). Of course you always need to try the local cuisine, the first Khmer dish I went for was Amok. This is a mild, creamy curry with fish or chicken. It was delicious. I still cannot bring myself to get my camera out and take a photo of my food before I eat it though!</p>

                    <p>While in Phnom Penh, there were two places I really wanted to visit, despite how unpleasant I knew they would be. Tuol Sleng Genocide Museum (S21/Security Centre 21) and Choeung Ek (The Killing Fields).</p>

                    <p>S21 used to be a school but was converted into a prison, and place of torture during the Khmer Rouge era. From 1975 to 1979, the Khmer Rouge, lead by Pol Pot massacred over three million Cambodians. S21 was just one of many such places set up throughout the country. Words can hardly describe the feeling you experience when you set foot inside this place. The walls are surrounded with two layers of razor wire, and there are fourteen graves next to a wooden gallows in the courtyard. These are the graves of prisoners who were abandoned by the Khmer Rouge, discovered by the Vietnamese army when they invaded the country to stop the genocide. The odd thing is, if you ignore the fact there are graves and a gallows in the courtyard, you can still imagine what it must have been like when it was used as a school. Full of children running and playing, laughing with their friends. It's really quite chilling when you then think of what it became.</p>

