                    <h1>Siem Reap</h1>
                    <p class="post-date">1st August 2012 13:53</p>

                    <p>Aka 'Temples R Us'. If you need a temple fix, Siem Reap is the place for you.</p> 

                    <p>I actually saw very little of Siem Reap itself, I spent that long visiting the temples each day. A three day entry ticket to the Angkor complex costs $40, and then you hire a tuk tuk for those days, to drive you round to each site.</p>

                    <p>The temples really are incredible, I couldn't quite get my head around just how huge and complex they were. Angkor Wat is the most famous by far, and gets inundated with tourists (especially at sunrise) each day, hoping for that killer shot.</p>

