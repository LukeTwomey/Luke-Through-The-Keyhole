                    <p>My favourite by far, however, was Beng Mealea. This particular temple has been completely overrun by nature, I felt like Lara Croft (Tomb Raider) exploring that place, just with smaller breasts.</p> 

                    <p>Unlike the other temples you could visit, at Beng Mealea you were granted free reign to climb over all of the ruins and explore other hidden areas that you would not otherwise have seen. Few people deviate from the path like this, so you do have the sensation that you are there alone discovering the crumbling ruins for the first time.</p>

