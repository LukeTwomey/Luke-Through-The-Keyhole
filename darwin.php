                    <p>Darwin was my stop off point for the Kakadu National Park. When I first bought my round-the-world plane ticket I had to tell them which places I wanted to visit in Australia, so they could include those flights in the overall price. I picked some cool "attractions" around the country and went with the nearest cities. Seemed like a good way to do it.</p>

                    <p>Kakadu is actually a lot further from Darwin than you realise. Australia is quite big. Very big in fact - around sixty times bigger than the UK. Gone were the days of just showing up somewhere and easily being able to see the sights - I was forced to book onto a tour. I found one which visited both Kakadu and Litchfield National Parks, camping out in the bush overnight - perfect!</p>

                    <p>Arriving at the tour agent early in the morning, in preparation for a long day of awesomeness, we were rounded up by our tour guide Luke (I could tell immediately he was a great guy) and began our drive to the park. Along the way he handed round a microphone to everyone in the minibus so we could introduce ourselves and tell the group a little of what we were about - a nice way to break the ice. There were of course obligatory Germans - seriously, they're everywhere!</p>

                    <p>Our first stop saw us driving down our first red dirt road of the trip - very exciting! We were headed for the Mary River, where we would board a boat to take us on a little blast upriver and down, in search of crocodiles and other wildlife. Unfortunately all the crocodiles had decided that particular day was too hot for them, and had made their way into the water rather than basking in the sun on the riverbanks. This meant that while we did see them, it was no more than a fleeting glimpse as they briefly surfaced to check us out before submerging again a few seconds later. This made photos nearly impossible, but there was still plenty of twitching to be done:</p>

