                    <p>Overall, combined with the weather, Wellington was a very nice place to be. There are plenty of bars and restaurants, and it's apparently the craft beer capital also. Naturally that night we thought it only rude not to visit a craft beer bar to sample this for ourselves. The beer was good. The price wasn't. Good thing a visit to that particular type of bar isn't the norm, or I definitely would have run out of money a lot sooner!</p>

                    <p>By night, the view from the other side of the harbour is beautiful, as the lights of the city reflect in the water. Unfortunately I didn't have my camera with me, so you'll just have to take my word on that one! Eventually, we got too cold and headed back to the hotel hostel. Someone was watching American Gangster in the TV room. I'd already seen it a couple of nights ago in wellington, but I watched it again anyway. Good film. In my bunk that night, my thoughts turned to my next destination... Mount Doom, with Frodo and Sam.</p>

