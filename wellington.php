                    <p>This is a photo from the top of Mount Victoria Lookout, which gave spectacular 360 degree views over the area. Again, I got very lucky with the weather and had clear blue skies giving great visibility.</p>

                    <a href="images/posts/wellington/wellington-8_8585379526_o-large.jpg" class="fancybox">
