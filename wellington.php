                    <p>Amanda had originally intended to spend longer travelling, but her plans changed to a shorter trip. This meant she had a fair bit of extra money to enjoy while she was there, and certainly made the most of it that night. She'd booked a posh, forty seven course meal (OK, I'm slightly exaggerating) which cost a couple of hundred dollars, including the wine that was specifically chosen to accompany each course. Flo and I were very jealous. Tuna pasta this certainly was not.</p>

                    <p>She enjoyed it immensely. I was shocked and appalled she didn't bring any back to the hostel in a doggy bag for us, but you can't win them all.</p>

                    <p>During a break in our explorations that day, I briefly met up with Sarah on her lunch hour, a Kiwi who had come to work for my old company back in London for a short while. She's a lawyer, and had recently moved to Wellington for a new job, which apparently was going very well. She was still settling into the city and getting used to it herself. Conveniently, right by her workplace was the entrance to the Wellington Cable Car, which took you up to the top of another hill where the Botanic Gardens are. Meeting up with Flo and Amanda again, we made our way to the top via the steep, noisy cable car. Appreciating the views there for a while, we slowly wandered back down through the gardens, heading back to the city centre at the bottom. At this point we nearly got lost. Again â€“ thank God for Google Maps.</p>

