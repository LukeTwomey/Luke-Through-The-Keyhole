                    <h1>Wellington</h1>
                    <p class="post-date">21st May 2013 16:37</p>

                    <p>Wellington. Capital of New Zealand since 1865. Home to the biggest hostel I've ever seen. 320 beds over six floors make it seem more like a hotel!</p>

                    <p>As I arrived in the city, and began the walk from the coach stop, the rain began to fall. I was on a mission to find an electronics store, for a new charging cable for my iPhone. The connector had bent and all the wires were coming out. Running repairs and replacement items are part and parcel of the backpacker experience (especially when you lose things a lot, like me).</p>

                    <p>In my dorm, I met Flo (German, short for Florian) and Amanda, from Washington. None of us had any particular plans while we were there, so we spent the following day exploring the city together.</p>

                    <a href="images/posts/wellington/wellington-9_8585382408_o-large.jpg" class="fancybox">
