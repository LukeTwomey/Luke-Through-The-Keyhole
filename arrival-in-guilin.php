                    <h1>Arrival in Guilin</h1>
                    <p class="post-date">6th May 2012 09:50</p>
                    <p>The drivers here are INSANE. I'd only been in Guilin half an hour and the airport shuttle bus driver nearly killed us by running straight through a red light. The thing is, at the time I thought that was a moment of madness. No. Everyone does it. All the time! </p>

                    <p>The tour coach driver insisted on overtaking at breakneck speed - even when there were other coaches coming head on at us. He seemed to think using the horn would make them magically disappear somehow. They didn't. I'm not ashamed to admit I nearly shit myself on a number of occasions. As far as I can recall it's the only time I've actually been scared in a vehicle. I can only assume he was rushing back to an orgy with fifty hot Chinese women.</p>

                    <p>The horn here seems to mean "I'm in the middle of doing something ridiculously stupid, I'd advise you to get out of my way if you can". The red man at crossings means it's very unsafe to cross, people do it anyway. The green man STILL appears to mean it's very unsafe to cross, as people just drive straight through. Motorbikes and scooters ride down the wrong side of the road, on the pavements and with no lights on at night. Vehicles in general pull out into fast moving traffic apparently with no knowledge there are actually other things on the road with them and pedestrians wander out aimlessly into the firing line, safe in the knowledge their invisible forcefield will allow them to bounce majestically and harmlessly off the front of any oncoming car/coach/lorry and float gracefully back down to the asphalt. Anyway, like I said, insane.</p>

                    <p>So... I'm in Guilin! This wasn't in the original plan, but having been recommended to go there by my friends Chan and Lilian (my Hong Kong CouchSurfing host) it had to be done.</p>

                    <p>I didn't get off to the best of starts. We boarded the plane in Shenzhen, and proceeded to sit on the apron for an hour and a half (longer than the actual flight time would be). Fun.</p>

                    <p>I then nearly died in the shuttle bus, which I've already shared. I finally made it to my hostel at 1am to check in, only for them to say there was no reservation for me, despite having booked online a few days before. After an hour of waving my arms around and trying to explain to the staff who could hardly speak English that I had reserved online, we searched through piles and piles of paperwork to find my booking. Thank God. Another thirty minutes later (while they cleaned my room!) and I was in.</p>

