                    <p>I think you'll agree, it was well worth the trip. The first part was four hours on a big boat cruising down the Li river to Yangshuo, where we had an hour of free time before heading to a small village for a couple of hours bamboo rafting. It was absolutely incredible. The limestone karsts provide a unique backdrop, it makes for quite an experience.</p>

                    <p>The big boat had an upper deck (you can see an identical boat in one of the photos above) where I stood at the front and leant against the railing to take photos. I totally felt like Kate Winslet.</p>

                    <p>There was a moment when I was getting on the bamboo raft, concentrating entirely on not toppling over and falling into the water, when I heard a loud PLONK. Oh. That's not good. I turned around and watched whatever it was sink down underneath the water, then confusingly pop back up again to the surface. It was my bottle of water in the side pocket of my bag! Phew.</p>

                    <p>The biggest thing I am struggling with so far is the language barrier. Hong Kong eased me in very gently, as lots of people speak English there due to it being an official language. Not so in China. I'm getting by (just) but it's harder than I expected it to be. Simply having English speaking hostel staff can be extremely useful. I moved to another hostel for my last night staying here, as I originally wasn't sure how long I would be in Guilin. I couldn't explain to the (non English) speaking staff at the first establishment that I wanted to stay another night, so I gave up and went to somewhere else. If I'd have persisted I'm sure we would have got there, but it was just easier to walk to somewhere else a few minutes down the road. These guys spoke perfect English and it's one less hassle for you to worry about. The hostel almost turns into a safe haven where you can go back and understand people again!</p>

                    <p>One night i did have a nightmare scenario where for half an hour or so my world imploded on me. I was running low on cash (I'd initially changed some Hong Kong dollars to Chinese yuan whilst still in Hong Kong) so tried to withdraw some money from the first cash machine I visited in China. I chose 1000 yuan (around £100). The machine thought about it for a good two minutes, spat out my card, printed out my receipt showing I'd withdrawn 1000 yuan, then nothing, no money. Shit.</p>

                    <p>I tried again at the machine next to it... same thing, but this time no receipt. Oh shit, shit. Tried again with another card, same result. SHIT!</p>

                    <p>By this time I was sweating profusely. This was a pretty bad situation to be in. Sure, not the worst thing that could possibly happen but at the time I was panicking and it felt like a total disaster!</p>

                    <p>I frantically searched for other banks along the road. I found the Bank of China. I tried again. The machine thought about it for what seemed like forever before dispensing some glorious money! Thank Christ for that.</p>

                    <p>I phoned up the other bank and gave them all the details of the three failed attempts. They said they're dealing with it, so I hope it gets sorted and I don't get screwed over. I'll check my account online and see what's happening there.</p>

                    <p>Moral of the story... erm, don't spend all your cash on rent boys? I don't know.</p>

                    <p>That was a long one, thanks for sticking with it! Next up, Nanning.</p>
