                    <p>One that got recommended to me by Amanda - who I'd met in Wellington - was Cafe Melba. I can definitely say the setting was very nice, sat outside at a small table in one of those tiny, quaint lanes. The staff were very friendly, the service was good. The one thing that let it down (which was my fault!) was ordering "white chocolate" as I don't drink coffee, but actually receiving a white chocolate mocha (at least so I'm told by Amanda). I drank it anyway... it tasted like coffee.</p>

                    <p>Continuing steadily downhill, you reach the ferry terminals where the big cruise ships are docked, and then the public harbour surrounded by more restaurants and sailing trip companies. Of course I didn't need any of them, as I'd already purchased my boat at the warehouse earlier that day.</p>

