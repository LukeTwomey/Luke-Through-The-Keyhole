                    <h1>Auckland</h1>
                    <p class="post-date">23rd June 2013 18:25</p>

                    <p>I had mixed emotions arriving in Auckland. It was my last stop in New Zealand, so I was a little upset at the prospect of leaving such a nice country. At the same time I was mega excited as New York, New York was so close I could almost taste it!</p>

                    <p>The hostel in Auckland was another big one, as you'd expect for such a large city. The best bit was there was a couple of young football teams there taking part in some local competition, so they were running around screaming a lot and generally being annoying. No really... it was great. Actually the hostels in New Zealand were very different to the other ones I'd stayed in, in terms of the guests. New Zealand had a much wider variety of ages of people, from young teams and such like, to more "seasoned" travellers who I guess were also taking advantage of the cheap accommodation to see the country. I didn't really see that in the other places I went to, it was mostly just younger people.</p>

                    <p>The most obvious landmark in Auckland is the Sky Tower, which of course is pretty hard to miss. Especially when crazy people are base jumping from the top of it! Well, I guess it is New Zealand after all. I set about exploring, heading in that general direction, but unfortunately didn't see anyone jump when I was at the bottom looking up.</p>

                    <a href="images/posts/auckland/img_9907_8675189212_o-large.jpg" class="fancybox">
                        <img src="images/posts/auckland/img_9907_8675189212_o.jpg"/>
                    </a>

                    <p>Apparently it's a base jump on wires – so you jump off the edge and freefall for 11 seconds before being arrested by the wires and "very smoothly" landing at the platform on the ground – at least that's the plan! So it's not like bungy where you spring back up a couple of times. Presumably so you don't hit the building!</p>

                    <p>The main shopping street looked impressive, plenty to keep people amused if you're into that kind of thing (I wasn't)... at least not whilst travelling with no spare money! Every now and then a little lane would appear at the side with small restaurants and cafes to enjoy.</p>

                    <a href="images/posts/auckland/img_9854_8675194340_o-large.jpg" class="fancybox">
                        <img src="images/posts/auckland/img_9854_8675194340_o.jpg"/>
