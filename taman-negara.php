                    <p>We saw a couple of animals while we were there, tapirs and some kind of an eagle I think. It would sit in the tree and occasionally we'd see it swoop down and grab some prey from the forest floor:</p>

                    <a href="images/posts/taman-negara/taman-negara-33_8059974513_o-large.jpg" class="fancybox">
