                    <p>Inside were twelve wooden, double deck bunks, where you could roll out your sleeping mat and sleeping bag. Trust me when I say, this was probably the most uncomfortable bed I've had on my journey so far! There was a long, thin window in one wall, with a wooden bench next to it where you could sit and look out over a salt lick in the forest:</p>

