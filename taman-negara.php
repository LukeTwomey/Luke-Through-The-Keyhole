                    <p>"You don't know... oh oh..."</p>

                    <p>To reward ourselves for our first day trekking we settled down to a delicious five star beer at the five star resort they have right at the entrance of the park... the one you have to walk right through and gaze at the nice, clean bungalows longingly as you head out to the trails. In case you were wondering, a five star beer is one that looks and tastes exactly the same as a normal beer, but costs literally five times as much. As we were sitting there drinking, we heard music behind us. We turned round to witness a musical welcome, plus free cold towel and drink for some new guests that had just arrived. I wasn't jealous in the slightest.... OK I was a little bit. But it just goes to show how much I could be spending on this trip if I were doing things the "normal" package holiday way. We finished our beers and headed back over by boat to the peasant's side of the river, to our zero star dorm.</p>

                    <p>"You don't know you're beautiful..."</p>

                    <p>That night we'd booked to go on a 4x4 night safari through a huge palm oil plantation. You can also do a walking version, but apparently the one in the truck is the better option as you cover more ground, and have a better chance of seeing more animals that way. And see them we did:</p>

