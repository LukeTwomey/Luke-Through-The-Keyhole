                    <h1>Taman Negara</h1>
                    <p class="post-date">9th October 2012 14:35</p>

                    <p>Ok have One Direction actually taken over the world? I come to Taman Negara, a national park three hours boat ride away from civilisation, and I hear their song playing on the radio in the local restaurant (shack). I also heard someone in Penang singing it too. So annoying. What's even more annoying is it gets stuck in my head and repeats for hours after the event. It's like a form of torture by terrible music.</p> 

                    <p>"You don't knowwww, oh oh, you don't know you're beautiful..."</p>

                    <p>The journey to get to Taman Negara from the Cameron Highlands consisted of a few hours in a minibus - I was the only one apart from the driver! - to Jerentut. From there another half an hour or so to a jetty on the Tahan river, where a boat was waiting to continue the journey for three hours upriver to the village of Kuala Tahan. Not the simplest of places to get to, but I was soon to discover it was well worth it.</p>

                    <p>"Everyone else in the room can see it..."</p>

                    <p>The boat was long and thin, made up of around ten rows of pairs of seats (not exactly seats, but a padded mat across the bottom of the boat, and another to lean against). As I'm obviously travelling alone, I got sat next to another guy in the same boat - pun intended - for the journey. His name was Christoph, and he was also from Germany (I've met so many Germans on my travels it's ridiculous). We ended up staying at the same hostel together, so naturally ended up planning to trek as a pair.</p>

                    <p>"Everyone else but you..."</p>

                    <p>Our first day eased me in gently. We had planned a round trip route to a canopy walkway, and a big hill called Bukit Teresek. Approximately 10km in total. No problem. The view from the canopy and the top of the hill alike were very pleasant. Well worth the effort. I felt like I was on "I'm a Celebrity, Get Me Out Of Here" walking across those walkways...</p>

