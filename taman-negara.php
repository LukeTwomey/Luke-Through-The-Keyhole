                    <p>As well as the above, we also saw foxes, wild pigs, and a leopard cat. This is a small wild cat with leopard style camouflage, hence the name. Unfortunately these guys were all too quick to get any photos. They would freeze momentarily under the glare of the guide's bright spotlight, but then slowly move off and under cover.</p>

                    <p>"If only you saw what I can see..."</p>

                    <p>Apparently tigers have also been spotted here, very rarely - during the rainy season as they head through towards the river, with the national park on the other side. When that happens though, the truck drives off at great speed, as the back of it where the customers sit is completely open! Meals on wheels...</p>

                    <p>The following day we had something altogether more ambitious planned. We were going to trek for a similar distance to the day before, but this time sleep overnight in Kumbang hide, before returning via a different route the morning after. Sleeping overnight is a completely different story when it comes to what you need to take with you. You need at least three big bottles of water (I actually would have liked to have one more), a sleeping bag (I rented one), food, a change of clothes... all of this means you have a seriously heavy bag to start out with.</p>

                    <p>Before we left something really annoyed me as well, I bought a hydration pack for my main backpack before I left - its a waterproof bag you fill with water, with a hose (like a straw) that comes over your shoulder and attaches to a small mouthpiece for you to drink from. It saves you having to reach round for a bottle all the time. I'd barely used it the whole trip, so I was pleased I would finally be able to use it in anger. I filled it, put it in my bag and Christoph and I went for some breakfast. When we returned, the dorm room floor was completely soaked. Straight in the bin went the leaky piece of rubbish...</p>

                    <p>"You'd understand why I want you so desperately..."</p>

                    <p>To begin with the trek wasn't bad at all, I was full of energy and we were making good progress. We soon caught up another bunch of trekkers, who we discovered were also heading to the same hide, so we all walked together. Around the halfway point, we came across a really nice stream with a natural pool where we all took a well earned swim. I needed it by that point as well, I was beginning to struggle and I was literally dripping with sweat. My shorts were no wetter after getting in the water than they were before, that's how bad it was.</p>

                    <p>"Right now I'm looking at you and I can't believe..."</p>

                    <p>After a few more kilometres, I was starting to feel like I may have bitten off more than I could chew! The distance isn't the problem, it's the terrain you're covering. You are constantly clambering up and down as you cross lots of different streams making their way down to the river. I think five months of doing nothing has made me unfit too, as most of the group were doing well. A couple of others were also struggling though. When we finally made it to the hide, those two were already certain they were not trekking back the following morning, but making their way to a nearby lodge, where a boat could be taken back to the village. I wanted to see how I felt after some food and sleep, so I left it until the following morning before making my decision.</p>

                    <p>The hide itself was extremely basic, but very cool. When we arrived I was completely and utterly exhausted and in no mood at all to be taking photos, so I've stolen one of the outside from the Internet:</p>

