                    <p>When I showed it to one of the park wardens, he was (rightly so) less than impressed with my efforts, but advised it was probably a paradise tree snake. Looking on google I think he's right, as they appear to have a yellow underbelly which you can make out in my photo. They're not venomous apparently, which is nice.</p>

                    <p>"The way that you flip your hair gets me overwhelmed..."</p>

                    <p>We saw lots more animals that first day, from the relatively small termites and ants (which are still bloody massive by London standards!), to an iguana type lizard thing (actual name), and monkeys when we got back to the village beating each other up! Check the one on the right with blood dripping from his face... nasty.</p>

