                    <p>I also learnt on that first day my initial reaction when seeing a snake in the wild is not to run away screaming like a girl, but to reach down for my camera and try and get a photo. I'm not sure if that's a good or bad thing. As it turns out, the photo I did get was absolutely rubbish:</p> 

