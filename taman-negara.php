                    <p>The very coolest thing for me though, is during one of the countless times I woke up during the night/early morning, in the far distance I heard an elephant trumpet, and five minutes later the same thing but for longer. The sound carried so far through the jungle, and it was really incredible to hear whilst lying in bed! Sadly we didn't see any while we were there...</p>

                    <p>The following morning I wussed out and got the boat back. My legs were aching simply walking up the steps of the hide, there was no way I could manage another 10km! I got back, had a nice long shower, changed into some clean clothes and did absolutely nothing for the rest of the day! And it felt great.</p>

                    <p>"You don't know you're beautiful!"</p>

