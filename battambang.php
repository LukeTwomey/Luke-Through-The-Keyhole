                    <h1>Battambang</h1>
                    <p class="post-date">31st July 2012 10:10</p>

                    <p>Health and safety freaks need not apply. Or, just avoid the bamboo train.</p>

                    <p>Locals have taken it upon themselves to build 'trains' out of bamboo, which they run on disused railway lines. The trains consist of a small bamboo platform plonked ontop of two sets of wheels, powered by a motorbike engine. The rails have seen far better days, so you're in for a bumpy, noisy unsafe, uncomfortable ride... but great fun all the same!</p>

                    <p>It's a single line track, so if you meet another train coming towards you, the train with the lighter load is dismantled and reassembled once the other has passed. It turned out to be a smart move loading the moto and driver I'd hired for the day onto the train then, as apparently they always take precedence and the other train needs to be taken apart. </p>

