                    <p>We had forced about three other trains off the track before we reached the end of the line. My arse was pretty sore by that point so I was glad to arrive. I'm definitely planning on building my own bamboo train when I get back to London though, it would certainly save me money on train fares.</p>

                    <p>Sadly the future of the bamboo train looks bleak. There are plans to establish proper rail connections between the major cities in Cambodia, which would use the existing track and force the bamboo trains to stop operating. It can only be a good thing to have that kind of new development, but it's still a shame something so quaint has to be sacrificed as a result.</p>

                    <p>After the bone shaking experience on the bamboo train, came the more sedate temple exploration of Ek Phnom, and Phnom Banan. I'd read that these were the poor relations of the Angkor temples in Siem Reap (where I was headed next), but were still worth checking out. I found them impressive to say the least, so I was really looking forward to what Siem Reap had to offer.</p>

