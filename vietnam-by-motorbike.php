                    <p>These roads were just perfect. They wound their way up through the mountains, providing spectacular views and they were all smooth, sealed and intact! On the third day, that all changed.</p>

                    <p>Travelling from Son La to Lai Chau shall forever more be referred to as "Hell Day". The first part of the day - before stopping for lunch - lulled me into a false sense of security. The roads were fine, and the going was good.</p>

                    <p>The conditions soon changed. The road suddenly disappeared and basically turned to rubble. Gone was the nice, smooth tarmac I'd been used to. In it's place were rocks, gravel, potholes and puddles.</p>

                    <p>I had thought this was a temporary glitch. The road would soon be reinstated and I'd be motoring along at a decent pace just like the first two days riding. I was gravely mistaken. It continued like this for the rest of the day. The going was slow and arduous, winding through mountain after mountain, and at one point I doubted I'd reach my destination before dark.</p>

                    <p>Just when I thought things couldn't get any bleaker, I came across mud for the first time in my biking "career". I think I went too slowly and in too low a gear as the bike slipped to the side and I was unable to stop it as my foot also slid in the mud. The end result of this was a footpeg that had been bent up and no longer allowed me to change gear. I managed to bend it back down to the correct level, but it was then too far backwards. It was as I was bending it further forward that the footpeg and stand both snapped off in my hand. I can only attribute this to my superhuman strength:</p>

