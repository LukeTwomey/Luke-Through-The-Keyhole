                    <p>Naturally, when this happened I was filled with happiness and joy. I rode for the next twenty kilometres with my left foot all the way back on the pillion peg, bringing my whole foot forward every time I had to change gear. It was uncomfortable to say the least, and by the time I got to my destination and found a place to stay, I was well and truly over my initial enthusiasm for the bike!</p>

                    <p>The next morning saw me get up nice and early to find a local mechanic. This is easily done in Vietnam, everywhere you go there will be someone available to help you out. I got the peg welded up for approximately Â£1.50. Ridiculously expensive, I think you'll agree.</p>

                    <p>With the bike repaired, I set off with renewed enthusiasm for my next stop, Sapa.</p>

