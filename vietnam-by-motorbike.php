                    <p>So having become an expert motorcyclist in just a couple of days, I was ready for my first big adventure - The Northwest Loop.</p>

                    <p>This is exactly what it says on the tin. Starting from Hanoi you ride a loop up to Sapa, stopping overnight at various places in-between, then back down to Hanoi again. The length of time taken to do this varies, but I'd heard six to seven days was a leisurely pace, so that's what I was aiming for.</p>

                    <p>I set out amongst the mid morning traffic, heading out of the madness that is Hanoi. The road soon quietened down, and it was actually a fairly pleasant ride to my first stop, Mai Chau.</p>

                    <p>Almost immediately after stopping to check my phone for a potential place to sleep, a guy on a motorbike pulled up next to me and tried to convince me to come and stay in his sister's home-stay. I agreed to at least go and have a look, in the absence of a better option, and I'm very glad I did because the place was beautiful:</p>

