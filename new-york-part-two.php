                    <p>This made for very cool photos, but unfortunately it was short lived, as the staff came outside and rounded up all the tourists, closing the topmost observation deck in anticipation of the imminent bad weather. I didn’t have to wait for long. Within a few minutes, the skies opened and torrential rain began falling, forcing the last of the stragglers to run back inside. With no prospect of it stopping anytime soon, I made my way back down, disappointed... consoling myself with the smoky BBQ burger and milkshake I posted last time!</p>

                    <p>This absolutely did the trick, and apparently also had some freaky control over the weather too, as when I came out of the restaurant, the rain had stopped! With nothing else planned that night - as I had intended to hang around at the top for a while enjoying the view and going overboard with photos as I usually do - I decided I would pay to go up again. What the hell, I was only going to be there once... make the most of it!</p>

                    <p>Repeating the journey upwards, with a strong sense of deja vu, I emerged onto the observation deck again to see clouds and lightning in the distance. The camera was set up, and hundreds of photos later I managed to capture one I was really pleased with!</p>

