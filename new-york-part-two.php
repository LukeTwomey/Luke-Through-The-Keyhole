                    <p>Of course no trip to the Big Apple would be complete without a trip in a rather quick elevator (lift, for us British folk) to the top of a very tall skyscraper. You really have two options, Top of the Rock (Rockefeller Centre), or the Empire State Building.</p> 

                    <p>The Top of the Rock has the advantage of being able to see the iconic Empire State Building when you’re at the top. The Empire State Building, well... it’s the Empire State Building! I couldn’t decide between the two, so I did both. In fact, I did Rockefeller twice...</p>

                    <p>As I made my way there, I was looking upwards between the rows of tall buildings at clouds drawing in, and covering up the previously sunny sky. I could have delayed my visit, but the forecast for the next few days was no better. The sun I had been enjoying looked to be coming to an end. I made the decision to go up anyway, but wasn’t out on the top observation deck for long at all before the sky came over very dark, and the wind picked up enormously.</p>

