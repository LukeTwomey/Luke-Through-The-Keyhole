                    <p>The following morning I had a slight problem. I was unable to withdraw any money from the ATM, and I was down to my last $30. Since I’d lost the card for the current account I’d opened specifically for my trip, back in Vietnam - I’d been using my credit card to withdraw money. Not only did that cost me a load more in fees, but once you reach a certain percentage of your credit limit, you are then unable to withdraw anymore. I’d already learnt this the hard way somewhere in Asia. I assumed this was what the problem was again, so logged in and juggled things around to pay off some of the balance.</p>

                    <p>This didn’t work. The following day I was still unable to withdraw money (it should have cleared by then), so I was forced to phone the credit card company. It transpired that due to suspected fraudulent activity, they’d frozen my account. This, despite the fact I’d phoned them once before I left to inform them I’d be travelling abroad, and again somewhere in Asia where they’d done the same thing. The lesson to learn from this is - don’t be an idiot and leave your bank card in an ATM in Vietnam.</p>

                    <p>Having eventually been able to get my hands on some more cash, I continued on my sightseeing extravaganza. The name of the next building I was going to visit was an uncomfortable reminder of what I would soon be heading home to after so long doing absolutely no housework! The Flatiron building is so named because of its unusual shape in construction. Built where an avenue and a street converge, the iron shape is very unique and many people come to view the clever architecture.</p>

