                    <p>If you're a regular reader, you'll know mud is what broke my bike and nearly stranded me on a mountain in Vietnam. I was determined to beat it this time. Remembering some advice I'd been given from another rider on my travels, I attempted to ride through it with a bit of speed and therefore stay balanced on the bike despite any wobbles it may throw. Big mistake. I hit the very first bit of mud, felt the back of the bike slide sideways, somehow managed to stay on and upright as the bike rode off out of control, coming to a halt on the other side of the road from where I'd started, completely covered in mud from the waist down. Directly in front of me by the side of the road was a small, local restaurant (wooden shack). The owner was stood there smiling in amusement, and I had little option but to join him and laugh at my unfortunate situation! He kindly provided a hose which I used to wash myself and the bike down with, so now it at least looked like I'd only wet myself. I parked the bike up by the side of the road, and walked the further 2km to the waterfall. Fortunately, the view was worth it.</p>

