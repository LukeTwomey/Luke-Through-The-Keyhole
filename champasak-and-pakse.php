                    <p>I'm starting to experience a lot more rain lately, I guess that's the inevitable consequence of being here in rainy season! Almost feels like I'm back in London! I always knew that would be the case, it was pretty much impossible for me to be in each place at the perfect time of year. It's only a little bit of water though, right?</p>

                    <p>Pakse is a much bigger place than Champasak (not hard - it has more than one street). Waterfalls are the order of the day there. The problem is, they are relatively far out of town, the closest one being around 40km away. Every option I explored for getting there worked out ridiculously expensive. This included going on an organised tour to the area - because I would have been the only person doing it. Hiring a tuk tuk for the day also would have been very dear.</p>

                    <p>I was only really left with one option... getting back on my old friend, the motorbike and making my own way there. This was the very last resort, remember I had sold my bike in Cambodia due to not wanting to ride in Asia anymore, so I wasn't too keen on hiring one now, but on my budget I was left with little option.</p>

                    <p>I was soon back in the swing of things, even if I was paranoid that every other bike on the road was hell bent on ramming into me. Things went well, up until the turn off the main road to reach the waterfall. I was faced with this:</p>

