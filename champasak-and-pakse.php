                    <p>Usually at this point I would end with a comment about where I was heading to next. On this occasion, however - the journey itself warrants a mention. The local bus I'd boarded to Savannakhet was completely overloaded with vegetables, the kind of overloaded they manage so well here in Asia. The whole back half of the bus was packed from floor to ceiling, and they were also piled high on the roof.</p>

                    <p>We were casually pulling out of one of the bus stations we'd stopped at, when we heard a horribly loud scraping of metal. I initially guessed the back of the bus was grinding against a car or bike to the side of us, but further investigation uncovered the root of the problem.</p>

