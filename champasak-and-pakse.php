                    <p>The driver was forced to continue driving out of the pothole, the sound of metal grinding against the ground was not a pleasant one, and I was sure the engine would be damaged. Luckily it was ok, and apart from being very bent up, we made it all the way to Savvanakhet with no breakdowns!</p>

                    <p>Oh, we also stopped to pick up a herd of pigs. I kid you not. On one of the routine "oh-why-the-hell-are-we-stopping-again" stops, a truck reversed up to the side of the bus, the luggage compartment underneath us was opened, and the pigs were unceremoniously hoisted by their back legs out of the truck, squealing in terror, into the hold. To their credit, once they were in there I almost forgot they were under my feet until I heard the occasional grunt or squeal.</p>

