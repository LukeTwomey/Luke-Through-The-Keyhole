                    <h1>Champasak and Pakse</h1>
                    <p class="post-date">13th August 2012 11:23</p>

                    <p>Champasak is further north from Si Phan Don, and Pakse further still. I'm combining them as Champasak is basically one short dirt road with some restaurants and guesthouses. Not much in the way of things to do, but then I went there as it was closer to one of the main Laos sights, Wat Phu.</p>

                    <p>Wat Phu is another Khmer temple complex, and UNESCO World Heritage Site. After visiting Siem Reap already and towards the end of the three day pass, almost becoming "templed out" as is commonly experienced by others, I initially wasn't sure if I would visit this place. However, it was on my way up north through the country anyway and I also didn't want to miss anything if I could help it.</p>

                    <p>I arrived at the site, and immediately knew I'd made the right decision. The location was quite unlike any other temple I'd been to in Siem Reap, in a beautiful location at the base of a mountain. Even if the temple weren't there, it would still have been a worthwhile place to visit. As it was, the temple was pretty much "same same, but different" - a common phrase heard in Southeast Asia! The area was the standout feature in this case.</p>

