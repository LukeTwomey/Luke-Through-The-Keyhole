                    <h1>Phonsavan</h1>
                    <p class="post-date">30th August 2012 18:12</p>

                    <p>I spent most of the minivan ride to Phonsavan completely terrified and convinced we were about to plunge to our deaths down the side of a mountain. The driver was absolutely mental, even by Asian standards.</p>

                    <p>I honestly believe he was trying to see just how fast he could throw the van into the corner without it tipping over and sliding off the edge. He was also employing the classic drive-into-the-corner-in-the-middle-of-the-road-then-violently-swerve-back-to-your-own-side-of-the-road-when-you-see-a-huge-oncoming-truck technique. That's my favourite technique by the way.</p>

                    <p>According to a couple of other backpackers sitting up front with him, he also had a micro-sleep at one point, prompting him to drink a couple of cans of Red Bull like cans at the next food stop. Which, coincidentally is where we saw them chopping up a dog in the back. Consequently the only food I ate there was a pot noodle, crisps and biscuits... delicious.</p>

                    <p>Some nailbiting hours later, we made it to Phonsavan alive. I immediately set about looking for tours to the Plain of Jars, my reason for travelling to Phonsavan. The Plain of Jars consists of a number of sites containing thousands of large stone jars. No one knows exactly what they were used for, or how old they are. Theories vary from locals believing they were used by a race of giants to brew Lao Lao whiskey in celebration of a great war victory, to the more widely accepted funeral urns.</p>

                    <p>I soon found myself having the same problem as I've been encountering previously, there are too few tourists around making it extremely difficult to find tours where people have already booked, thereby reducing the cost. With the choice of paying a ridiculous sum of money to have a "private tour", or hiring a motorbike again for a sixth of the cost, I opted for the bike. I was soon whizzing my way to site 1.</p>

