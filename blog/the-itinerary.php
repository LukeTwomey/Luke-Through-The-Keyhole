                    <h1>The Itinerary</h1>
                    <p class="post-date">28th March 2012 15:51</p>
                    <p>Other than the order of the places I'm visiting, I'm trying to keep things as open as possible. This should afford me some spontaneity when it comes to deciding exactly what I'll be doing and when. The plan is:</p>

                    <p>Hong Kong -&gt; China -&gt; Vietnam -&gt; Cambodia -&gt; Laos -&gt; Thailand -&gt; Malaysia -&gt; Singapore -&gt; Australia -&gt; New Zealand -&gt; America (NY) -&gt; Home</p>

                    <p>The journey begins in Hong Kong:</p>

                    <figure>
                        <img src="http://farm8.staticflickr.com/7182/7021844943_8a8ca87a3f.jpg" />
                        <figcaption>Erm, wow...</figcaption>
