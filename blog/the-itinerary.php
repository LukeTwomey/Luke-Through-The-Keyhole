                    <p>I should be in Hong Kong for a couple of weeks, maybe a little less. I'm trying to give myself a decent amount of time in each place. A good friend suggested two weeks may be longer than I'd need, having been there himself, but that's OK. I'd rather be in that situation than feel like I hadn't seen enough of a place before I had to leave. Next up, China:</p>

