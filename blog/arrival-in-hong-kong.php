                    <p>The food situation's been fine so far. I know some people (including myself!) predicted this would be a big problem - for those that don't know, I'm a slightly fussy eater. The general rule I have is meat = good, veg = bad. There are a few exceptions but in general that holds true. I've stuck to things I know (crab dumplings, BBQ pork with rice, egg fried rice with prawns, skewered chicken and beef) and everything's been hunky dory. I
                    might decide to get more adventurous as time goes on, I'm just easing myself in gently!</p>

                    <p>The hostel I'm staying in doesn't have a common area, so I haven't really had a chance to meet anyone yet. I briefly chat to a Spanish guy I saw coming out of his room as we went down in the lift, but that's about it. But tonight I'm meeting my first Couchsurfing host, to stay with her for the next two nights. More on that in the next update!</p>
