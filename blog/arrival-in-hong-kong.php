                    <h1>Arrival in Hong Kong</h1>
                    <p class="post-date">19th April 2012 06:04</p>
                    <p>Well, I made it!</p>

                    <p>The flight wasn't the most fun in the world, I don't have a problem with flying but as predicted, I did struggle to sleep. It's just so hard to get comfortable when you're bolt upright (save for a slightly reclining seat back). </p>

                    <p>Food was OK (they must have fired Gordon due to foul language though). </p>

                    <p>Films were OK (watched Mission Impossible: Ghost Protocol and Moneyball). </p>

                    <p>Flight itself was OK (some fun turbulence where it felt like we were about to plunge to our deaths, but hey - they have to liven it up a bit, right?).</p>

                    <p>First impressions of Hong Kong... manic! There's countless people lining the pavements, all busy going about their day, or trying to tailor clothes for you, or trying to get you to stay in their hostel... If you love big cities, and everything very loud and full on, this is the place for you.</p>

                    <p>I must admit, when I arrived in my hostel room and plonked myself down on the bed, I was completely shell shocked. Thoughts like 'have I bitten off more than I can chew' did briefly enter my head! But having had more time since then I'm going to put that down to tiredness from the flight! I've had a good night's sleep since then and today's been great.</p>

                    <p>By the way, don't come here if you're claustrophobic (or maybe just don't stay in a cheap as chips hostel). The room is tiny. Don't get me wrong, I knew what to expect before I arrived (and had also seen pictures) but it's a different thing entirely when you actually arrive! Actually, come to think of it - you'd never even make it up to the hostel in the tiniest lift you've ever seen, so it's a non-issue!</p>

