                    <p>A-Ma Temple, pre-dating Macau itself. Apparently the naming of Macau originated from the temple:</p>

                    <a href="images/posts/macau-and-farewell-to-hong-kong/IMG_4832-large.jpg" class="fancybox">
                        <img src="images/posts/macau-and-farewell-to-hong-kong/IMG_4832.jpg"/>
                    </a>

                    <p>Macau is a very pretty place and so clean as well. That's something I've noticed both here and in Hong Kong - they're hard to miss - an army of street cleaners everywhere, sweeping up rubbish and hosing down pavements etc. It makes for a very good first impression of the places you arrive in.</p>

                    <p>In terms of food, I saw two things always mentioned that should be tried, egg tarts (a bit like custard tarts back in England) and beef jerky. Both were consumed, and both were delicious.</p>

                    <p>I was about ready to move on, having seen pretty much everything I wanted to in Hong Kong. I made one last stop off at Kowloon Park - this time during the day - to get some photos of the birds there.</p>

