                    <p>The closest beach to Ho Chi Minh is at Vung Tau, around one hundred and twenty kilometres away. This is the place the city dwellers head to on the weekend to get away from the city and enjoy some sun, sea and sand. I thought it would be rude not to check it out while I was in the area. I must admit, I was quite disappointed, in the beach at least. It was very dirty, I guess due to the huge numbers of people who flock there at the weekend. The best beach I came across in Vietnam by far, was at Hoi An, and I didn't spend any time on it! I do slightly regret that one.</p>

                    <p>Vung Tau luckily does have more to offer than its beaches. Ontop of Mount Nho, after a gentle seven hundred step climb lies the Christ of Vung Tau - a thirty two metre high statue of Jesus. Just in case you weren't exhausted enough from the hike up the mountain, there are an additional one hundred and thirty three steps inside the statue itself. The climb up these is well worth the effort, however. At the top you can exit onto the outstretched arms of Christ, affording a magnificent view across Vung Tau:</p>

