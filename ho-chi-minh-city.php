                    <p>The above photo shows our tour guide, Hai. Hai was quite a character. He was explaining to us how the Viet Cong guerrillas living underground during the war were not only male, but female also. He went on to explain how the majority of these women were virgins, as was the lady who worked in the coffee shop (whom he obviously knows on a fairly intimate level). As if this information wasn't enough, we were asked the question... "You know what virgin means?". You could sense the discomfort in the air. Some people nodded uneasily, collectively we were willing him to stop talking. "Virgin means she not yet married, and doesn't know how big the banana is!". Thanks Hai.</p>

                    <p>The Mekong Delta tour took us an hour and a half outside of the city, to Turtle Island. We were first able to try some honey directly from a beehive, before drinking tea with royal jelly added. I don't usually drink tea, but this was actually pretty tasty. Then again I do have a sweet tooth. You could of course purchase the royal jelly for a small fee. We were also presented with a selection of exotic fruit, including the smallest banana I have ever seen:</p>

