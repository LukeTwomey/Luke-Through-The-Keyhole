                    <h1>Ho Chi Minh City</h1>
                    <p class="post-date">4th July 2012 13:14</p>
                    <p>After the journey I'd been on to get here, Ho Chi Minh City certainly had a lot to live up to. I'm delighted to say it didn't disappoint.</p>

                    <p>The first thing I noticed is how much bigger and more modern everything is compared to Hanoi. Staying in the old quarter there, everything feels almost claustrophobic as street vendors, motos, taxis, buses and pedestrians all jostle for position. Don't get me wrong, I like this as it's part of what makes Hanoi feel unique, but I think I like Ho Chi Minh better.</p>

                    <p>Maybe it's my familiarity with London which is swaying me in that direction. In my opinion there is a stronger sense of being in a big city when wandering the streets of hcm, compared to Hanoi.</p>

                    <p>Also if you measure a city by it's skyline, hcm isn't doing too badly:</p>

                    <a href="images/posts/ho-chi-minh-city/IMG_6374-large.jpg" class="fancybox">
                        <img src="images/posts/ho-chi-minh-city/IMG_6374.jpg"/>
                    </a>

                    <p>There were plenty of tours available via the hostel and the various travel agents in the backpacker area, as per usual. Two that I wanted to go on were the Cu Chi Tunnels tour, and the Mekong Delta tour.</p>

                    <p>The Cu Chi tunnels are a network of underground tunnels that were used by Viet Cong guerrillas during the war. They inexplicably used to live down there. I still have no idea how they could possibly do it, I was uncomfortable enough as it was and the tunnels have since been made wider and taller for tourists to move through! Here I am:</p>

                    <a href="images/posts/ho-chi-minh-city/IMG_6381-large.jpg" class="fancybox">
                        <img src="images/posts/ho-chi-minh-city/IMG_6381.jpg"/>
