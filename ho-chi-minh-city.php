                    <p>The area we were dropped off at had people making coconut candy sweets. Grinding up coconut produces coconut powder, which is then pressed in a vice to produce coconut juice. This, plus malt and sugar is cooked together for forty five minutes, when it takes on a 'gloopy' consistency. As it cools it becomes harder, so it is pressed into moulds to produce long strands, then chopped into smaller, sweet sized pieces. These smaller, sweet sized pieces are little cubes of joy. They taste so good I bought three packs of them, one with added chocolate, one with nuts and one plain. Chocolate is the best, just because, well, it's chocolate.</p>

