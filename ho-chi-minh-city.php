                    <p>Not only did the guerrillas have to crawl around in the ridiculously cramped tunnels, they didn't exactly make it easy for themselves to get into them in the first place either. This hatch shows the size of a typical entrance, which would then be covered with leaves and dirt to conceal it:</p>

                    <a href="images/posts/ho-chi-minh-city/IMG_6209-large.jpg" class="fancybox">
