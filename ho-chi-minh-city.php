                    <p>On the subject of food... this little gem would have been more suited to the Hanoi to Saigon post but I forgot about it, and I want to include it here somewhere so I remember it in future (although it is hard to forget). At the various stops along the way during our epic ride, the most common food you encounter is pho, which is a noodle soup with either chicken (ga)  or beef (bo). However, on one terrifying stop we were served the most disgusting looking boiled eggs you have ever seen. They appeared to be covered in veins and looked malformed and misshapen. It was something I imagine an alien egg to look like if it were boiled up and served on a plate. Needless to say, we didn't eat them. I've looked online since, and I think they were duck embryo eggs. This image certainly looks very similar to what we had in our bowls:</p>

