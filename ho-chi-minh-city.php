                    <p>I really enjoyed Ho Chi Minh City and the surrounding area, I thought personally it was nicer than Hanoi, but not the nicest place I visited in Vietnam. That one is a really tough decision in fact. It's a choice between Sapa, Hoi An and Da Lat. Each of those places are quiet and peaceful and I'm fast learning I prefer that environment to the busy cities. Having said that, it's far easier to meet people in hostels and go out for drinks and things in the evenings when you're in a vibrant city.</p>

                    <p>One thing remains constant regardless of where I visit though, the time seems to fly past and before I know it I need to move on to my next stop. From Ho Chi Minh, I ride across the border to a brand new country; Cambodia.</p>
