                    <p>The tours were definitely worth doing. They are very popular, which does mean you end up plodding along in a big group of people, but with the added benefit of a tour guide they do have their benefits. Also for something like the Mekong Delta tour, I think you'd find it difficult and expensive to arrange each individual element yourself.</p>

                    <p>The War Remnants Museum is a very worthwhile place to visit while you're here. It pulls no punches in delivering the cold, hard realities of war and some of the photos in the exhibits there make for difficult viewing. It is obviously very biased against America, but definitely a must see.</p>

