                    <p>Ok so that's not really me. I did go through the tunnel but I had no one to take a photo of me. Choose to believe me or not - I won't hold it against you if you don't! You can see from that random internet picture just how tight it is though. Not the most comfortable of places to be, that's for sure.</p>

                    <p>They showed the booby traps that the guerrillas would set to injure and kill enemy troops. This isn't somewhere you wanted to end up:</p>

