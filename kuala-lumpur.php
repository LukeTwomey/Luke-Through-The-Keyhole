                    <p>Chinatown was great, I really like going there wherever I'm staying. There's always such a buzz about the place, and Kuala Lumpur was no exception. The popular Petaling Street market was the place to go for all your counterfeit goods, cheap food stuffs and evening drinks. I ate most of my meals here, as the food court provides delicious meals at rock bottom prices. It's also fascinating people-watching, with a hugely diverse array of people roaming the street.</p>

                    <a href="images/posts/kuala-lumpur/kuala-lumpur-12_8098920483_o-large.jpg" class="fancybox">
