                    <h1>Kuala Lumpur</h1>
                    <p class="post-date">14th November 2012 03:21</p>

                    <p>Kuala Lumpur (KL) was a great capital city to visit, as it was small enough that you could walk around all the sights very easily, without the need for public transport. Perfect for the backpacker on a budget!</p>

                    <p>I stayed in Chinatown, and had the easiest trip to a hostel from the coach so far - it was right around the corner from where I got dropped off. After an exhausting walk of approximately 50 metres, I arrived at Raizzy's Guesthouse and checked into my eight bed dorm. As sad as it sounds, one of the main reasons for choosing to stay here was the fact they had a plug socket right beside each bunk. Those of you with an iPhone will understand why that little perk is so important to me!</p>

                    <p>Before I arrived in KL, I only really had one item on the agenda, which was a trip up the Petronas Towers. Strangely enough, this didn't actually end up happening due to the relatively high cost, and a very good point I read on a website somewhere. One of the main reasons for going to a place is to get a photo with the iconic landmarks included. It goes without saying, that if you're up the Petronas Towers at the time, you can't really get a great photo with them in! I searched around and discovered a place called the Trader's Hotel, which had a rooftop bar for guests and visitors, with apparently a fantastic view of the towers. Oh, and there was also a pool..... In the bar.....</p>

