                    <p>At one point during my meandering around I noticed what appeared to be a monk walking towards me in the opposite direction. Nothing out of the ordinary there, you often see them in countries here, but what followed was certainly unusual. He stopped me as we passed, and held out his hand whilst holding a small trinket of some kind, promising good luck should I take it. I vaguely recalled hearing or reading of something similar, whereby a "monk" (i.e. fake scam artist) would offer you a sticker or something small and once you'd accepted the "gift", they would demand money for it. Remembering this story, I politely declined, and we both went our separate ways. I'm convinced that had I accepted what was offered, I would have found myself in a tricky situation. A narrow escape, and one that could so easily have turned out very differently.</p>

                    <p>Merdeka Square is where the Malysian flag was first raised in 1957, signifying independence from British rule. Very nice buildings around this area, and a national history museum with a very impressive scale model of the city.</p>

