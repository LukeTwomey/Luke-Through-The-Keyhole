                    <p>Also in Chinatown, a few doors down from where I was staying in fact, is the very intricately decorated Sri Mahamariamman Hindu temple. A very impressive view as you walk past on the street:</p>

                    <a href="images/posts/kuala-lumpur/kuala-lumpur-11_8098928164_o-large.jpg" class="fancybox">
