                    <p>Leaving Kuala Lumpur for Singapore was actually really great. Not because I disliked the place, but due to the best coach I've ever been on! The seats were literally like armchairs, and were so wide they only had three in each row, where you'd usually get four. The legroom was amazing too. Possibly the best thing about it however, was the massaging seats. I'd not been on a coach I didn't actually want to get off until now!</p>

