                    <p>Keen to save money where I could (but only if I thought it wouldn’t detract from the experience), I opted not to do the climb over the Sydney Harbour Bridge, instead paying a fraction of the price to get to the top of one of the pylons. The view from the top of here was still perfectly good, and you have the added benefit of being able to take your camera. On the bridge climb they don’t let you, but are quite happy to take photos for you to purchase at the end of the walk!</p>

