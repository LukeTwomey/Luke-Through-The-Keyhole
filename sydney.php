                    <p>I seriously think I melted a bit. You know how on a very hot day you usually welcome a nice cooling, refreshing breeze? On that occasion even the wind was hot. It was like someone had opened the oven door and blasted a wall of heat at your face. That day had us running for cover to the nearest building with an aircon unit, where we didn't move for quite some time. It just may have been a public house.... I forget.</p> 

                    <p>The following couple of weeks thankfully weren't so hot, but it was still great weather for the many things we had planned while I was there. One of the first places we visited was Cockatoo Island, which used to be a naval dockyard and shipbuilding facility. There are still lots of cool abandoned industrial machines and warehouses to explore, and I loved taking photos there.</p>

