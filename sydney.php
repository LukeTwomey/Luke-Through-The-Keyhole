                    <h1>Sydney</h1>
                    <p class="post-date">22nd April 2013 13:30</p>

                    <p>Sydney! This was the milestone. Finally get to see my friends Ollie and Emma, over Christmas and New Year, and spend some time in just one place for a little while! It's funny how normal it can start to seem flitting from place to place after a while. A stop-off in one place is definitely what was needed.</p>

                    <p>I arrived late at night, after a thirteen hour bus ride from Byron Bay. I'll just say there are definitely nicer ways of spending that amount of time... and in the darkness I almost missed the official sign welcoming me to the city, a dimly lit Opera House in the middle of the harbour! Welcome to Sydney!</p>

                    <p>It was great to see Ollie and Emma again after such a long time. Of course a visit to a drinking establishment was in order, to celebrate my arrival without any significant damage to my person.</p> 

                    <a href="images/posts/sydney/camera-roll-455_8393163321_o-large.jpg" class="fancybox">
                        <img src="images/posts/sydney/camera-roll-455_8393163321_o.jpg"/>
                    </a>

                    <p>One of Emma's first comments on seeing me was "Luke, you're so brown!". I'm usually a fetching shade of pasty white, so hearing this was quite a novelty! The sunny weather obviously agrees with me. It's not like we get that much of it in the UK. I left the country and to punish the rest of you for letting me leave, mother nature sent rain.... and lots of it. However, while sunny weather agrees with me, 41 degree heat certainly doesn't.</p>

                    <a href="images/posts/sydney/camera-roll-550_8394227470_o-large.jpg" class="fancybox">
                        <img src="images/posts/sydney/camera-roll-550_8394227470_o.jpg"/>
