                    <p>Another short ferry ride from the main harbour lies Manly, a very popular seaside location which draws a huge number of people on hot, sunny days. Although we did briefly sit on the beach and enjoy the sun for a while, our main objective here was to visit The Lord Nelson and Four Pines breweries. We spent the rest of the day there getting acquainted with the delicious craft beers on offer! You may notice a theme here... it's just a shame that while the beer at these places is extremely good, it's not quite sold at Southeast Asian prices! Our (or rather, my) amusement for the day came about as Ollie was walking around the ferry on our way there. The strap on his flip flop (thong, if you're Australian) literally fell apart, meaning he had to walk around barefoot like a hobo until we found a shop that sold suitable replacements. It was a hot day, which also meant hot pavements... Ollie nearly running when crossing the road to get to some shade did thoroughly amuse me, especially after my ordeal on the sand on Fraser Island!</p>

                    <p>The Blue Mountains (confusingly enough) are not actually blue. The name lies in the appearance given by the blue mist that is emitted by the millions of Eucalyptus trees in the area on hot days. This gives a faint blue appearance to the mountains from a distance, hence the name. Whatever the reason, the view was quite wonderful, and as with most of my photos, cannot truly be depicted in print. It is always far more spectacular in person.</p>

