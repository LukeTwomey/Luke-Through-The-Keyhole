                    <p>I left a contented man, barely able to waddle to the kerb to hail a taxi to take us home. I’m not going to lie to you, that night was uncomfortable. Maybe I ate too much? .......nah.</p>

                    <p>A brief summary of the other places we went to in case you’re ever in Sydney in future: Harry's cafe, very famous and has the photos of hundreds of celebrities having paid a visit to prove it, amazing pie with mushy peas, mash ontop, with a pool of gravy in the middle of the mash! Schnitzel the size of a small planet from Una's! Steak with the most delicious diane sauce from The Botany View, Mad Mex serving heavy, chunky, delicious burritos, smoked salmon and eggs from Newtown cafe, the biggest fried breakfast I’ve ever seen from another cafe in Newtown... I could go on, but I won’t.</p>

