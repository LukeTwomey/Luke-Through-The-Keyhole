                    <p>As you will no doubt all understand, a man has to eat. Consequently, there were one or two visits to one or two different restaurants to eat one or two tasty meals. Actually I’ll try and blame Ollie here, as he kept on recommending new places to go... I’m the innocent man here! The very best place we went to, without a shadow of a doubt, was Churrascos. This was a brazilian barbecue place where you have a small disc on the table, green on one side, red on the other. When you turn this to green, the food begins – and it doesn’t stop until you turn it back to red! You pay a fixed price for all you can eat, meaty goodness. As soon as the food is cooked, it is brought round all the tables and offered to you all the while your disc is green. We made sure we went on Wednesday rib night... and it was one of the best decisions we’ve ever made.</p>

                    <p>Just some of the delights on offer are: Rump Cap with Ginger & Soy, Baby Beef with Churrasco BBQ Sauce, Chicken Thigh Fillet in Honey & Soy Sauce, Lamb Rump marinated in Lemon & Oregano, Pork Belly served with Sea Salt & Fresh Lime, Pork Chorizo... I could go on, were it not for the drool that’s now all over my keyboard.</p>

