                    </a>

                    <p>It's a good job she laid that many, as out of the thousands and thousands of baby turtles that hatch and find their way to the ocean, only one in one thousand actually survive to adulthood.</p>

                    <p>Our services were then enlisted to carry the eggs up the beach and hand them back to the warden to place in the new nest.</p>

                    <a href="images/posts/bundaberg/bundaberg-12_8260760568_o-large.jpg" class="fancybox">
                        <img src="images/posts/bundaberg/bundaberg-12_8260760568_o.jpg"/>
                    </a>

                    <a href="images/posts/bundaberg/bundaberg-13_8260762810_o-large.jpg" class="fancybox">
                        <img src="images/posts/bundaberg/bundaberg-13_8260762810_o.jpg"/>
                    </a>

                    <p>Here they would lay until a few months later, when they would hatch out and begin making their way to the water. All of the babies hatched on this particular beach end up being female - as the temperature of the sand determines the sex. The hotter sand produces females, whereas on other more white sand beaches, where turtles also lay, the cooler temperature produces males.</p>

                    <p>With the show over, I headed back to the hostel to sleep, and set my alarm for the bus the next morning, to Hervey Bay!</p>

