                    <h1>Bundaberg</h1>
                    <p class="post-date">19th January 2012 05:39</p>

                    <p>Bundaberg didn't really count as a stop, as I was only there for one night, to see one thing.</p>

                    <p>I'd read about a beach there where turtles return annually, to lay their eggs on the beach before making their way slowly back down to the water. Months later, the eggs they laid then hatch, and you can witness the baby turtles frantically flapping their way down to the sea. I was there at the right time of year to be able to see the eggs being laid, rather than hatching. I thought it sounded pretty cool so I decided to stop off there briefly to check it out.</p>

                    <p>Apparently this is not a popular thing to do - out of a whole Greyhound coachload of people, only myself and one other person got off the bus! I like that though, it makes a nice change.</p>

                    <p>Bundaberg also wins the award for shortest walk from the bus to my hostel. I literally stepped off the coach in the bus garage/industrial estate, turned to my right and I was there. Result!</p>

                    <p>This particular hostel was different to the others I'd been to, in that it catered mainly for backpackers who intended to work on their working holiday visa (mine was just a tourist visa). I checked into my room and met some very friendly Taiwanese girls who had already been there some months, working on a farm picking tomatoes during the day. I made them do this pose:</p>

                    <a href="images/posts/bundaberg/bundaberg-1_8260731424_o-large.jpg" class="fancybox">
                        <img src="images/posts/bundaberg/bundaberg-1_8260731424_o.jpg"/>
                    </a>

                    <p>They said for every big bucket of tomatoes they pick, they get $5. Not very good money, but still work!</p>

                    <p>Around 8pm I got a lift from the hostel owner down to the beach, as they were associated with the turtle watching there. As you enter the resort, you're given a group number sticker, then herded around a kind of exhibition, with information about the turtles and the conservation efforts being undertaken by all the wardens at the site. After this, you're moved to a huge outdoor amphitheatre to be welcomed by the head warden and watch a documentary about turtles.</p>

                    <p>Meanwhile, wardens on the beach itself are on turtle patrol, and when they see one coming up the beach to lay, radio back to the head guy. He would then pause the video and call a certain group number to head down to the beach to see the turtle. It's done this way to ensure there are not too many people surrounding the turtle at any one time.</p>

                    <p>There were about five groups in total, and I was in group number three. The first two groups got called, and I was willing another turtle to come to the beach for my group to get called!</p>

                    <p>Luckily, after a further ten minutes the call came through, and we were soon trudging down to the beach following a warden. He explained that photography was only allowed at very specific times during the egg laying process, and he would let us know when it was safe to take pics. When we arrived the turtle had picked her spot, and was busily scraping away at the sand with her rear legs, digging a nice deep hole for the eggs. We gathered around behind her, and the warden explained further about what exactly they do when the turtle is on the beach. They will measure them, to gather as much data as possible about that turtle, tag them if they do not already have one and generally check on the condition of the mother to make sure she's healthy.</p>

                    <p>It was as the warden was rambling, that mum decided the hole was now deep enough, and it was time to begin laying the eggs. Everybody craned their necks to try and get a better view, as the warden reached in to place a flashlight at the side of the hole to help us see. It was fascinating watching the ping pong ball style eggs slowly but steadily dropped into the hole.</p>

                    <p>When all the eggs were laid, it was time for mum to go crazy flipping sand behind her in order to cover up the nest. It was also our cue to take photos, as there was now no danger of disturbing here once she was in this "mode".</p>

                    <a href="images/posts/bundaberg/bundaberg-6_8260736248_o-large.jpg" class="fancybox">
                        <img src="images/posts/bundaberg/bundaberg-6_8260736248_o.jpg"/>
                    </a>

                    <a href="images/posts/bundaberg/bundaberg-7_8259671315_o-large.jpg" class="fancybox">
