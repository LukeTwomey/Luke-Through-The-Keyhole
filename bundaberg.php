                    <p>It was as she was doing this, the warden explained we'd have an extra bit of excitement that night, as mum had been lazy and not crawled far enough up the beach to lay her eggs. This meant the nest was too close to the water, and would not have been safe to leave there. When she had finished, and returned to the sea, we would need to dig up the nest, and move the eggs to a different, hand dug nest further up the beach!</p>

                    <p>She carried on for what seemed like forever, thrashing about in the sand and hurling it everywhere, until eventually she was satisfied that the nest was well covered. She stopped, gave a huge sigh (I guess) and painstakingly dragged herself back down the beach to the water, a job well done. Well, not really - there was still the matter of having to redo her sloppy job.</p>

                    <p>The warden dug down into the nest that he'd marked off before she began covering it, and began pulling the eggs out a few at a time. In total, she'd laid one hundred and forty three eggs!</p>

                    <a href="images/posts/bundaberg/bundaberg-10_8260755566_o-large.jpg" class="fancybox">
                        <img src="images/posts/bundaberg/bundaberg-10_8260755566_o.jpg"/>
                    </a>

                    <a href="images/posts/bundaberg/bundaberg-11_8260758234_o-large.jpg" class="fancybox">
