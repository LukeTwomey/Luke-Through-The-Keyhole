                    <p>On disembarking, I hunted through the crowd of taxi/moto drivers, guest house touts and dive people for Ban's dive school. This company owns Sunshine Divers, and a glowing recommendation from Rob saw me heading straight to their resort on the south of the island on a free moto arranged by the Ban's lady. A nice coincidence meant the two instructors hanging around there when I arrived were Bastiaan and his wife Kim. Bastiaan had taught Rob and Gilly while they were there, so it was nice to be learning with him. It also meant comparisons could easily be made - at the end he said I was the most bestest and awesomest diver he'd ever seen (sorry Rob, sorry Gilly).</p>

                    <p>Within ten minutes of arriving, I was sat down watching the enthralling instructional videos accompanying each chapter I had to read in the manual. Yep, that's right folks, a textbook. This is followed by knowledge reviews of each section, with a final exam at the end of it all. It felt like I was back at school! You can dive without certification, but this will be a one off shallower dive. By passing your PADI Open Water diving course, you can then dive all over the world and to a depth of 18 metres, 30 metres if you go on to do the advanced course.</p>

                    <p>The following day I returned, having completed my homework (yes, it just got worse), to meet my "buddy" for the duration of the course, Eliza. Eliza is from Germany and had been on Koh Samui with two of her friends, but needed a little alone time so headed off to Tao for the diving.</p>

