                    <p>It was tricky at first to actually swim horizontally, as bizarre as that sounds. I had a tendency to always be swimming upwards slightly. By the end of the dive though, it was coming far more naturally to me. We surfaced and Bastiaan seemed pretty happy with his "Dream Team". The next dive that day merely served to reinforce the techniques we'd already set in place, and by the end of the day we felt like expert divers! Certainly compared to when we first entered the water... My ears were still giving me a problem though, but Bastiaan said he had a magic pill I could take which would open up my airways a bit before the next dive the following morning.</p>

                    <p>The alarm was set for 6:30 again (certainly not something I'd been used to doing on my trip so far, but a nice reminder of what I had to look forward to when I returned home again!), and we sailed out to our next dive site, Red Rock. On the way we were treated to a lovely display of vomit from Bastiaan... We had a guy on board called Chris who was taking his rescue dive course at the time. He basically had to know how to handle every kind of dive emergency on (and off) the boat you could imagine. On this occasion, it was Bastiaan. And vomit. All down the front of his body. Nice. I genuinely believed he had actually made himself physically sick, just to give that added sense of realism to the acting as he threw up over Chris, but I later found out it was only cookies and tea or something like that. It looked very, very real!</p>

                    <p>On this dive we got to see a banded sea snake, which was very cool. It was only when we resurfaced and Bastiaan told us that one bite could easily kill us, that it became less so! I was also introduced to a character I'd come to get intimately acquainted with on a later dive - the trigger fish. These guys are extremely territorial, and when they feel threatened a small trigger will pop up on their back. They also have teeth....... On this particular occasion I was swimming after Bastiaan when he signalled me to come towards him, what appeared to be urgently. The next thing I knew I felt a tug on my fin (you're not allowed to call them flippers). I honestly thought I was about to be eaten by a shark. I was too terrified to even look behind me to see what it was! I just knew it wasn't Eliza as she was right next to me, also swimming away at great speed! When we were above water, I discovered it was this type of fish that had bitten my fin as I was swimming across his territory. We would meet again....</p>

