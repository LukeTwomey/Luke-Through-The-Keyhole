                    <p>The maximum class size with Sunshine is four, but as it is low season, it was just Eliza and I being taught by Bastiaan. When diving, you must always have a "buddy" who you dive with, in case you get into an emergency underwater. The main thing is being able to use their secondary regulator to breathe air from their tank if you experience a problem with yours.</p>

                    <p>On our first day we headed off to the swimming pool, where we had to prove to Bastiaan we could actually swim (apparently some people don't actually realise this is a requirement!) and then began our introduction to diving. My immediate reaction when first descending below the surface was one of mild panic. After breathing out fully for the first time underwater, my brain told me it would be impossible to regain my breath. It took me a little while to get used to the fact I could breathe without a problem underwater. It did feel very unnatural though. We practised skills including removing the regulator from your mouth, and learning the correct technique to retrieve it, and removing your face mask and replacing it, before blowing through your nose in order to clear the water from it again.</p>

                    <p>The following day was where the really fun stuff began. We'd graduated from the pool, it was time to get in the boat and head out to sea! Our first dive was at Hin Ngam. It took an age for me to descend to the bottom, as I was having some trouble equalising. You have to do this as you go down, otherwise your ears will start causing you pain due to the water pressure. It's the same kind of thing as when you fly in a plane and have to swallow so you hear them pop. Once we got down there though, it really was something else. I quickly forgot about any problems I'd been having with my ears, as I was completely distracted by the underwater world I was now in.</p>

