                    <p>On the following dive we saw a turtle steadily ripping away at the coral looking for food. It was amazing, I'm just gutted we didn't have the camera that time. There was also a motorbike (not something you'd usually expect!), and a deliberately wrecked catamaran. On this occasion it was my turn to navigate, using a compass and a hand drawn map of the dive site on a slate. I actually did surprisingly well, leading us around a set route and finishing within around twenty feet of where we were meant to be. Not bad for a first attempt!</p>

                    <p>One of the modules I chose to do as part of the advanced course was "Peak Performance Buoyancy". This is basically fine tuning your buoyancy in the water and learning to better control your movement underwater. It was also a good excuse to have lots of fun at an underwater playground!</p>

