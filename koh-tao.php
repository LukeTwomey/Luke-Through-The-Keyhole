                    <p>It figures that following something fun, there had to be something not so fun. Like getting bitten on the finger and the ankle by a trigger fish. We were busy looking at another one, when I felt something tugging on my finger. Thinking it was Bastiaan messing around, I turned to see a much larger trigger fish (Dad!) attached to my finger. He then removed himself and attacked my leg. It hurt.... they have teeth! But I was more worried about it being poisonous. Bastiaan assured me it was ok though. Even so, I got a weird numb/tingling feeling in my foot as if I'd had an injection like you'd have at the dentist, but the doctor said it was nothing to worry about. I really wanted to hunt that fish down and fry him up for dinner.</p>

