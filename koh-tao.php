                    <h1>Koh Tao</h1>
                    <p class="post-date">28th September 2012 15:44</p>

                    <p>Scuba diving baby! Koh Tao is THE place to dive in Thailand. It has the best dive sites, and I've also heard getting certified there can work out two or three times cheaper than elsewhere in the world. It's on the south east coast, so I actually had to pass through Bangkok to get there, before going back up to see Ella later on.</p>

                    <p>To travel south, I opted for the relative comfort of a sleeper train. I'd quite enjoyed my time on one when I was in Vietnam, in a nerdy, childish kind of way. The first one I booked took me from Chiang Mai to Bangkok, where I spent half a day before boarding another to go to Chumphon, before finally sailing on a ferry to Koh Tao. The train has comfy seats in booths either side of a central aisle during the day, with train staff changing them transformers style into beds by night. These little compartments are awesome fun, when the curtain is drawn it feels like you're in a fort like when you were a little kid. Yes, believe it or not I'm actually 27 years old.</p>

