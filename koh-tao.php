                    <p>The final dive was a night dive, which I must admit I was most nervous about. It seems slightly mad to throw yourself off a completely satisfactory boat into the middle of a pitch black sea... we did it though, and it was pretty incredible. We saw things which we never would have seen during the day, like hermit crabs, barracudas hunting, stingrays, a moray eel. The best thing of all is trigger fish sleep at night! We also stopped at one point and turned off our torches, then waved our arms around in front of us, which gave an incredible light show of green bioluminescent plankton illuminating in front of us, it really was spectacular.

                    <p>Having earned my "Advanced Open Water Diver" title, I was free to relax on the beautiful beach at the dive resort for a few more days, before heading back to Bangkok to meet Ella who was flying in on the 21st September.</p>

