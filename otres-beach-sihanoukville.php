                    <h1>Otres Beach, Sihanoukville</h1>
                    <p class="post-date">26th July 2012 07:52</p>

                    <p>As far as they go, this one scores highly on my beachometer. Clean white sand, palm trees, clean water, quiet... I had only intended to stay for a few days, I ended up spending a week here.</p>

                    <p>Everyone I'd spoken to so far who had travelled to Sihanoukville while in Cambodia, recommended Otres Beach. With such high ratings, I headed straight here when I reached Sihanoukville, and didn't regret my decision in the slightest.</p>

                    <p>There are other beaches in the vicinity, Serendipity, Ochheuteal, Independence, Sokha, but Otres has the reputation as being the quietest and nicest. After visiting the others (briefly, admittedly) I'd definitely agree. There's a higher class of people here. It explains why I fit in so comfortably to be honest... (ahem!). Plus at night Serendipity has ladyboys at the bars. Ladyboys! My first encounter with them. Merely spotting them in the bars and nothing more before anybody gets any ideas... I didn't think I'd need to worry about this until Thailand, but apparently 'snook' has a thriving ladyboy population also.</p>

                    <p>I'd planned to sell my bike in Sihanoukville so I asked around at some bars along the beach to see if anyone was interested. I was offered $150 by the owner of one of the restaurants there, which wasn't bad but I was pretty sure I could get more for it if I went to the trouble of printing out for sale posters, riding into the main town and sticking them up on the walls of bars and hostels. The decision should have been a no-brainer, but by this time i'd already found my way back to a sun lounger on the beach and Otres Beach easily magnifies my laziness. It was very hard indeed to force myself to get up to go to the toilet, let alone make the trip into town to walk around posting up flyers. Common sense prevailed however, and I knew I had to do it if I wanted some extra dollar (which I did!). The effort paid off, and I managed to get $225 for it, which I was very happy with. I was sad to say goodbye to my beautiful 'Win', but the time had come for us to part ways and start seeing other people.</p>

                    <p>I really did very little else with my time in Sihanoukville, other than relaxing on the beach and swimming. I probably could have found plenty to do, had I wanted to... but Otres is the kind of place where you feel entirely comfortable with doing very little. Besides, after my accident I figured I deserved some rest, before heading back to Phnom Penh to collect the camera battery and charger I left in the wall socket of the hostel I stayed in (oops).</p>

