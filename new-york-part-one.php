                    <p>The girls were from Louisiana, Ohio, but had come to the city for an adventure and had been living and working there for nearly a year. King is their hyperactive, rescued pitbull. They were great fun, as you can see from the photos, which is just what I wanted, to end the year on a high note.</p>

                    <p>Amber is a huge Game of Thrones fan, and there was an exhibition running while I was there, displaying the props/costumes etc. The following day after all of us woke a little late (I was catching up on much needed sleep and they were both off work that day), we made our way along there intending to check it out.</p>

                    <p>Funnily enough, on the subway there, after just talking about how great it is to see performers and musicians on the subway, at the very next stop a bunch of guys with bongos got on and yelled “good afternoon!” to the carriage. We were the only ones who replied! It reminded me of the tube in London where everybody is in their own little unresponsive bubble. They sat down and played and they got off at the same stop as us. After tipping them, we chat to them for a while and they said how surprised they were that people had said hello to them, until they found out we weren’t from New York and then they weren’t surprised at all!</p>

