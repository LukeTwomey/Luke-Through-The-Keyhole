                    <p>I hasten to add - this wasn’t all consumed in one day.... more like two.</p>

                    <p>So after feeling cold again at the zoo, I realised I needed to buy some cold weather clothes. I’d nearly made it one year without needing them, but it was just too cold in New York! I went out and bought a cheap coat, hat, gloves, and plimsol type shoes - emphasis on cheap as I already had no money!</p>

                    <p>After getting warm I hopped aboard the Staten Island ferry. I didn’t particularly want to go to Staten Island, but due to Hurricane Sandy, this was the cheapest (free!) way to get near to The Statue Of Liberty. The hurricane had damaged Ellis Island, where the statue stands, and it was closed for repairs for the foreseeable future. Disappointing for me, as I’d wanted to go up to the top of the statue, but a photo from the ferry would have to do! It also gave a nice view back across to Manhattan.</p>

