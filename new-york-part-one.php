                    <p>You know when you fly from one country to another, and stupidly wear something that was completely acceptable at your departure point, but wildly inappropriate at your destination? Well, I did that. I received some strange looks on the subway, sitting there in shorts, flip flops, and my fleece thrown on for good measure. Thick coats and scarves were the order of the day, and after exiting the subway I could see why. I was absolutely freezing. After buying my SIM card I managed to pull some socks and my sandals out of my overloaded bag, and cover my frostbitten toes. I had trousers in there somewhere but there was no way I was going to pull everything out of my bag and repack it, and I had nowhere to change into them anyway. After a cold onward journey, I reached my hosts, Amber, Emily and King.</p>

