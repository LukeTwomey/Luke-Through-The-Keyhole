                    <p>My (essential) nighttime visit would take place another time, as I made my way back to the house, picking up some beer on the way, chilling with the girls watching a movie, drinking, and ordering takeaway. Ahhhhhh...... lovely!</p>

                    <p>I had pre-booked my place for the following morning - the 9/11 memorial site understandably has extremely strict security measures in place. This includes reserving your visiting time slot online beforehand - registering all your details, and a comprehensive airport-esque security check on your arrival at the memorial. After making your way through security, you come out on a large expanse of land, in stark contrast to the congested blocks surrounding you. In the footprints of where the towers stood, two large pools have been constructed, with water cascading down the edges before sinking into a hole in the middle to repeat the process. The names of all the victims are carved into bronze panels surrounding the pools, allowing the public to stand and pay their respects.</p>

                    <p>While this is a memorial, the sheer number of people there does mean it isn’t exactly quiet, and all the people taking photos (guilty of this myself) does make it seem like more of a tourist attraction unfortunately. It’s a sad place to be when you think of all the people who lost their lives there, but overall I think what they’ve done with the memorial there is appropriate and fitting. The new tower is called One World Trade Center (with the crane on top in the Manhattan skyline photo I included earlier) and was actually finished just after I left. It now stands higher than the Empire State Building at 1776 feet.</p>

