                    <h1>New York - Part One</h1>
                    <p class="post-date">29th July 2013 17:21</p>

                    <p>40 minutes on the bus to Auckland airport, 3 and a half hours on the plane to Sydney, 13 and a half hours from Sydney to Los Angeles, 5 hours from LA to New York, 30 minutes on the wrong train doing a lap of the airport terminals, 45 minutes on the subway to Manhattan and the nearest T-Mobile store for my 3G SIM card (priorities!) and finally 40 minutes on the subway to Brooklyn and my couchsurfing hosts.</p>

                    <p>So after what felt like possibly the longest 24 hours of my life, I was in NEW YORK BABY!</p>

                    <a href="images/posts/new-york/img_2659_8660502279_o-large.jpg" class="fancybox">
                        <img src="images/posts/new-york/img_2659_8660502279_o.jpg"/>
