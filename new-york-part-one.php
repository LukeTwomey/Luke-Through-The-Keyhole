                    <p>The subway train gave me my first real taste of the city. In a relatively short period of time, three different sellers peddling quality merchandise made their way through the carriages. One was selling various snacks for $1 - bargain (apparently). The following lady was selling chocolate bars. I was sensing a confectionary theme to the proceedings, but that was blown out of the water by the “Smell Good Man”! “I’ll make ya smell GOOD!” he loudly advertised. “Sometimes when ya turn a corner in this subway, it’ll blow ya HEAD OFF!”. I was learning a hell of a lot about the New York City population already... He concluded his act, and having made no sales of his homemade fragrances, trudged along to the next carriage to repeat the performance. I knew instantly I was going to love New York every bit as much as I expected.</p>

                    <a href="images/posts/new-york/img_1387_8661652656_o-large.jpg" class="fancybox">
