                    <p>This sail is a huge, unruly beast and Sammy was in two minds about whether or not to actually put it up, as it can be quite dangerous if not handled correctly and it somehow gets caught up or ends up in the water. That's why when it came to putting it back down, all our help was enlisted as we sat at the front side of the boat in a row, ready to pull in the sail onto our laps as it was lowered. All went without a hitch, and we safely made it back to the harbour on engine power, as by then the wind had died down completely.</p>

                    <p>This was one of the best things I've done so far, and yes... I now need a boat. I probably should learn how to sail first, but a boat is definitely in my future. And a nice top floor apartment, with a balcony overlooking the marina where my beautiful boat is moored up. That's the dream! One day...</p>

