                    <p><p>You could literally run your hand along the body of this fish as it was swimming along, take a deep breath, dive down and swim along with it and it wouldn't care in the slightest. It was by far the best snorkelling experience I've had. It's remarkable being in the water at the same time as something that big - even though it probably pales into insignificance compared to some beasts! It was also a manic experience when the deckhands would throw chunks of bread into the water at you from the dinghys, and you would instantly be surrounded by hundreds of smaller fish all wanting a piece of the action. Quite incredible, and so much fun.</p>

                    <p>After emerging from the water, we continued on to our next anchorage for the night. Another beautiful sunset, then a nice (nearly) full moon illuminating the boat at night:</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-14_8260815702_o-large.jpg" class="fancybox">
                        <img src="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-14_8260815702_o.jpg"/>
                    </a>

                    <p>Rising the next morning, we moved a short way away from where we'd stopped to the last reef of the trip. Unfortunately, the visibility here wasn't so good, so we didn't spend quite so long in the water as the previous day. We clambered back on board and began our journey back home to Airlie Beach, on the way putting up the spinnaker sail.</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-19_8260843224_o-large.jpg" class="fancybox">
