                    <p>The beach is something like 98% pure silica, which is why the sand is so white, and also how it stays so cool. Unlike most beaches that become a nightmare to walk on in the midday sun, this was perfect as it remained cool no matter how hot it became. We sunbathed here for a while, with stingrays in the water only a metre or two away.</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-4_8260796618_o-large.jpg" class="fancybox">
                        <img src="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-4_8260796618_o.jpg"/>
