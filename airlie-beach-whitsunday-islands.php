                    <p>OK not really, if you look closely you can see the wheel was still tied - we were still anchored at this point! We got underway with the real skipper at the wheel, and sailed off to our next snorkelling spot. The best one so far, easily! This reef was home to a giant maori wrase (pic stolen from google):</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/camera-roll-725_8388874666_o-large.jpg" class="fancybox">
                        <img src="images/posts/airlie-beach-whitsunday-islands/camera-roll-725_8388874666_o.jpg"/>
