                    <p>When our time was up, we reluctantly made our way back to the boat, to continue our journey around the islands. Before we left we managed to catch sight of a few green turtles though, as they surfaced for air. Much like all the other marine life I've tried to photograph along the way (dolphins in Cambodia, crocodiles in Darwin), these things were a nightmare to get a photo of, but luckily a couple came up and stuck around long enough to get a few decent shots:</p>

