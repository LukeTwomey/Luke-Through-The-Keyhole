                    </a>

                    <p>She is a mini maxi, holding up to twelve people. Some of the bigger party catamarans can hold around one hundred... 'nuff said.</p>

                    <p>We hopped on board, and met our skipper Sammy and deckhand Taylor. They took us through the do's and don'ts for the boat, the big one being don't go on the suicide side of the boat (nearest the water) when you're leaning over at a ridiculous angle sailing along, as you'll probably fall in. And that's bad. Oh, wait - sorry I forgot the number one rule of the boat was actually "stay sexy at all times". An important requirement when sailing!</p>

                    <p>As we left the harbour, we put the sail up for the first time. I helped with one other guy to pull the rope down through the mast, which hoists the sail up to the top. It gets harder the further up it goes, as there's more weight you need to pull. Good fun though, and the most exercise I'd had in a few weeks! With the sails up, the boat began to lean over heavily. It's such a strange feeling at first but you soon get used to walking around at an angle after a while. It's very odd being down below and looking out, as everything is all wonky compared to the horizon, The cooker swings on hinges though, to keep anything cooking on there level, which I thought was pretty clever.</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-20_8259652877_o-large.jpg" class="fancybox">
                        <img src="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-20_8259652877_o.jpg"/>
                    </a>

                    <p>After some time, the wind died down substantially and we took the sails down, switching instead to running the engine to keep us moving. That was actually the only time on the trip we went that far sideways, as although we put the sail up plenty of other times, the wind was blowing at a different angle, which meant we kept level but sailed at the same speed.</p>

                    <p>We arrived at a small cove a little while later, where we were to go for our first snorkel of the trip. This was just a little "warm up" as it wasn't the best location, but was convenient to get to and and on the way to where we needed to moor up for the night. We were handed our snorkels and taken off the back of the boat closer to shore, on the small dinghy we had been trailing along behind us. Jumping off the side of the boat backwards (navy style!), we spent some time in the water before getting called back to the boat. IT wasn't the best place, as expected - but we still saw a good few fish while we were there. We were promised better visibility at the other places we would stop at over the next couple of days.</p>

                    <p>We continued on to Hooke Passage, where we dropped anchor and came to rest at our stop for the night. There were a few other boats around us, as this was the best place to hold tight for the night, in smooth waters sheltered from the rough waves. We watched the sunset from here and it was such a chilled out moment, it was so nice. Sitting there with a beer in your hand, bobbing very gently on the water and enjoying the view, it was amazing.</p>

