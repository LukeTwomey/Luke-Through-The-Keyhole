                    <p>It wasn't quite so amazing when we went to bed that night though, it was then I discovered I had the world's loudest snorer in the bunk below me! Ah well, you win some you lose some. It still didn't stop me from sleeping! The whole boat burst out laughing when he let out the loudest fart in the world though, it literally rocked the boat.</p>

                    <p>Waking up nice and early the next morning, we rose anchor and began sailing to Whitehaven Beach. This beach is something like number two in the list of best beaches in the world, and I can really see why. We did ask Sammy what number one was, but his only reply was "it doesn't matter". When we got there, we saw why.</p>

