                    <p>I spent a lot of time online researching which boat to actually book onto. There are a huge number to choose from, and it would really be impossible to decide without seeing what others have had to say about their past experiences on each one. I wanted to avoid a party boat - as I was paying all that money and could easily just go and get pissed on dry land. After much deliberation I settled on one and phoned the agent to book my place. They were already fully booked! The agent told me about all the other boats they had available instead, so I went back to the drawing board and looked into those ones. They really didn't look as good, and I was pretty disappointed I couldn't get on the one I wanted initially! I phoned back and asked her if she had any space on the next trip for that boat, leaving a couple of days later. She did! I booked it. It would mean hanging around slightly longer than I'd intended to, but I decided it was worth it.</p>

                    <p>So a few days later, I showed up at the marina and met her for the first time, Silent Night:</p>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-18_8260716172_o-large.jpg" class="fancybox">
                        <img src="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-18_8260716172_o.jpg"/>
                    </a>

                    <a href="images/posts/airlie-beach-whitsunday-islands/whitsunday-islands-4_8260889184_o-large.jpg" class="fancybox">
