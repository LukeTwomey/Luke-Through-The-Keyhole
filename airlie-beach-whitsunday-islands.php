                    <h1>Airlie Beach/Whitsunday Islands</h1>
                    <p class="post-date">18th January 2012 01:01</p>

                    <p>I'm buying a boat and no one can stop me!</p>

                    <p>Airlie Beach is packed pull of tourists (as most of the places along the east coast are), shops catering to tourists, and backpacker hostels. It's a fairly lively place, and has plenty of places to get drunk, but really - the draw is the access to the boat tours around the Whitsunday Islands.</p>

